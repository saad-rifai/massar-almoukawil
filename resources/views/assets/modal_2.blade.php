                <div class="section-text">
                    <p>L&rsquo;entrepreneuriat est l&rsquo;un des principaux axes de d&eacute;veloppement. Il rev&ecirc;t une importance majeure, tout particuli&egrave;rement depuis l&rsquo; importante &eacute;volution qu&rsquo;a connue la dynamique entrepreneuriale ces 10 derni&egrave;res ann&eacute;es.&nbsp;</p>
                    <p>Cependant, selon une enqu&ecirc;te men&eacute;e entre 2013 et 2015, le taux de survie global des entreprises apr&egrave;s trois ans d&rsquo;existence au Maroc n&rsquo;est que de 51% ( contre 61% au Canada, 69% en Espagne, et 78% au Royaume Uni ). Ceci met en &eacute;vidence plusieurs dysfonctionnements et facteurs de blocage, dont le principal est le manque d&rsquo;accompagnement.&nbsp;</p>
                    <p>La notion d&rsquo;accompagnement en entrepreneuriat se situe &agrave; plusieurs niveaux. N&eacute;anmoins deux grands th&egrave;mes semblent dominer : les structures (p&eacute;pini&egrave;res, incubateurs, services de valorisation, clubs, associations&hellip;), et les modes d&rsquo;accompagnement (formation, conseil, coaching, tutorat, mentoring, parrainage, essaimage&hellip;)&nbsp;</p>
                    <p>L&rsquo;importance de l&rsquo;accompagnement entrepreneurial, tant au niveau op&eacute;rationnel qu&rsquo;au niveau financier, n&rsquo;est plus &agrave; d&eacute;montrer. Toutefois, l&rsquo;enjeu principal r&eacute;side dans la capacit&eacute; des acteurs publics et priv&eacute;s &agrave; appr&eacute;hender les besoins et les attentes des porteurs de projets et entrepreneurs, en mati&egrave;re d&rsquo;appui et de soutien &agrave; la cr&eacute;ation, au d&eacute;veloppement et &agrave; la p&eacute;rennit&eacute; de l&rsquo;entreprise. De ce fait, il est primordial d&rsquo;&oelig;uvrer pour une meilleure compr&eacute;hension de la cha&icirc;ne de valeur entrepreneuriale en termes de besoins/offres, ainsi qu&rsquo;une ma&icirc;trise des diff&eacute;rentes &eacute;tapes dans le cycle de vie d&rsquo;une entreprise.&nbsp;</p>
                    <p>C&rsquo;est dans cette perspective que s&rsquo;inscrit le projet MASSAR AL MOKAWIL, un guide dont l&rsquo; objectif est la r&eacute;alisation d&rsquo;un diagnostic de l&rsquo;offre d&rsquo;accompagnement actuelle, ainsi que des besoins en mati&egrave;re d&rsquo;appui et d&rsquo;accompagnement entrepreneurial. Cet outil propose une plateforme inclusive et innovante permettant de cartographier l&rsquo;&eacute;cosyst&egrave;me entrepreneurial. Il pr&eacute;sente un circuit complet des diff&eacute;rentes &eacute;tapes par lesquelles passe tout entrepreneur et offre les informations n&eacute;cessaires dont il a besoin pour mener &agrave; bien son projet.&nbsp;</p>
                    <p>Ce guide est le fruit du travail commun entre 5 partenaires de la r&eacute;gion Tanger &ndash; T&eacute;touan &ndash; Al Hoceima, &agrave; savoir la CGEM TTA via la commission &lsquo;&lsquo;D&eacute;veloppement de l&rsquo;entrepreneuriat des TPE et START-UPS&rsquo;&rsquo;, la Soci&eacute;t&eacute; Nationale de Garantie et de Financement des Entreprises &laquo; TAMWILCOM &raquo; via le centre d&rsquo;affaires de Tanger, DAR AL MOUKAWIL via les deux agences de Tanger, le TECHNOPARK site de Tanger et la soci&eacute;t&eacute; NERYOU, via sa plateforme DESKY. Le financement du projet est enti&egrave;rement assur&eacute; par le groupe MAROC FORCE EMPLOI.&nbsp;</p>
                    <p>Les informations qui y sont r&eacute;unis sont issues d&rsquo;une collecte bas&eacute;e sur les sites web et les donn&eacute;es re&ccedil;ues de chaque intervenant dans le processus entrepreneurial. Les donn&eacute;es relatives &agrave; la partie financement sont issues des travaux du guide du financement de la r&eacute;gion TTA &eacute;labor&eacute; conjointement par TAMWILCOM, la CGEM TTA, le CRI TTA et BAM Tanger. La partie relative au financement de l&rsquo;&eacute;conomie verte est issue du guide des programmes de financement et d&rsquo;appui pour la d&eacute;rarbonation et l&rsquo;&eacute;conomie verte r&eacute;alis&eacute; par CLUSTER SOLAIRE en partenariat avec AMEE avec le soutien du Minist&egrave;re de l&rsquo;Industrie et du Commerce. La d&eacute;marche et la cartographie sont l&rsquo;aboutissement des &eacute;tudes et de l&rsquo;analyse faites par le groupe de travail en charge de ce projet.&nbsp;</p>
                    <p>Le groupe de travail &oelig;uvrera pour maintenir &agrave; jour le guide et sa plateforme num&eacute;rique avec toutes les nouveaut&eacute;s concernant le domaine entrepreneurial, au niveau de notre pays en g&eacute;n&eacute;ral et de la r&eacute;gion nord en particulier.&nbsp;</p>
