                        Présentation
                    </h2>
                    <p class="section_normale_text">
                        La banque de projets industriels lancée par le Ministère de l’industrie et du commerce s’inscrit
                        dans le cadre du nouveau
                        plan de relance industriel (2021-2023). Cette nouvelle stratégie repose sur l’instauration d’une
                        politique de substitution
                        aux importations, le renforcement du capital marocain dans l'industrie, et la décarbonation de
                        l'industrie pour préserver
                        et renforcer l'export. <br><br>
                        Plusieurs branches industrielles sont représentées à travers une centaine de projets exposés
                        sous forme de fiches-projets.
                        L’objectif des fiches est de donner une vision globale des principales opportunités
                        d’accélération du marché intérieur.
                        L’ambition est de rendre accessible les perspectives générales basées sur des données publiques
                        et des entretiens avec
                        des experts et des industriels de chaque filière. <br><br>
                        Une cellule appelée War-Room composée de plus de 25 personnes dédiées et plusieurs Directeurs
                        centraux a été mise
                        en place au sein du Ministère pour accompagner les porteurs de projets dans les différentes
                        phases de développement de
                        leurs projets.
                    </p>
                </div>

