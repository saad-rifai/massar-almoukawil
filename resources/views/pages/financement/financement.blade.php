                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Le financement par fonds propres
                    </h2>
                    <p class="section_normale_text">
                        Le financement par fonds propres peut prendre plusieurs formes

                        <br>
                        <br>
                        <strong>L’autofinancement</strong>
                        <br>
                        <br>Il s’agit d’opérations de désinvestissement, la plus-value de cession après impôt constitue
                        également
                        une ressource de financement interne obtenue suite à la cession des actifs immobilisés de
                        l’entreprise.
                        Elle peut être le résultat d’une réorientation stratégique de certaines activités ou tout
                        simplement de
                        renouvellement d’immobilisation. Cependant, les cessions ne peuvent constituer une ressource
                        récurrente significative

                        <br><br>
                        <strong>
                            L'augmentation du capital
                        </strong>
                        <br>
                        <br>
                        L’augmentation du capital constitue une création de nouvelles actions achetées par des
                        actionnaires
                        déjà présents dans l'entreprise ou bien par de nouveaux actionnaires. Au niveau des PME, elle
                        est
                        généralement fermée, étant donné que les actionnaires majoritaires sont souvent soucieux de la
                        préservation de leur indépendance et de leur contrôle. Il existe plusieurs modalités
                        d’augmentation
                        du capital :
                    </p>
                    <ul class="section_normale_list">
                        <li>L'augmentation de capital par apports en numéraire</li>
                        <li>L'augmentation de capital par apports en nature </li>
                        <li>L'augmentation de capital par incorporation de réserves</li>
                        <li>L’augmentation de capital par conversion de dettes</li>
                    </ul>
                </div>
                <hr class="section_hr">
                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Le love money
                    </h2>
                    <p class="section_normale_text">
                        Le love money est un terme qui désigne les fonds à récupérer en faisant participer les amis, la
                        famille
                        ou une quelconque connaissance au capital de l’entreprise. C’est un moyen de financement assez
                        utilisé mais il n’est pas aisé de réunir des chiffres probants permettant l’appréciation de la
                        valeur de
                        ce type de financement dans les PME. Toutefois, cet apport reste inclus généralement dans les
                        fonds
                        propres ou les apports en compte courants associés. <br> <br>
                        A noter que l’Etat, par le billet d’incitations fiscales, encourage fortement la capitalisation
                        des
                        entreprises. Une des mesures phares dans ce cens est le maintien dans la loi de finances 2019 de
                        la
                        limitation au montant forfaitaire de 1.000 DH pour les droits d’enregistrement de toute
                        augmentation
                        de capital inférieure à 500.000 DH. La mesure vise à encourager les PME à renforcer leurs fonds
                        propres et indirectement améliorer leur accès au financement. Beaucoup de ces entreprises
                        présentant
                        souvent des bilans déséquilibrés par le recours abusif aux avances en compte courant d’associés.
                        <br>
                        Le recours massif au financement par fonds propres dans la PME marocaine nait de la volonté des
                        entrepreneurs de préserver le contrôle de l’entreprise, de la réticence envers d’avantage de
                        transparence financière ou encore de la difficulté d’accès aux outils de financement. <br> Mais
                        il est
                        extrêmement rare d'avoir les moyens suffisants pour un éventuel investissement et fonctionnement
                        d’une PME et il est parfois plus rentable de se financer par dette lorsque les taux d’intérêt
                        sont à des
                        niveaux assez bas (Effet de levier).
                    </p>
                </div>


                <hr class="section_hr">
                <div class="text-containet">
                    <h2 class="blue-section-title">
                        La recherche d’un associé
                    </h2>
                    <p class="section_normale_text">
                        Une PME peut rechercher un partenaire avec lequel travailler et pouvoir se développer avec
                        toutefois
                        la condition de lui offrir une participation dans le capital. En création ou en développement,
                        un associé
                        peut permettre d’assurer d’avantage par ces propres compétences (partenaire métier) ou par ces
                        propres moyens financiers (partenaire investisseur) l’avenir de l’affaire, d'augmenter ces
                        chances de
                        réussite et l’aboutissement de ces projets. <br>
                        L’inconvénient majeur de ce type de financement, c’est la perte du contrôle total sur
                        l’entreprise et
                        l’obligation de partage de décision, d’où l’intérêt de bien choisir le type juridique de société
                        et de
                        ficeler de manière très précise les statuts juridiques validant l’association afin d’anticiper
                        toutes les
                        éventuelles sources de désaccords futures.
                    </p>
                </div>


                <hr class="section_hr">
                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Le crédit commercial
                    </h2>
                    <p class="section_normale_text">
                        C’est un crédit accordé à l'acheteur par le fournisseur dans le cadre d’un contrat commercial.
                        <br> Selon
                        les pays, les branches d'activité et les accords particuliers passés entre un fournisseur et son
                        client, il
                        peut être convenu (en matière commerciale) que le fournisseur soit payé au terme d'un délai
                        déterminé
                        (30 à 90 jours le plus souvent, voir plus longtemps dans certains cas). A signaler que
                        l’entreprise
                        profite de ce financement quand elle est en position d’achat mais devra également le subir quand
                        elle
                        est en position de vente.
                    </p>
