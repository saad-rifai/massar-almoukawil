                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Description
                    </h2>
                    <p class="section_normale_text">
                        L’appui de l’Etat porte sur la participation dans les frais de réalisation et de mise en œuvre du plan d’actions marketing et commerciales visant à développer les exportations de l’entreprise bénéficiaire dans ses marchés cibles.    
                    </p>
                </div>
                <div class="text-containet">
                    <div class="row">
                        <div class="col">
                            <h2 class="blue-section-title">
                                Cible
                            </h2>
                            <p>
                                <ul>
                                    <li>Entreprises confirm&eacute;es &agrave; l&rsquo;export dont le chiffre d&rsquo;affaires &agrave; l&rsquo;export compris entre 50 et 500 millions de DH/an&nbsp;;</li>
                                    <li>Entreprises exportatrices &eacute;mergentes dont le chiffre d'affaires &agrave; l'export est inf&eacute;rieur &agrave; 50 millions de dirhams/an.</li>
                                    </ul>
                              </p>

                        </div>
                        <div class="col">
                            <h2 class="blue-section-title">
                                Critères d’éligibilité
                            </h2>
                            <p class="section_normale_text">
                                <ul>
                                    <li>Les entreprises de droit marocain ayant au minimum deux ann&eacute;es d&rsquo;activit&eacute; au moment du d&eacute;p&ocirc;t de la demande.</li>
                                    <li>Les entreprises, personnes morales uniquement, soumissionnant &agrave; titre individuel, ou dans le cadre d&rsquo;un consortium ou d&rsquo;un groupement d&rsquo;entreprises</li>
                                    <li>Les entreprises ayant un chiffre d&rsquo;affaires &agrave; l&rsquo;export inf&eacute;rieur &agrave; 500 MDH :</li>
                                    <li>Confirm&eacute;es &agrave; l&rsquo;export (entre 50 et 500 MDH)</li>
                                    <li>Exportatrices &eacute;mergentes (inf&eacute;rieur &agrave; 50 MDH)</li>
                                    </ul>
                                    <p>Les entreprises ayant une situation r&eacute;guli&egrave;re vis-&agrave;-vis du Registre de Commerce, de l&rsquo;Administration fiscale, de la CNSS et de l&rsquo;AMO</p>

                            </p>
                        </div>

                    </div>

                    <br>
                    <div class="row">
                        <div class="col">
                            <h2 class="blue-section-title"> Plafond de financement </h2>
                            <p>
                                <p><strong>Montant de l&rsquo;appui :</strong></p>
<p>10% du chiffre d&rsquo;affaires additionnel &agrave; l&rsquo;export pour une ann&eacute;e donn&eacute;e, pendant 3 ans. Il est plafonn&eacute; annuellement &agrave; :</p>
<ul>
<li>5 MDH pour les exportateurs confirm&eacute;s</li>
<li>2 MDH pour les exportateurs &eacute;mergents</li>
</ul>
<p>Ce montant n&rsquo;est accord&eacute; que sous condition de r&eacute;alisation, par l&rsquo;entreprise, d&rsquo;une croissance additionnelle minimale de chiffre d&rsquo;affaires &agrave; l&rsquo;export.</p>
<p><strong>Part de participation de l&rsquo;&eacute;tat :</strong></p>
<p>La participation aux charges de r&eacute;alisation du projet de croissance &agrave; l&rsquo;export, pour la premi&egrave;re ann&eacute;e, est r&eacute;partie de la mani&egrave;re suivante :</p>
<ul>
<li>Part de l&rsquo;Etat : 80% du montant des d&eacute;penses valid&eacute;es</li>
<li>Part de l&rsquo;entreprise : 20% du montant des d&eacute;penses valid&eacute;es</li>
</ul>
<p>La part de la participation de l&rsquo;Etat au titre des deux ann&eacute;es suivantes sera index&eacute;e au degr&eacute; de r&eacute;alisation des objectifs de croissance &agrave; l&rsquo;export.</p>
                            </p>
