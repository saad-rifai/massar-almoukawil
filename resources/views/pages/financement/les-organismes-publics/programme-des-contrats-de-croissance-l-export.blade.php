                        <p class="section_normale_text">
                            <p>Pour b&eacute;n&eacute;ficier du programme, l&rsquo;entreprise passera par un processus de s&eacute;lection constitu&eacute; de trois phases :</p>
                            <ul>
                            <li>La phase de pr&eacute;s&eacute;lection est la phase durant laquelle sera &eacute;valu&eacute; la conformit&eacute; administrative de l&rsquo;entreprise soumissionnaire.</li>
                            <li>La phase de s&eacute;lection est la phase au cours de laquelle seront &eacute;valu&eacute;es la pertinence, l&rsquo;ambition et la faisabilit&eacute; du business plan &agrave; l&rsquo;export de l&rsquo;entreprise.</li>
                            <li>La phase de contractualisation permet d&rsquo;&eacute;laborer le cadre contractuel liant l&rsquo;entreprise &agrave; l&rsquo;Etat selon les sp&eacute;cificit&eacute;s de chaque entreprise et de signer le contrat de croissance &agrave; l&rsquo;export entre l&rsquo;Etat et l&rsquo;entreprise.</li>
