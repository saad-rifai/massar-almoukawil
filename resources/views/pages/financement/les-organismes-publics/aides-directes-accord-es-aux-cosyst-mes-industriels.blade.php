                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Objet
                    </h2>
                    <p class="section_normale_text">
                    
                        Subvention à l’investissement accordé aux écosystèmes industriels.
                        
                    </p>
                </div>
                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Description
                    </h2>
                    <p class="section_normale_text">
                        Le Fonds de Développement Industriel et des Investissements (FDII) a été créé pour le financement des programmes relevant du Plan d’Accélération Industrielle (PAI). <br>
                        Le FDII permet au tissu industriel de se consolider, de se moderniser et de développer sa capacité de production par rapport aux produits importés.
                        
                        
                    </p>
                </div>
                <div class="text-containet">
                    <div class="row">
                        <div class="col">
                            <h2 class="blue-section-title">
                                Cible
                            </h2>
                            <p>
                                <p>Peuvent b&eacute;n&eacute;ficier du FDII deux cat&eacute;gories de projets : &laquo; Strat&eacute;gique &raquo; et &laquo; Structurant &raquo;.</p>
                                - Projets strat&eacute;giques&nbsp;: <br>
                                &middot; Projet &laquo; Locomotive &raquo; port&eacute; par un acteur cl&eacute; qui vise l'expansion et le d&eacute;veloppement de ses activit&eacute;s ayant un impact significatif et structurant sur le tissu de ses fournisseurs ou ses clients et permettant d'am&eacute;liorer la comp&eacute;titivit&eacute; globale de son &eacute;cosyst&egrave;me et son ancrage en profondeur au Maroc.
                                <ul>
                                <li><strong>Projet &laquo; Donneur d'ordres &raquo; :&nbsp;</strong>Projet port&eacute; par un donneur d'ordres adjudicataire d'une commande publique qui vise &agrave; r&eacute;aliser des mesures de compensation industrielle ayant un impact &eacute;conomique important en termes, notamment, d'investissement direct ou en joint-venture, de formation, de R&amp;D ou de transfert de technologie.</li>
                                </ul>
                              - &laquo; Projets Structurants &raquo;&nbsp;: <br>
                                <ul>
                                <li><strong>Projet &laquo; Pionnier &raquo; :</strong> qui vise le d&eacute;veloppement d&rsquo;une activit&eacute; n&rsquo;ayant jamais &eacute;t&eacute; r&eacute;alis&eacute;e au Maroc ou compl&eacute;tant la cha&icirc;ne de valeur d&rsquo;une fili&egrave;re dans le cadre d&rsquo;un &eacute;cosyst&egrave;me.</li>
                                <li><strong>Projet &laquo; Sourcing &raquo; :</strong>port&eacute; par une entreprise locale qui vise &agrave; se qualifier pour assurer le sourcing des donneurs d&rsquo;ordres soit au niveau local soit &agrave; destination des sites internationaux.</li>
                                <li><strong>Projet de centres d'ing&eacute;nierie et R&amp;D :</strong>ayant pour vocation la cr&eacute;ation de centres de conception, de d&eacute;veloppement, de tests et des essais au profit des fili&egrave;res industrielles.</li>
                                <li><strong>Projet &laquo; Valorisation des ressources &raquo; :</strong> permettant la valorisation des ressources naturelles et d&eacute;chets valorisables qui contribue &agrave; la cr&eacute;ation de la valeur ajout&eacute;e.</li>
                                <li><strong>Projet &laquo; Int&eacute;gration locale &raquo; :</strong>port&eacute; par un donneur d&rsquo;ordres qui vise &agrave; d&eacute;velopper ses activit&eacute;s pour son sourcing depuis le Maroc ou un acteur industriel bas&eacute; au Maroc permettant d&rsquo;am&eacute;liorer le taux d&rsquo;int&eacute;gration locale.</li>
                                </ul>
                            </p>

                        </div>
                        <div class="col">
                            <h2 class="blue-section-title">
                                Critères d’éligibilité
                            </h2>
                            <p class="section_normale_text">
                                <p><strong>Seuils d'&eacute;ligibilit&eacute;</strong> <strong>Pour les projets strat&eacute;giques&nbsp;locomotive :</strong></p>
                                <ul>
                                <li>Investissement sup&eacute;rieur &agrave; 50 MDH HT<br /> <em>Ou</em></li>
                                <li>Cr&eacute;ation d'au moins 200 emplois stables.</li>
                                </ul>
                                <p><strong>Seuils d'&eacute;ligibilit&eacute;</strong> <strong>Pour les projets strat&eacute;giques donneur d&rsquo;ordres :</strong></p>
                                <p>Une commande publique d'au moins 200 MDH HT.</p>
                                <p><strong>Seuils d'&eacute;ligibilit&eacute; pour les projets de la cat&eacute;gorie "Projet structurant" :</strong></p>
                                <ul>
                                <li>Investissement sup&eacute;rieur &agrave; 20 MDH HT</li>
                                <li>Ou cr&eacute;ation d&rsquo;au moins 50 emplois stables.</li>
                                </ul>
                                <p>&nbsp;</p>
                            </p>
                        </div>

                    </div>
            
                    <br>
                    <div class="row">
                        <div class="col">
                            <h2 class="blue-section-title"> Plafond de financement </h2>
                            <p>
                                <ul>
                                    <li>Une prime globale &agrave; l&rsquo;investissement mat&eacute;riel et immat&eacute;riel pouvant aller jusqu&rsquo;&agrave; 30% du montant total d&rsquo;investissement hors taxes. Cette prime peut couvrir les rubriques suivantes :
                                    <ul>
                                    <li>Co&ucirc;t d&rsquo;acquisition ou de location du foncier, des b&acirc;timents professionnels et &eacute;quipements industriels ;</li>
                                    <li>Frais d&rsquo;assistance technique ;</li>
                                    <li>D&eacute;penses relatives &agrave; la recherche et d&eacute;veloppement, &agrave; l&rsquo;innovation/cr&eacute;ation (y compris les frais de d&eacute;veloppement de nouveaux mod&egrave;les/collections, prise en charge des salaires des comp&eacute;tences, frais de contr&ocirc;le et tests, achats de collection, expertise externe li&eacute;e &agrave; l&rsquo;innovation et &agrave; la cr&eacute;ativit&eacute;) ;</li>
                                    <li>Frais d&rsquo;amor&ccedil;age pour les trois premi&egrave;res ann&eacute;es suivant le d&eacute;marrage effectif de l&rsquo;activit&eacute;.</li>
                                    </ul>
                                    </li>
                                    <li>Une prime de croissance &agrave; l&rsquo;export pouvant aller jusqu&rsquo;&agrave; 10% du chiffre d&rsquo;affaires additionnel &agrave; l&rsquo;export</li>
                                    <li>Une prime annuelle de substitution aux importations : les entreprises faisant partie d&rsquo;un &eacute;cosyst&egrave;me ayant transf&eacute;r&eacute; au moins 60% des achats de leurs intrants initialement r&eacute;alis&eacute;s &agrave; l&rsquo;&eacute;tranger vers des fournisseurs industriels install&eacute;s au Maroc peuvent b&eacute;n&eacute;ficier d&rsquo;une prime annuelle de substitution aux importations pouvant aller jusqu&rsquo;&agrave; 2% de ces achats.</li>
                                    </ul>
                                    <p>Une aide annuelle d&rsquo;installation au Maroc : les entreprises &eacute;trang&egrave;res non bas&eacute;es industriellement au Maroc, clientes des entreprises relevant d&rsquo;&eacute;cosyst&egrave;mes ayant fait l&rsquo;objet de contrat de performance, qui ouvrent des bureaux de repr&eacute;sentation au Maroc, peuvent b&eacute;n&eacute;ficier, sur la p&eacute;riode 2015-2020, d&rsquo;une aide annuelle d&rsquo;incitation &agrave; l&rsquo;installation au Maroc pouvant aller jusqu&rsquo;&agrave; 2% du chiffre d&rsquo;affaires annuel additionnel g&eacute;n&eacute;r&eacute; par ses commandes adress&eacute;es au Maroc</p>
                            </p>
                        </div>

