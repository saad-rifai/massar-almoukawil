                    </h2>
                    <p class="section_normale_text">
                        Ce fond a été mis en place dans le cadre de la charte d’investissement, dans l’objectif d’accorder un soutien financier aux investisseurs, à travers la prise en charge partielle du coût lié au terrain et aux bâtiments ainsi qu’aux équipements neufs nécessaires à la réalisation de nouveaux projets, dans des secteurs choisis en fonction de leur intérêt en matière de création d’emploi et de développement des exportations. 

                    </p>
                </div>
                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Description
                    </h2>
                    <p class="section_normale_text">
                        Fonds Hassan II contribue à la mise en œuvre des stratégies de promotion de l’investissement déployées par les pouvoirs publics, à travers des fonds dédiés et des programmes d’appui, dont la gestion et confiée à des partenaires publics.

                    </p>
                </div>
                <div class="text-containet">
                    <div class="row">
                        <div class="col">
                            <h2 class="blue-section-title">
                                Cible
                            </h2>
                            <p>
                                <p>Investisseurs op&eacute;rant dans les secteurs suivants&nbsp;:</p>
                                <ul>
                                <li>Automobile, A&eacute;ronautique et Electronique.</li>
                                </ul>
                                <p>- La fabrication des &eacute;quipements pour l&rsquo;industrie automobile ;</p>
                                <p>- La fabrication d&rsquo;&eacute;quipements pour&nbsp;l&rsquo;industrie a&eacute;ronautique ;</p>
                                <p>- La fabrication d&rsquo;outils et de moules pour l&rsquo;industrie automobile et pour l&rsquo;industrie a&eacute;ronautique ;</p>
                                <p>- La maintenance a&eacute;ronautique et le d&eacute;mant&egrave;lement des avions ;</p>
                                <p>- La fabrication de composants d&rsquo;ensembles et de sous-ensembles &eacute;lectroniques.</p>
                                <ul>
                                <li>Chimique &amp; Para-chimique&nbsp;:</li>
                                </ul>
                                <p>- La fabrication des produits cosm&eacute;tiques et la fabrication d&rsquo;extraits naturels ;</p>
                                <p>- Le recyclage des d&eacute;chets m&eacute;talliques et plastiques ;</p>
                                <p>- La valorisation de l&rsquo;&eacute;l&eacute;ment phosphore (phosphates).</p>
                                <ul>
                                <li>Pharmaceutique&nbsp;:</li>
                                </ul>
                                <p>- La recherche de nouveaux m&eacute;dicaments innovateurs ayant un potentiel th&eacute;rapeutique luttant contre les maladies graves ;</p>
                                <p>- La fabrication de m&eacute;dicaments innovants, g&eacute;n&eacute;riques ou biosimilaires ayant une haute valeur ajout&eacute;e sur le plan &eacute;conomique et th&eacute;rapeutique.</p>
                                <ul>
                                <li>Nanotechnologie, Micro&eacute;lectronique et Biotechnologie&nbsp;:</li>
                                </ul>
                                <p>- Activit&eacute;s de fabrication li&eacute;es &agrave; la nanotechnologie, &agrave; la micro&eacute;lectronique et &agrave; la biotechnologie</p>
                            </p>

                        </div>
                        <div class="col">
                            <h2 class="blue-section-title">
                                Critères d’éligibilité
                            </h2>
                            <p class="section_normale_text">
                                Peuvent bénéficier de la contribution du Fonds Hassan II, les nouveaux projets d’investissement (création ou extension) dont le montant total d’investissement est supérieur à 10 millions de DH HT (hors droit d’importation et taxes) et à la condition que le montant de l’investissement en biens d’équipement soit supérieur à 5 millions de DH HT (hors droit d’importation et taxes) 
                            </p>
                        </div>

                    </div>

                    <br>
                    <div class="row">
                        <div class="col">
                            <h2 class="blue-section-title"> Plafond de financement </h2>
                            <p>
                                <p>Aides financi&egrave;res, au titre des b&acirc;timents et au titre des &eacute;quipements, plafonn&eacute;es &agrave; 15 % de l&rsquo;investissement total et &agrave; 30 millions de DH comme suit :</p>
                                <p><strong>B&acirc;timents professionnels&nbsp;:</strong></p>
                                <ul>
                                <li>Cas de l&rsquo;acquisition du foncier et construction des b&acirc;timents : 10% du co&ucirc;t d&rsquo;acquisition du foncier et de construction des b&acirc;timents.</li>
                                <li>Cas de l&rsquo;acquisition des b&acirc;timents : 10% du co&ucirc;t d&rsquo;acquisition des b&acirc;timents professionnels.</li>
                                <li>Cas de la location* du foncier et construction des b&acirc;timents : 100% du co&ucirc;t de loyer du foncier calcul&eacute; sur la base des 6 premi&egrave;res ann&eacute;es et d&rsquo;un loyer maximum de 7,5 DH HT/m2/ mois et dans la limite d&rsquo;une contribution unitaire maximale de 540 DH par m2 couvert des b&acirc;timents construits, ou bien 10% du co&ucirc;t de construction des b&acirc;timents.</li>
                                <li>Cas de la location des b&acirc;timents : 100% du co&ucirc;t de loyer des b&acirc;timents professionnels calcul&eacute; sur la base des 2 premi&egrave;res ann&eacute;es et d&rsquo;un loyer maximum de 30 DH HT/m2/mois (Les aides &agrave; la location sont r&eacute;serv&eacute;es aux projets install&eacute;s dans des espaces d&rsquo;accueil industriel dont la liste est arr&ecirc;t&eacute;e par d&eacute;cision du Ministre charg&eacute; de l&rsquo;Industrie, assortie d&rsquo;un contrat de location d&rsquo;une dur&eacute;e minimale de 6 ans)</li>
                                </ul>
                                <p><strong>Biens d&rsquo;&eacute;quipements neufs&nbsp;:</strong></p>
                                <p>Contribution maximale de 20% du co&ucirc;t d&rsquo;acquisition des biens d&rsquo;&eacute;quipement neufs (hors droits d&rsquo;importation et taxes). Les projets d&rsquo;investissement &eacute;ligibles dans les activit&eacute;s de fabrication d&rsquo;&eacute;quipements pour l&rsquo;industrie automobile peuvent b&eacute;n&eacute;ficier d&rsquo;une contribution maximale de 20% &agrave; l&rsquo;acquisition des biens d&rsquo;&eacute;quipement usag&eacute;s import&eacute;s utilis&eacute;s dans les m&eacute;tiers de l&rsquo;emboutissage, de l&rsquo;injection plastique, de fabrication d&rsquo;outils et de moules.</p>
                            </p>
