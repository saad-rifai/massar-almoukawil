                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Objet 
                    </h2>
                    <p class="section_normale_text">
                        Promouvoir l'investissement privé dans le secteur agricole et de l'orienter, par des subventions ciblées, vers des activités permettant une meilleure exploitation du potentiel agricole national. 
                    </p>
                </div>
                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Description
                    </h2>
                    <p class="section_normale_text">
                        En application des dispositions du code des investissements Agricoles, l’Etat accorde des aides financières aux investissements agricoles réalisés par les agriculteurs (personnes physiques ou morales). Elles concernent l’acquisition des facteurs de production, l’aménagement et l’équipement des exploitations agricoles, le développement de l’élevage à travers l’amélioration génétique des races, la construction des bâtiments d’élevage, l’acquisition du matériel d’élevage et le développement des infrastructures de valorisation et la promotion des exportations des produits agricoles.
                        Ces aides sont régies par les textes réglementaires et une instruction conjointe du ministre de l’Agriculture et de la Pêche Maritime et du ministre de l’Economie et des Finances qui a pour objet de définir les modalités de traitement des dossiers de demande de l’aide financière de l’Etat distribuée dans le cadre du Fonds de Développement Agricole (FDA).
                        
                    </p>
                </div>
                <div class="text-containet">
                    <div class="row">
                        <div class="col">
                            <h2 class="blue-section-title">
                                Cible
                            </h2>
                            <p>
                                <p>Les aides directes accord&eacute;es aux agriculteurs englobant&nbsp;:</p>
                                <ul>
                                <li>Les am&eacute;nagements hydro-agricoles (irrigation localis&eacute;e et de compl&eacute;ment) et l&rsquo;am&eacute;lioration fonci&egrave;re (&eacute;pierrage,&nbsp;...)&nbsp;;</li>
                                <li>L&rsquo;&eacute;quipement des exploitations en mat&eacute;riel agricole (m&eacute;canisation, serres, filets anti gr&ecirc;les, &hellip;)&nbsp;;</li>
                                <li>L&rsquo;intensification de la production animale (am&eacute;lioration g&eacute;n&eacute;tique, b&acirc;timents d&rsquo;&eacute;levage, &hellip;)&nbsp;;</li>
                                <li>Le d&eacute;veloppement de l&rsquo;arboriculture fruiti&egrave;re (agrumes, olivier, palmier dattier, rosac&eacute;es, &hellip;)&nbsp;;</li>
                                <li>L&rsquo;encouragement &agrave; l&rsquo;utilisation des semences s&eacute;lectionn&eacute;es (bl&eacute; dur, bl&eacute; tendre, orge, riz et semences monogermes de betterave &agrave; sucre)&nbsp;;</li>
                                <li>Les analyses de laboratoires (sol, eau, plantes et r&eacute;sidus)&nbsp;;</li>
                                <li>La reconversion au syst&egrave;me de production biologique&nbsp;;</li>
                                <li>La construction et l&rsquo;&eacute;quipement des unit&eacute;s de valorisation (conditionnement, entrep&ocirc;ts frigorifiques, trituration, transformation, abattoirs et salles de d&eacute;coupes, valorisation de lait et d&rsquo;&oelig;ufs, &hellip;)&nbsp;;</li>
                                <li>La promotion et la diversification des exportations (agrumes, tomates, huiles d&rsquo;olives, olives de tables, produits conditionn&eacute;s, &hellip;)&nbsp;;</li>
                                <li>Les aides &agrave; l&rsquo;agr&eacute;gation agricole (subvention &agrave; taux bonifi&eacute;s &agrave; la m&eacute;canisation et &agrave; l&rsquo;irrigation et subvention forfaitaire pour l&rsquo;agr&eacute;gateur);</li>
                                <li>La contribution au Fonds des calamit&eacute;s naturelles (FCN);</li>
                                <li>La promotion de l&rsquo;assurance agricole;</li>
                                </ul>
                                <p>Le d&eacute;veloppement des fili&egrave;res de productions, objet de contrat programme sign&eacute;s entre le minist&egrave;re et les diff&eacute;rentes interprofessions agricoles.</p>           </p>

                        </div>
                        <div class="col">
                            <h2 class="blue-section-title">
                                Critères d’éligibilité
                            </h2>
                            <p class="section_normale_text">
                                Les critères d’éligibilité à l’aide financière de l’Etat dans le cadre du FDA sont fixés par l’instruction conjointe pour chaque type d’investissement.
                            </p>
                        </div>

                    </div>

