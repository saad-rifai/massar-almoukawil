                        <h2 class="blue-section-title">Informations supplémentaires </h2>
                        <p class="section_normale_text">
                            <p>Avant la r&eacute;alisation de l&rsquo;investissement, le postulant d&eacute;pose, un dossier de demande de l&rsquo;approbation pr&eacute;alable de son projet, aupr&egrave;s du Guichet Unique ou ses antennes de la DPA ou de l&rsquo;ORMVA du ressort desquels d&eacute;pendent les exploitations agricoles supports des investissements.</p>
                            <p>Apr&egrave;s l&rsquo;approbation pr&eacute;alable et la r&eacute;alisation de l&rsquo;investissement, le postulant d&eacute;pose, dans les d&eacute;lais fix&eacute;s, un dossier de demande de l&rsquo;aide financi&egrave;re, aupr&egrave;s du Guichet Unique ou ses antennes de la DPA ou de l&rsquo;ORMVA du ressort desquels d&eacute;pendent les exploitations agricoles support des investissements.</p>
                            <p>Si le dossier de demande de l&rsquo;aide et les r&eacute;alisations sur le terrain du projet objet de l&rsquo;aide sont jug&eacute;es conformes, le montant de l&rsquo;aide financi&egrave;re de l&rsquo;Etat est accord&eacute; selon un taux et un plafond fix&eacute; pour chaque type d&rsquo;investissements.</p>
