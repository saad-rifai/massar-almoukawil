                            <h2 class="blue-section-title"> Plafond de financement </h2>
                            <p>
                                <p>Irrigation et am&eacute;nagements fonciers : 45 000 DH/Hectare &eacute;quip&eacute;<br /> Semences certifi&eacute;es et plantations fruiti&egrave;res&nbsp;: jusqu&rsquo;&agrave; 28 000 Dh/Hectare<br /> Promotion et diversification des exportations&nbsp;: jusqu&rsquo;&agrave; 4000dh/Tonne</p>
                                <p>Am&eacute;lioration g&eacute;n&eacute;tique&nbsp;: jusqu&rsquo;&agrave; 5000 dh<br /> Unit&eacute;s de valorisation&nbsp;: jusqu&rsquo;&agrave; 21 000 000 Dh<br /> Aides aux projets d&rsquo;agr&eacute;gation&nbsp;: 45 000 DH/Hectare &eacute;quip&eacute;<br /> Equipement des exploitations&nbsp;: jusqu&rsquo;&agrave; 720 000 dh</p>
                                <p>&nbsp;</p>
