                        <h2 class="blue-section-title">Documents nécessaires </h2>
                        <p class="section_normale_text">
                            <p>Pour constituer les dossiers de demande de l&rsquo;aide de l&rsquo;Etat et r&eacute;cup&eacute;rer les mod&egrave;les de documents demand&eacute;s, les postulants sont invit&eacute;s &agrave; se rapprocher du Guichet Unique de la Direction Provinciale de l&rsquo;Agriculture (DPA) ou de l&rsquo;Office R&eacute;gional de Mise en Valeur Agricole (ORMVA) dont rel&egrave;ve l&rsquo;exploitation objet de l&rsquo;investissement.</p>
                            <p>S&rsquo;agissant des aides accord&eacute;es au titre de la commercialisation des semences certifi&eacute;es et de boutures agr&eacute;&eacute;es et des analyses de laboratoires, les soci&eacute;t&eacute;s b&eacute;n&eacute;ficiaires sont appel&eacute;es &agrave; d&eacute;poser leur dossier aupr&egrave;s du minist&egrave;re de l&rsquo;Agriculture et de la P&ecirc;che Maritime (Direction Financi&egrave;re).</p>
                            <p>Les mod&egrave;les des documents &agrave; t&eacute;l&eacute;charger pour constitution du dossier suivant le type du projet sont accessibles via le lien suivant&nbsp;:</p>
                            <p><a href="http://ormvah.com/guichet-unique-fda/imprimes-a-telecharger/" target="_blank" rel="noopener">http://ormvah.com/guichet-unique-fda/imprimes-a-telecharger/</a></p>
                        </p>
