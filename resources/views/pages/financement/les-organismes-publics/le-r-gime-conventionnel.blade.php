                    <p>La subvention du Fonds de D&eacute;veloppement Industriel et des Investissements (FDII- partie
                        &laquo;&nbsp;Promotion des Investissements&nbsp;&raquo;), porte sur la participation de
                        l&rsquo;Etat aux :</p>
                    <ul>
                        <li>D&eacute;penses relatives &agrave; l'acquisition du terrain n&eacute;cessaire &agrave; la
                            r&eacute;alisation du programme d'investissement dans la limite de 20% du co&ucirc;t de ce
                            terrain&nbsp;;</li>
                        <li>D&eacute;penses d&rsquo;infrastructures externes n&eacute;cessaires &agrave; la
                            r&eacute;alisation du programme d&rsquo;investissement&nbsp;(tout &eacute;quipement, hors
                            site, du terrain support du projet en r&eacute;seaux de voiries, d'assainissement,
                            d'alimentation en eau, d'&eacute;lectricit&eacute; ou de
                            t&eacute;l&eacute;phonie,&nbsp;station d'&eacute;puration et terrassement) dans la limite de
                            5% du montant global du programme d&rsquo;investissement&nbsp;;</li>
                    </ul>
                    <p>Frais de la formation professionnelle pr&eacute;vue dans la limite de 20% du co&ucirc;t de cette
                        formation.</p>
                    </p>
                </div>
