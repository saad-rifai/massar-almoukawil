                                    &agrave; 250 ;</li>
                                <li>assurer un transfert de technologie&nbsp;;</li>
                                <li>contribuer &agrave; la protection de l&rsquo;environnement.</li>
                            </ul>
                            <p>Seuls les projets d&rsquo;investissements dans les secteurs de l&rsquo;Immobilier et de
