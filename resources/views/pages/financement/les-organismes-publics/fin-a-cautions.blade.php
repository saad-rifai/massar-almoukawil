                        <h2 class="blue-section-title">Informations supplémentaires  </h2>
                        <p class="section_normale_text">
                            <p><strong>Caution provisoire&nbsp;:</strong> constitue une garantie de l&rsquo;engagement du soumissionnaire de ne pas revenir sur sa d&eacute;cision en cas d&rsquo;attribution du march&eacute;. La caution s&rsquo;&eacute;l&egrave;ve &agrave; 1,5% du montant du march&eacute; permettant &agrave; la TPME de soumissionner &agrave; l&rsquo;appel d&rsquo;offres.<br /> <br /> <strong>Caution d&eacute;finitive&nbsp;:</strong> D&rsquo;un montant se situant &agrave; 3% en g&eacute;n&eacute;ral du montant initial du march&eacute;, cette caution repr&eacute;sente une garantie pour l&rsquo;administration par rapport au respect des engagements contractuels de la TPME, notamment le respect du cahier des charges. Cette caution reste valide jusqu&rsquo;&agrave; la r&eacute;ception d&eacute;finitive du march&eacute; par le donneur d&rsquo;ordre et l&rsquo;ach&egrave;vement des travaux.<br /> <br /> <strong>Caution retenues de garantie&nbsp;:</strong> Cette caution permet de remplacer les retenues de garantie qu&rsquo;effectue l&rsquo;administration, pour se prot&eacute;ger de toute d&eacute;faillance d&rsquo;ex&eacute;cution des obligations de l&rsquo;adjudicataire du march&eacute;. Son montant se situe &agrave; 7% de celui-ci.</p>
                         
                            <p><strong>Caution de restitution d'avances et d'acomptes&nbsp;:</strong> Elle est d&eacute;livr&eacute;e dans le but de garantir au donneur d&rsquo;ordre le remboursement des avances ou des acomptes qu&rsquo;il consent &agrave; l&rsquo;adjudicataire du march&eacute;, pour lui faciliter le financement des travaux faisant objet du contrat pass&eacute; avec lui. Cette caution est de l&rsquo;ordre de 10% du montant du march&eacute;.</p>            </p>
