                    <h2 class="blue-section-title">
                        Définition et démarche de rédaction
                    </h2>
                    <p>Le &laquo;&nbsp;Business Plan&nbsp;&raquo; ou &laquo;&nbsp;Plan d&lsquo;affaire&nbsp;&raquo; ou
                        &laquo;&nbsp;Plan de d&eacute;veloppement&nbsp;&raquo; est le passage oblig&eacute; de la
                        cr&eacute;ation d&rsquo;entreprise nouvelle ou du lancement d&rsquo;un projet nouveau important
                        dans une entreprise existante.</p>
                    <p>Le business plan est important dans tous les secteurs, industriels ou de services,
                        ind&eacute;pendamment de leur degr&eacute; d&rsquo;innovation technologique ou marketing. De nos
                        jours, les projets tendent &agrave; devenir plus importants et plus complexes. Ils doivent
                        &ecirc;tre pr&eacute;sent&eacute;s &agrave; des partenaires ext&eacute;rieurs et &ecirc;tre
                        n&eacute;goci&eacute;s. Le business plan devient alors de plus en plus un outil indispensable
                        pour y parvenir.</p>
                    <p> Le business plan se pr&eacute;sente donc comme une m&eacute;thode de pr&eacute;paration aux
                        projets d&rsquo;entreprise. Il permet d&rsquo;int&eacute;grer, sur un m&ecirc;me document,
                        l&rsquo;ensemble des &eacute;l&eacute;ments constitutifs du projet et de les mettre en
                        ad&eacute;quation avec tous les autres param&egrave;tres et facteurs ext&eacute;rieurs&nbsp;:
                        les donn&eacute;es financi&egrave;res, la communication, le march&eacute;&hellip; Un bon
                        business plan permet surtout de v&eacute;rifier que le projet est viable et
                        d&rsquo;&eacute;laborer des hypoth&egrave;ses pour l&rsquo;avenir. Il permet de constituer
                        int&eacute;gralement votre projet et de le pr&eacute;senter aux partenaires&nbsp;: banques,
                        associ&eacute;s&hellip; Il permet &eacute;galement de s&rsquo;assurer que tous les
                        &eacute;l&eacute;ments ont &eacute;t&eacute; pris en compte dans la construction de votre
                        projet.</p>
                    <p>Voici quelques conseils pour aider &agrave; &eacute;laborer un business plan en abordant sa
                        pr&eacute;sentation, son contenu et enfin quelques &eacute;cueils &agrave; &eacute;viter.</p>
                    <p> La pr&eacute;sentation d&rsquo;un business plan doit &ecirc;tre claire et dynamique. Il se
                        traduit par un document g&eacute;n&eacute;ralement constitu&eacute; de vingt &agrave; cinquante
                        pages et s&rsquo;articule autour de deux parties <a name="_ftnref1"></a>principales :</p>
                    <p>1- Le premier volet int&egrave;gre&nbsp;:</p>
                    <p>- Une sous partie &laquo;&nbsp;qualitative&nbsp;&raquo;, qui expose les choix, la
                        strat&eacute;gie et les plans d&rsquo;action&nbsp;;</p>
                    <p>- Une sous partie &laquo;&nbsp;quantitative&nbsp;&raquo;, contenant les donn&eacute;es
                        chiffr&eacute;es issues de ces choix et plans d&rsquo;actions.</p>
                    <p>2- Le second volet renferme les annexes.</p>
                    <p>Ce qui distingue un bon business plan est sa capacit&eacute; &agrave; relier clairement les
                        parties qualitatives et quantitatives dans un ensemble homog&egrave;ne. Plus il est clair et
                        bien pr&eacute;sent&eacute;, plus il sera efficace. Une pr&eacute;sentation compl&egrave;te,
                        convaincante et attrayante du projet donne aux partenaires une impression favorable et contribue
                        &agrave; sa r&eacute;ussite. Afin de parvenir &agrave; ce r&eacute;sultat, il faut effectuer
                        pr&eacute;alablement toutes les analyses n&eacute;cessaires et avoir inclus tous les arguments
                        susceptibles d&rsquo;influencer le lecteur concernant votre projet, les march&eacute;s, la
                        technologie, les m&eacute;thodes d&rsquo;industrialisation et de commercialisation. Cette
                        analyse devra se faire objectivement et minutieusement.</p>
                    <p> Afin que la construction du business plan ait les meilleures chances de retenir l'attention de
                        son lectorat potentiel, il est recommand&eacute; de pr&ecirc;ter une attention
                        particuli&egrave;re aux quelques points suivants lors de la construction du document :</p>
                    <ul>
                        <li>Exhaustivit&eacute; des informations en en-t&ecirc;te et pied de page ;</li>
                        <li>Num&eacute;rotation des pages ;</li>
                        <li>Libell&eacute;s des titres ;</li>
                        <li>Lisibilit&eacute; et profondeur n&eacute;cessaire du sommaire&nbsp;;</li>
                        <li>Homog&eacute;n&eacute;it&eacute; des polices ;</li>
                        <li>Soins apport&eacute;s &agrave; la couverture ;</li>
                        <li>Mise en &eacute;vidence des contacts n&eacute;cessaires &agrave; l'approfondissement du
                            dossier ;</li>
                        <li>R&eacute;f&eacute;rences du dossier (version, date, r&eacute;f&eacute;rences internes,
                            &hellip;).</li>
                    </ul>
                    <p>La pr&eacute;sentation impeccable du document final devra pr&eacute;voir une "accroche",
                        c&rsquo;est-&agrave;-dire une entr&eacute;e en mati&egrave;re percutante d&eacute;crivant
                        clairement la proposition de valeur, ainsi qu'une note de synth&egrave;se d&eacute;crivant
                        rapidement mais compl&egrave;tement le projet et ses enjeux.</p>
                    <p>Le business plan doit comporter&nbsp;les objectifs du projet et les moyens de les atteindre. Le
                        contenu d'un business plan doit apporter les &eacute;l&eacute;ments de r&eacute;ponse &agrave;
                        cinq familles de questions qui conditionnent la r&eacute;ussite d'un projet de
                        d&eacute;veloppement :</p>
                    <ul>
                        <li>Quelle est la finalit&eacute; du projet de mon entreprise ?</li>
                        <li>Quelle strat&eacute;gie g&eacute;n&eacute;rale cela conditionne-t-il ?</li>
                        <li>Quelles strat&eacute;gies fonctionnelles sont applicables et quels plans d'action y sont
                            attach&eacute;s ?</li>
                        <li>Quels sont les moyens &agrave; mobiliser et quels en sont les objectifs chiffr&eacute;s ?
                        </li>
                        <li>Quelle rentabilit&eacute; financi&egrave;re peut en &ecirc;tre attendue ?</li>
                    </ul>
                    <p>Pour r&eacute;pondre &agrave; l'ensemble de ces questions, il est recommand&eacute; de suivre le
                        triptyque g&eacute;n&eacute;rique suivant :</p>
                    <ul>
                        <li>Pr&eacute;sentation du projet d&rsquo;entreprise, des facteurs cl&eacute;s de succ&egrave;s
                            et avantages concurrentiels, planning de mise en &oelig;uvre, perspectif de
                            rentabilit&eacute; et de d&eacute;veloppement&nbsp;;</li>
                        <li>Analyse des march&eacute;s, environnements concurrentiels, risques et opportunit&eacute;s ;
                        </li>
                        <li>Strat&eacute;gie g&eacute;n&eacute;rale et d&eacute;clinaison en strat&eacute;gies
                            fonctionnelles, plans d'actions, incidences organisationnelles et financi&egrave;res.</li>
                    </ul>
                    <p>Il est clair que les informations significatives d&eacute;pendent de la position du lecteur
                        vis-&agrave;-vis de la soci&eacute;t&eacute; concern&eacute;e&nbsp;: un investisseur, un
                        partenaire commercial, un associ&eacute; dans la recherche&hellip; chacun de ces destinataires
                        aura sa propre vision de ce qu&rsquo;il souhaite voir &eacute;crit et inclus dans
                        &laquo;&nbsp;son&nbsp;&raquo; document. C&rsquo;est en r&eacute;pondant aux attentes du lecteur
                        que vous r&eacute;ussirez &agrave; le convaincre&nbsp;:</p>
                    <ul>
                        <li>Le partenaire financier&nbsp;: souhaite une pr&eacute;sentation tr&egrave;s compl&egrave;te,
                            depuis la description de la soci&eacute;t&eacute; ou de l&rsquo;activit&eacute;
                            jusqu&rsquo;aux projections financi&egrave;res tr&egrave;s d&eacute;taill&eacute;es, ainsi
                            qu&rsquo;une analyse de la rentabilit&eacute;.</li>
                        <li>Le partenaire commercial&nbsp;: s&rsquo;attachera &agrave; la description du produit, aux
                            donn&eacute;es concernant le march&eacute; et la concurrence et &agrave; la contribution de
                            votre propre projet &agrave; sa rentabilit&eacute;.</li>
                        <li>Le partenaire industriel sera sensible aux donn&eacute;es de march&eacute; et de concurrence
                            qui seront fournies.</li>
                    </ul>
                    <p>Les tableaux &laquo;&nbsp;livrables&nbsp;&raquo; &agrave; fournir sont le tableau de formation du
                        r&eacute;sultat et de la capacit&eacute; d&rsquo;autofinancement, un bilan pr&eacute;visionnel,
                        un tableau d&rsquo;impact des co&ucirc;ts par fonction sur le r&eacute;sultat, un plan de
                        tr&eacute;sorerie &agrave; douze mois, un calcul du point mort et un plan de financement
                        &agrave; 7 ans.</p>
                    <p>&nbsp;Enfin, voici quelques &eacute;cueils &agrave; &eacute;viter et recommandations &agrave;
                        suivre dans l&rsquo;&eacute;laboration de votre business plan&nbsp;:</p>
                    <ul>
                        <li>Eviter d&rsquo;&ecirc;tre trop optimiste dans l&rsquo;&eacute;valuation du chiffre
                            d&rsquo;affaires et du point mort&nbsp;: les banquiers ont acc&egrave;s &agrave; des
                            moyennes par profession et par r&eacute;gion. Aussi, calculer le point mort,
                            (c&rsquo;est-&agrave;-dire le moment &agrave; partir duquel le chiffre d&rsquo;affaires sera
                            sup&eacute;rieur aux charges fixes), est un des grands enjeux du business plan. En effet, ce
                            n&rsquo;est qu&rsquo;une fois que l&rsquo;entreprise a d&eacute;pass&eacute; le seuil de
                            rentabilit&eacute; que ses cr&eacute;ateurs peuvent se r&eacute;mun&eacute;rer. Afin
                            d&rsquo;&eacute;viter les mauvaises surprises, il est pr&eacute;f&eacute;rable de prendre en
                            compte les hypoth&egrave;ses les plus d&eacute;favorables.</li>
                        <li>Pr&eacute;senter une ou plusieurs &eacute;tudes de march&eacute; cr&eacute;dibles. Une
                            simple liste des concurrents ne convaincra pas, sauf si elle est comment&eacute;e avec une
                            &eacute;valuation de leur positionnement, d&rsquo;offre et de prix.</li>
                        <li>Mentionner les points faibles ou &laquo;&nbsp;risques&nbsp;&raquo; afin de
                            cr&eacute;dibiliser le business plan. Ceci montre qu&rsquo;ils sont identifi&eacute;s et
                            qu&rsquo;il y a une strat&eacute;gie pour y rem&eacute;dier.</li>
                        <li>Force est de constater que la r&eacute;daction d'un business plan para&icirc;t souvent
                            constituer une exception plut&ocirc;t qu'une r&egrave;gle chez les dirigeants de TPME, ce
                            qui est &agrave; regretter. Le business plan devient un outil de management indispensable de
                            l&rsquo;entreprise moderne. En effet, il est le seul moyen rigoureux de se fixer des
                            objectifs coh&eacute;rents &agrave; atteindre, dont on pourra ensuite mesurer le niveau de
                            r&eacute;alisation.</li>
                    </ul>
