                    <h2 class="blue-section-title"> Modèle de rédaction à suivre</h2>
                    <p><strong>COUVERTURE&nbsp;: (une page)</strong></p>
                    <p>Nom de la soci&eacute;t&eacute;</p>
                    <p>Secteur d'activit&eacute;</p>
                    <p>Nom du responsable du projet</p>
                    <p>Forme juridique</p>
                    <p>Coordonn&eacute;es (adresse postale, t&eacute;l&eacute;phone, fax, e-mail...)</p>
                    <p><strong>SOMMAIRE&nbsp;: (une page)</strong></p>
                    <ol>
                        <li>&nbsp;R&eacute;sum&eacute; du projet</li>
                        <li>&nbsp;Pr&eacute;sentation des cr&eacute;ateurs ou de l'&eacute;quipe</li>
                        <li>&nbsp;Description du produit ou service</li>
                        <li>&nbsp;Analyse du march&eacute;</li>
                        <li>&nbsp;Strat&eacute;gie commerciale et moyens mis en &oelig;uvre</li>
                        <li>&nbsp;Montage juridique</li>
                        <li>&nbsp;Dossier financier</li>
                        <li>&nbsp;Annexes</li>
                    </ol>
                    <p><strong>RESUME DU PROJET&nbsp;: (une &agrave; deux pages)</strong></p>
                    <p><br /> Objectif : donner envie &agrave; l'investisseur d'en savoir plus et le convaincre de
                        l'int&eacute;r&ecirc;t du projet<br /> </p>
                    <ul>
                        <li><u>Offre&nbsp;:</u></li>
                    </ul>
                    <p>Quelle est votre secteur d'activit&eacute; ?</p>
                    <p>Quel est votre offre (produit ou service) ?</p>
                    <p>Quelle opportunit&eacute; d'entreprendre saisissez-vous ?</p>
                    <ul>
                        <li><u>March&eacute;&nbsp;:</u></li>
                    </ul>
                    <p>A qui s'adresse votre produit ou service ?</p>
                    <p>Quelle est la taille de votre march&eacute; potentiel ?</p>
                    <p>Quels sont vos avantages vis-&agrave;-vis de la concurrence ?</p>
                    <p>Quelle m&eacute;thode de distribution avez-vous choisie ?</p>
                    <ul>
                        <li><u>Entreprise&nbsp;:</u></li>
                    </ul>
                    <p>Qui sont les fondateurs ?</p>
                    <p>Quel est votre montage juridique ?</p>
                    <p>De qui ou de quel organisme pouvez-vous vous recommander (r&eacute;seau d'aide &agrave; la
                        cr&eacute;ation, concours, chef d'entreprise...) ?</p>
                    <ul>
                        <li><u>Financement&nbsp;:</u></li>
                    </ul>
                    <p>Quels sont vos r&eacute;sultats nets pr&eacute;visionnels ?</p>
                    <p>Combien votre projet peut-il rapporter &agrave; vos partenaires ?</p>
                    <p>Quels sont vos besoins en financement ?</p>
                    <p>Quels sont vos objectifs ou vos ambitions &agrave; 3 ans ?</p>
                    <p>Pour plus de d&eacute;tail, veuillez-vous r&eacute;f&eacute;rer aux formations en ligne sur les
                        liens suivant&nbsp;:</p>
                    <p>&nbsp;<strong>PRESENTATION DES CREATEURS OU DE L'EQUIPE (une &agrave; deux pages)</strong></p>
                    <p><br /> Objectif : montrer qu'il y a ad&eacute;quation entre les hommes et le projet pour inspirer
                        confiance &agrave; l'investisseur<br /> </p>
                    <ul>
                        <li><u>CV&nbsp;:</u></li>
                    </ul>
                    <p>Quels sont les pr&eacute;nom, nom et &acirc;ge des membres de l'&eacute;quipe ?</p>
                    <p>Quelles comp&eacute;tences ont-ils acquises&nbsp;?</p>
                    <p>Quelles formations ont-ils suivies&nbsp;?</p>
                    <p>Quels dipl&ocirc;mes ont-ils obtenus (en rapport avec le projet) ?</p>
                    <p>Quelles exp&eacute;riences ont-ils effectu&eacute;es&nbsp;?</p>
                    <p>Quels r&eacute;sultats ont-ils obtenus (en rapport avec le projet) ?</p>
                    <p><u><br /> 2- Equipe&nbsp;:</u></p>
                    <p>Comment se sont-ils rencontr&eacute;s autour de ce projet ?</p>
                    <p>Pourquoi vont-ils rester durablement ensemble ?</p>
                    <p>Comment se compl&egrave;tent-ils ?</p>
                    <p>Quelle fonction chacun remplit-il dans le projet ?</p>
                    <p><u><br /> 3- Ressources humaines&nbsp;:</u></p>
                    <p>Quels sont leurs points forts et leurs points faibles (en rapport avec le projet) ?</p>
                    <p>Que comptez-vous faire pour rem&eacute;dier aux points faibles (formation, recrutement...) ?</p>
                    <p>Quelle est votre politique en mati&egrave;re de recrutement ?</p>
                    <p><strong>DESCRIPTION DU PRODUIT OU SERVICE&nbsp;: (deux &agrave; trois pages)</strong></p>
                    <p><br /> Objectif : faire comprendre ce que vous vendez en insistant sur la valeur ajout&eacute;e
                        de l&rsquo;offre</p>
                    <p><u><br /> 1- Offre&nbsp;:</u></p>
                    <p>Quel est votre produit ou service ?</p>
                    <p>Quelle est sa valeur ajout&eacute;e, son originalit&eacute; ou son innovation ?</p>
                    <p>En quoi vous distinguez-vous de la concurrence ?</p>
                    <p>Pourquoi votre produit va-t-il r&eacute;ussir ?</p>
                    <p>Quel est l'utilisateur final ou le client vis&eacute; ?</p>
                    <p><u>2- Fabrication&nbsp;:</u></p>
                    <p>A quelle &eacute;tape en &ecirc;tes-vous de la fabrication (prototypes, R&amp;D, brevet...) ?</p>
                    <p>Comment est organis&eacute; le processus de production ?</p>
                    <p>Quel est le partage entre ce qui est fait par l'entreprise et ce qui est fait par les
                        sous-traitants ?</p>
                    <p><u><br /> 3- Co&ucirc;t&nbsp;:</u></p>
                    <p>Quel est le co&ucirc;t de revient unitaire du produit (mati&egrave;res premi&egrave;res,
                        machines, main d'&oelig;uvre, co&ucirc;ts indirects...)?</p>
                    <p><strong>ANALYSE DU MARCHE&nbsp;: (deux &agrave; trois pages)</strong></p>
                    <p><br /> Objectif : v&eacute;rifier l'existence d'un besoin et sa traduction en une demande
                        solvable</p>
                    <p><u><br /> 1- Etude&nbsp;:</u></p>
                    <p>Quelle(s) m&eacute;thode(s) avez-vous utilis&eacute;e(s) pour r&eacute;aliser votre &eacute;tude
                        de march&eacute; ?</p>
                    <p>Par qui, quand, o&ugrave; et comment a-t-elle &eacute;t&eacute; r&eacute;alis&eacute;e ?</p>
                    <p><u><br /> 2- Tendances&nbsp;:</u></p>
                    <p>Quelle est la taille de votre march&eacute; en volume et en valeur ?</p>
                    <p>Quelles sont les principales tendances du march&eacute; (&eacute;mergeant, croissant,
                        satur&eacute;, d&eacute;clinant...) ?</p>
                    <p>Quels sont les acteurs-prescripteurs du march&eacute; (associations, syndicats, journaux,
                        acheteurs...) ?</p>
                    <p>Quelles sont les contraintes juridiques (r&eacute;glementations, autorisations, licences,
                        dipl&ocirc;mes...) ?</p>
                    <p><u><br /> 3- Clients&nbsp;:</u></p>
                    <p>Qui sont les clients potentiels ?</p>
                    <p>Quels sont leurs besoins (comportements, habitudes d'achat et de paiement...) ?</p>
                    <p><br /> Leur demande est-elle solvable ?</p>
                    <p>Quel est le prix de vente psychologique de votre produit ?</p>
                    <p><u><br /> 4- Concurrence&nbsp;:</u></p>
                    <p>Quel est le niveau de la concurrence ?</p>
                    <p>Quels sont les principaux concurrents directs ou indirects (points forts, points faibles,
                        strat&eacute;gies, positionnements, r&eacute;sultats...) ?</p>
                    <p>Quelle niche ou quel segment de march&eacute; prioritaire avez-vous choisi ?</p>
                    <p><strong>STRATEGIE COMMERCIALE ET MOYENS MIS EN &OElig;UVRE (deux &agrave; trois pages)</strong>
                    </p>
                    <p><br /> Objectif : pr&eacute;senter la strat&eacute;gie qu'a d&eacute;fini l'entreprise pour
                        atteindre ses objectifs commerciaux.</p>
                    <p><u><br /> 1- Distribution&nbsp;:</u></p>
                    <p>Quels circuits de distribution avez-vous choisis ?</p>
                    <p>Quels sont les ressources n&eacute;cessaires &agrave; mettre en &oelig;uvre pour
                        d&eacute;velopper les moyens de distribution (moyens humains, VRP...) ?</p>
                    <p></p>
                    <p><u>2- Marketing&nbsp;:</u></p>
                    <p>Comment allez-vous faire conna&icirc;tre votre produit ?</p>
                    <p>Pensez-vous communiquer au niveau local, national, europ&eacute;en... ?</p>
                    <p>Pensez-vous utiliser des techniques de marketing direct (phoning, mailing...) ?</p>
                    <p>Pensez-vous participer &agrave; des salons ?</p>
                    <p>Pensez-vous cr&eacute;er un site internet ?</p>
                    <p><u>3- Moyens humains&nbsp;:</u></p>
                    <p>Quels sont les moyens humains n&eacute;cessaires ?</p>
                    <p>Qui allez-vous embaucher ?</p>
                    <p>Qui va faire quoi et &agrave; partir de quand ?</p>
                    <p><u><br /> 4- Moyens mat&eacute;riels&nbsp;:</u></p>
                    <p>Quels sont les moyens mat&eacute;riels n&eacute;cessaires (machines, mat&eacute;riel
                        informatique, mobilier de bureau...) ?</p>
                    <p>Quels sont vos besoins en locaux (location, achat, travaux, achet&eacute;s, installations
                        sp&eacute;cifiques) ?</p>
                    <p>Quels sont vos besoins en financement (fonds de d&eacute;part, BFR...) ?</p>
                    <p><strong>MONTAGE JURIDIQUE ET FINANCIER&nbsp;: (une &agrave; deux pages)</strong></p>
                    <p><br /> Objectif : pr&eacute;ciser les pouvoirs r&eacute;els de chacun des partenaires et leur
                        &eacute;volution possible &agrave; moyen terme.</p>
                    <p><u><br /> 1- Statut&nbsp;:</u></p>
                    <p>Quelle est la structure juridique retenue (entreprise individuelle, SARL, SA...) ?</p>
                    <p>Quels sont les principaux dirigeants (g&eacute;rant, PDG, administrateur...) ?</p>
                    <p>Quel est l'organigramme de l'entreprise ?</p>
                    <p>Quel est le statut de chacun des cr&eacute;ateurs (g&eacute;rant majoritaire, minoritaire...) ?
                    </p>
                    <p>Quand a commenc&eacute; ou commencera l'activit&eacute; de la soci&eacute;t&eacute; ?</p>
                    <p></p>
                    <p><u>2-Capital&nbsp;:</u></p>
                    <p>Quels sont les actionnaires de la soci&eacute;t&eacute; (nature des apports...)?</p>
                    <p>Quelle est la r&eacute;partition du capital ?</p>
                    <p>Quelle est la logique d'&eacute;volution de la propri&eacute;t&eacute; de l'entreprise &agrave;
                        moyen ou long terme (ind&eacute;pendance, ouverture du capital, introduction au second
                        march&eacute;, vente &agrave; un grand groupe...) ?</p>
                    <p><br /> <u>3- Partenariats&nbsp;:</u></p>
                    <p>Avez-vous sign&eacute; ou comptez-vous signer des partenariats (commerciaux, laboratoires,
                        distributeurs, sous-traitants, fournisseurs, actionnaires...) ?</p>
                    <p>Combien aurez-vous de fournisseurs et quel sera leur pouvoir ?</p>
