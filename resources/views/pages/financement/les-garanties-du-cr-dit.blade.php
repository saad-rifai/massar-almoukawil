                    </ul>
                    <p>Cette garantie permet &agrave; son b&eacute;n&eacute;ficiaire de recevoir le montant des sommes dues jusqu&rsquo;&agrave; paiement int&eacute;gral du march&eacute; nanti. La main lev&eacute;e n&rsquo;est attribu&eacute;e qu&rsquo;apr&egrave;s remboursement des avances accord&eacute;es et lib&eacute;ration des cautions.</p>
                    <ul>
