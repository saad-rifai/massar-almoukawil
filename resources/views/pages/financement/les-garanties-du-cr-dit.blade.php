                    </ul>
                    <p>L&rsquo;acqu&eacute;reur du v&eacute;hicule roulant s&rsquo;abstient de payer totalement ou partiellement le vendeur du v&eacute;hicule (qui sera pay&eacute; par l&rsquo;organisme financier ayant consenti un cr&eacute;dit &agrave; l&rsquo;acqu&eacute;reur) et proc&eacute;der au barrement de la carte grise au nom de l&rsquo;organisme financier.</p>
