                    <p>Les garanties r&eacute;elles&nbsp;:</p>
                    <p>Portant sur des biens du d&eacute;biteur ou d&rsquo;une tierce personne.</p>
                    <ul>
                    <li><strong>Les garanties immobili&egrave;res&nbsp;:</strong></li>
                    </ul>
                    <ul>
                    <li>La promesse d&rsquo;hypoth&egrave;que&nbsp;:</li>
                    </ul>
                    <p>Un engagement pris par l&rsquo;emprunteur de constituer a premi&egrave;re demande de la banque, une hypoth&egrave;que qui prendra rang &agrave; sa date d&rsquo;inscription sur les livres fonciers et non a compter de la signature de l&rsquo;acte la constatant.</p>
                    <ul>
                    <li>L&rsquo;hypoth&egrave;que terrestre&nbsp;:</li>
                    </ul>
                    <p>C&rsquo;est un droit r&eacute;el grevant un bien immeuble immatricul&eacute; ou en cours d&rsquo;immatriculation qui permet &agrave; son propri&eacute;taire d&rsquo;en conserver l&rsquo;usage, le droit de le louer ou de l&rsquo;hypoth&eacute;quer de nouveau sauf interdiction au niveau du contrat.</p>
                    <ul>
                    <li>Le cautionnement hypoth&eacute;caire&nbsp;:</li>
                    </ul>
                    <p>C&rsquo;est un cautionnement r&eacute;el qui consiste pour la caution &agrave; affecter en garantie du remboursement du pr&ecirc;t consenti &agrave; l&rsquo;emprunteur, une hypoth&egrave;que sur le bien lui appartenant.</p>
                    <ul>
                    <li>Le d&eacute;p&ocirc;t de titres de propri&eacute;t&eacute;s non immatricul&eacute;s&nbsp;:</li>
                    </ul>
                    <p>C&rsquo;est le fait de donner en garantie un bien non immatricul&eacute; par le d&eacute;p&ocirc;t de l&rsquo;acte original de la Molkia aupr&egrave;s du bailleur de fonds.</p>
                    <ul>
                    <li>L&rsquo;hypoth&egrave;que maritime.</li>
                    </ul>
                    <p>Le financement des navires de p&egrave;che et de transport maritime pout &ecirc;tre garanti par l&rsquo;hypoth&egrave;que maritime malgr&eacute; l&rsquo;aspect mobilier et ce gr&acirc;ce &agrave; la publicit&eacute; l&eacute;gale organis&eacute;e loi les r&eacute;gissant qui est similaire &agrave; celle des immeubles.</p>
                    <ul>
                    <li>L&rsquo;hypoth&egrave;que sur a&eacute;ronef&nbsp;:</li>
                    </ul>
                    <p>Les a&eacute;ronefs sont selon la loi de l&rsquo;a&eacute;ronautique civile de 1962 sont des meubles susceptibles d&rsquo;hypoth&egrave;ques.</p>
                    <ul>
                    <li><strong>Les garanties mobili&egrave;res&nbsp;:</strong></li>
                    </ul>
                    <ul>
                    <li>Le nantissement de droit commun.</li>
