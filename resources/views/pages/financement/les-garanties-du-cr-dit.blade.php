                    <ul>
                    <li>Le nantissement de parts sociales&nbsp;:</li>
                    </ul>
                    <p>Les parts sociales d&rsquo;une SARL peuvent faire l&rsquo;objet d&rsquo;un nantissement qui n&eacute;cessite le consentement pr&eacute;alable de la majorit&eacute; des associ&eacute;s repr&eacute;sentant au moins les 3 quarts du capital.</p>
                    <ul>
                    <li>Le nantissement de bons de caisse&nbsp;:</li>
                    </ul>
                    <p>C&rsquo;est un nantissement de droit commun qui n&eacute;cessite la d&eacute;possession du propri&eacute;taire qui remet les bons nantis &agrave; la banque.</p>
                    <ul>
                    <li>Le nantissement du fonds de commerce&nbsp;:</li>
                    </ul>
                    <p>C&rsquo;est un bien meuble incorporel constitu&eacute; par l&rsquo;ensemble des biens mobiliers affect&eacute;s &agrave; l&rsquo;exercice d&rsquo;une activit&eacute; commerciale (l&rsquo;enseigne, l&rsquo;achalandage, la client&egrave;le, le nom commercial, le droit au bail)</p>
                    <ul>
                    <li>Le nantissement de mat&eacute;riel et outillage&nbsp;:</li>
                    </ul>
                    <p>Le privil&egrave;ge de ce nantissement porte sur le mat&eacute;riel professionnel s&rsquo;il est objet du financement m&ecirc;me d&rsquo;occasion (industriel, artisanal, agricole, m&eacute;dical, &hellip;)</p>
                    <ul>
                    <li>Le nantissement de marchandises&nbsp;:</li>
                    </ul>
                    <p>Le fait de mettre des marchandises en nantissement sans la d&eacute;possession du propri&eacute;taire mais il s&rsquo;agit d&rsquo;une liste de mati&egrave;res bien d&eacute;finie l&eacute;galement.</p>
                    <ul>
                    <li>Le nantissement de produits agricoles&nbsp;:</li>
                    </ul>
                    <p>Nantissement sans d&eacute;possession pouvant porter sur les r&eacute;coltes d&eacute;tach&eacute;es ou non, sur les produits naturels ou industriels de l&rsquo;exploitation agricole, le chaplet et le mat&eacute;riel agricole non immeuble.</p>
