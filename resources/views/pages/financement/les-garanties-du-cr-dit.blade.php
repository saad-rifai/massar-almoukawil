                    </ul>
                    <p>C&rsquo;est un engagement pris par une personne physique ou morale au profit d&rsquo;un cr&eacute;ancier de payer les sommes dues par le d&eacute;biteur principal en cas de d&eacute;faillance de ce dernier.</p>
