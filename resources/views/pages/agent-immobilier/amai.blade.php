                    <h2 class="blue-section-title">
                        Présentation
                    </h2>
                    <p class="section_normale_text">
                        Une agence immobilière est un élément important dans le processus d’achat, de vente, ou encore
                        de location de biens
                        immobiliers. L’agence immobilière propose des services qui facilitent la tâche aux différentes
                        parties concernées par un
                        bien immobilier. Elle permet de gagner du temps et d’éviter les problèmes avant, pendant et
                        après l’acquisition ou la
                        location d’un bien.
                    </p>
                </div>
                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Services
                    </h2>
                    <p class="section_normale_text">
                        - Propositions à l’achat, la vente ou la location de biens immobiliers  <br>
                        - Mettre à disposition des entrepreneurs, des professionnels formés et habiletés à servir
                        d’intermédiaires entre les
                        différents demandeurs et offreurs de biens immobiliers spécifiques  <br>
                        - En cas de location, Veiller aux intérêts du locataire et du propriétaire. <br>
                        Une agence immobilière peut également intervenir dans les domaines suivants : <br>
                        - Administration de biens  <br>
                        - Recherche de biens immobiliers pour les particuliers ou pour les entreprises  <br>
                        - Conseils et accompagnement lors d’un projet immobilier  <br>
                        - Établissement de contrat de vente  <br>
                        - Réalisations de formalités administratives pour l’achat, la vente ou la location  <br>
                        - Estimation de bien immobilier. <br>
                    </p>
