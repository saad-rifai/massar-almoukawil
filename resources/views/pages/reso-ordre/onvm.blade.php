                    </h2>
                    <p class="section_normale_text">
                    L’Ordre National des Vétérinaires a été institué par le Dahir. Il regroupe tous les docteurs vétérinaires exerçant au 
Maroc, soit à titre privé, soit dans les services de l’Etat, des collectivités locales et des établissements publics, soit en 
qualité d’enseignant dans les établissements d’enseignement supérieur de la médecine vétérinaire, soit dans les Forces 
Armées Royales, et auquel ces personnes doivent obligatoirement demander leur inscription avant de pouvoir exercer.
                    </p>
                </div>

                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Services
                    </h2>
                    <ul>
                        <li> Assurer la sauvegarde des principes et traditions de moralité, de dignité et de probité qui font l’honneur de la 
profession de vétérinaire et de veiller au respect par ses membres des lois, règlements et usages qui régissent 
l’exercice de la médecine vétérinaire ;</li>
<li>Admettre dans la profession les vétérinaires dans les formes et conditions prévues la loi ;</li>
<li>Veiller au libre choix du vétérinaire par le propriétaire et de s’assurer que les honoraires vétérinaires librement 
débattus entre les parties, sont justes et mesurés ;</li>
<li>Edicter tout règlement nécessaire à l’accomplissement de sa mission et d’établir le code des devoirs 
professionnels ;</li>
<li>Assurer la défense des intérêts matériels et moraux des vétérinaires ;</li>
<li> Organiser et gérer les œuvres sociales de ses membres ;
</li>
<li>Représenter la profession de vétérinaire auprès de l’Administration ;
</li>
<li>Apporter son concours, à la demande de l’Etat, à l’élaboration et à l’exécution de la politique de santé et de 
développement des ressources animales.
</li>
                    </ul>
                </div>
                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Démarches
                    </h2>
                    <p class="section_normale_text">
                    Nul ne peut être inscrit à l'ordre s'il ne remplit les conditions suivantes : <br>
                    Être de nationalité marocaine ; <br>  Être titulaire du diplôme de docteur vétérinaire délivré par les établissements d'enseignement vétérinaire marocains 
ou d'un diplôme obtenu dans un autre Etat et reconnu équivalent au diplôme national ou, sans être titulaire du doctorat 
vétérinaire, avoir été recruté en qualité de vétérinaire par le ministère de l'agriculture et de la réforme agraire 
antérieurement à la création de l'enseignement vétérinaire au Maroc ; <br> N'avoir encouru aucune condamnation pour des faits contraires à l'honneur, à la dignité ou à la probité ; <br>  Être en position régulière au regard des lois instituant les services civil ou militaire ; <br> Être en position régulière au regard des lois instituant les services civil ou militaire ; <br>
                    </p>
                    <ul>
                        <li>Aucun étranger ne peut exercer la profession de vétérinaire s'il ne remplit les conditions suivantes :</li>
                        <li>Résider sur le territoire national en conformité avec la législation relative à l'immigration ;</li>
                        <li> Être ressortissant d'un Etat ayant conclu avec le Maroc un accord par lequel les vétérinaires ressortissants d'un 
des Etats peuvent s'installer sur le territoire de l'autre Etat pour y exercer la profession ;</li>
                        <li>Être détenteur d'un doctorat en médecine vétérinaire ou d'un diplôme reconnu équivalent par l'administration 
et lui donnant droit d'exercer dans l'Etat dont il est ressortissant ;</li>
                        <li>N’avoir pas été condamné au Maroc ou à l'étranger pour des faits contraires à l'honneur, à la dignité ou à la 
probité.</li>
                        <li>Aucun étranger ne peut exercer la profession, à titre privé, au Maroc s'il n'y est autorisé par l'administration.
Pour plus d’information, consulter le lien suivant : <a target="_blank" href="http://www.sgg.gov.ma/Portals/0/profession_reglementee/dahir_ordre_veterinaire_fr.pdf">www.sgg.gov.ma/Portals/0/profession_reglementee/dahir_ordre_veterinaire_fr.pdf</a> </li>
                        
