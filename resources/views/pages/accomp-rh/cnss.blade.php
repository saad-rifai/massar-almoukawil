                    <h1>
                        La déclaration à la Caisse
                        Nationale de Sécurité Sociale
                        (Immatriculation des salariés)

                    </h1>
                </div>
                <br>
                <br>
                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Présentation
                    </h2>
                    <p class="section_normale_text">
                        L’immatriculation est l’opération d’identification des salariés par la CNSS leur octroyant un
                        numéro d’immatriculation
                        unique qui permettra :
                    </p>
                    <ul class="section_normale_list">
                        <li>De les identifier en tant qu’assurés.
                        </li>
                        <li>D’enregistrer leur déclaration de salaires et leurs périodes de travail qui constituent la
                            base de service des
                            prestations.</li>
                    </ul>
                </div>
                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Services
                    </h2>
                    <p class="section_normale_text">Il assure aux intéressés une protection contre :</p>
                    <ul class="section_normale_list">
                        <li>Maladie - Maternité</li>
                        <li>Allocations familiales</li>
                        <li>Accident du travail et maladies professionnelles</li>
                        <li>Décès</li>
                        <li>Invalidité</li>
                        <li>Vieillesse</li>
                        <li>Pension de survivants</li>
                        <li>Chômage</li>
                    </ul>
                </div>
                <div class="text-containet">
                    <h2 class="blue-section-title">
                        l'immatriculation de vos salariés
                    </h2>
                    <p class="section_normale_text"><strong>Documents à préparer pour inscrire les employés à la CNSS
                            :</strong></p>
                    <ul class="section_normale_list">
                        <li><a target="_blank"
                                href="https://drive.google.com/file/d/14y3EX5Jh8O_y_Ky4ZeDeIY-QUEwhwcxz/view?usp=sharing">Le
                                Formulaire <i class="fas fa-external-link-alt"></i></a>
                            “Demande d’immatriculation” à renseigner et imprimer.</li>
                        <li>Deux photos d’identité (dont une à coller sur la place réservée).
                        </li>
                        <li>Copie de la CNI ou le titre de séjour pour les étrangers (carte de résidence ou carte
                            d’immatriculation).
                        </li>
                        <li>Attestation bancaire comportant le RIB (pièce facultative).</li>
                        <li>Salariés des associations/groupements : Contrat ou attestation de travail.</li>
                        <li><strong>Déclaration sur BDS Entrants</strong></li>
                    </ul>
                    <p class="section_normale_text">Pour l’ensemble des salariés ne figurant pas sur le Bordereau de
                        Déclaration de Salaire (BDS) Préétabli par la CNSS,
                        L’entreprise devra procéder à leur déclaration sur le <a target="_blank"
                            href="https://drive.google.com/file/d/1gz5ipA7cfr5EZ0I8Q1E6d_IaCJKPNF8j/view?usp=sharing">formulaire
                            <i class="fas fa-external-link-alt"></i></a> «Bordereau de Déclaration des Salariés
                        Entrants»:
                        <br>
                        en renseignant leur numéro d'immatriculation, leur nom, leur prénom, le nombre de jours
                        travaillé et le salaire perçu; <br> </pr>
                    <ul class="section_normale_list">
                        <li>Le numéro d’affiliation de votre entreprise.</li>
                        <li> Son nom ou raison sociale et son adresse.</li>
                        <li>Son activité.</li>
                        <li>Le mois déclaré (régime général) ou le trimestre déclaré (régime agricole).</li>
                        <li>Le numéro et le nombre de pages remplies.</li>
                        <li>Le numéro d’immatriculation, le nom et prénom de chacun des salariés de l’entreprise.</li>
                        <li>Le nombre de jours travaillés, dans la limite de 26 jours ouvrables par mois civil déclaré.
                        </li>
                        <li>Le salaire brut perçu sans limitation de plafond.</li>
                        <li>Le total des salaires déclarés par page.</li>
                        <li>Les montants cumulés.</li>
                        <li>La date, le cachet et la signature de l’entreprise.</li>
                        <li>Déclaration sur BDS Préétabli</li>
                    </ul>
                    <p class="section_normale_text">Pour simplifier la déclaration de salaire sur papier, la CNSS
                        prépare et envoie avant la fin de chaque mois un BDS
                        Préétabli «Le formulaire » qui comporte déjà les informations relatives à l’entreprise ainsi que
                        les informations relatives
                        aux salariés déclarés auparavant notamment, le numéro d’immatriculation, le nom et le prénom. Il
                        suffit uniquement de
                        le compléter en y portant pour chaque salarié le nombre de jour et le salaire réel perçu au
                        titre de ce mois.</p> <br>
                    <p class="section_normale_text">Le BDS préétabli doit contenir de manière claire les informations
                        indiquées ci-après :</p>
                    <ul>
                        <li>Le nombre de jours travaillés, dans la limite de 26 jours ouvrables.
                        </li>
                        <li>Le salaire brut perçu sans limitation de plafond.</li>
                        <li>Le total des salaires déclarés par page.</li>
                        <li>Les totaux cumulés.
                        </li>
                        <li>La date, le cachet et la signature de l’entreprise.</li>
                        <li>La situation des salariés (SO : Sortant, CO : Congé sans solde MS : Maintenu sans salaire AT
                            : Accident de travail </li>
                    </ul>
                    <p class="section_normale_text">MP : Maladie Professionnelle ML : Maladie, MT : Maternité, DE :
                        Décédé)</p> <br>
                    <p class="section_normale_text"><strong>NB</strong> :Le BDS d’un mois est préétabli par la CNSS sur
                        la base des informations portées sur le formulaire : « Bordereau
                        de Déclaration des Salariés Entrants » des mois antérieurs.</p> <br>
                    <p class="section_normale_text">Toute déclaration de salaire mal libellées ou illisible ne peut être
                        exploitée et sera retournée. Ceci ayant pour conséquence
                        un retard dans la prise en charge des déclarations de salaires.</p>
                    <p class="section_normale_text">L’adhésion à DAMANCOM permet d’éviter toutes ces anomalies et les
                        éventuels retards dans la prise en charge de
                        déclarations des salariés.</p>
                    <p class="section_normale_text">Les BDS concernant les salariés entrants et les BDS préétablis
                        doivent être envoyés sous pli recommandé et dans les
                        délais impartis à l’adresse suivante : Caisse nationale de sécurité sociale, BP 2023 - CASA GARE
                        20300 Casablanca. <br>
                        <strong>Déclaration hors délai réglementaire</strong> <br>
                        Afin de permettre aux salariés de bénéficier à temps de leurs prestations, l’employeur est tenu,
                        par la loi, à les déclarer
                        dans les délais impartis, sous peine d’application d’astreintes. <br>
                    <p>Le montant de l’astreinte est fixé à 50 dhs pour chaque insuffisance, omission ou retard de
                        déclaration de salaire. Elle
                        est applicable pour chaque mois ou fraction de mois de retard. Pour les affiliés assujettis au
                        régime de l’AMO cette
                        astreinte est portée à 100 dhs pour chaque mois ou fraction de mois de retard.</p>
                    <p>Les astreintes sont appliquées dans les 3 cas suivants :</p>
                    <ul>
                        <li>Déclaration tardive des salariés.
                        </li>
                        <li>Déclaration d’un salaire inférieur au salaire légal perçu par salarié et dûment constaté par
                            l’inspection et contrôle.
                        </li>
                        <li>Non déclaration de salariés dûment constatée par l’inspection et contrôle.</li>
                    </ul>



                    </p>
