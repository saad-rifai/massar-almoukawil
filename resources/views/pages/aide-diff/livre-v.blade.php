                <div class="text-containet">
                    <h2 class="blue-section-title">
                    Les procédures des difficultés de l’entreprise 
                    </h2>
                    <p>La loi 73, 17 : <br>
Cette loi permet à l’entreprise de faire face à ses difficultés qu’elles soient internes ou externes avant qu’elle la mette en 
péril et ce à travers un ensemble de procédures.  <br>
<strong>La procédure de prévention</strong> en comporte 2 :
</p>
                    <ul class="section_normale_list">
                        <li>
                        La prévention interne : selon la loi, l’entreprise doit respecter les règles de bonne gouvernance lui permettant de 
prévenir les difficultés qui risquent de compromettre son exploitation. C’est pour cette raison que la loi oblige toutes 
associées ou commissaires aux comptes d’informer le chef d’entreprise de tout dysfonctionnement afin de prendre 
les mesures adéquates le plus tôt possible.

                        </li>
                        <li>La prévention externe : au cas où, l’entreprise n’arrive pas à résoudre tout seul ses problèmes et avant que cela ne 
s’aggrave. Elle peut solliciter du président du tribunal de commerce l’ouverture de l’une de ces deux procédures : <br> Au cas où la difficulté résulte des conflits internes ou avec les partenaires habituels de l’entreprise, le 
président du tribunal désigne un mandataire spécial qui intervient pour réduire les oppositions et trouver un 
terrain d’entente. <br> Dans le cas où les difficultés sont d’ordre financier et économique ou social, que l’entreprise ne peut gérer et 
sans être en cessation de paiement, le président du tribunal de commerce désigné un conciliateur chargé de 
trouver un accord avec les créanciers sur des nouveaux délais de paiement et il peut même procéder à une 
suspension provisoire des poursuites judiciaires. </li>
                       
                    </ul> <br>
<p><strong>La procédure de sauvegarde :
</strong> <br> Cette procédure est destinée aux entreprises qui éprouvent des difficultés pouvant les conduire à la cessation de 
paiement dès que la personne concernée dépose une demande qui répond aux conditions fixées par la loi. Le 
tribunal ouvre la procédure qui garantit à la fois la continuité de l’entreprise et de ses relations commerciales avec les 
partenaires, le paiement des dettes et la conservation des postes de travail. Il désigne également un syndic qui 
prépare un plan de sauvegarde sur la base de son diagnostic et des propositions du chef de l’entreprise. Ce plan 
permet un rééchelonnement des dettes sur une période allant jusqu’à cinq ans. </p>
<p><strong>La procédure de redressement judiciaire :</strong> <br>Le législateur permet à l’entreprise qui est en cessation de paiement de demander l’ouverture de cette procédure. Il faut 
que la demande soit présentée dans les 30 jours de la date de cessation de paiement. Une fois le redressement 
prononcé, le tribunal désigne un syndic qui dresse un rapport détaillé sur la situation de l’entreprise et propose des 
solutions en considérant l’intérêt de toutes les parties selon, trois possibilités se
présentent comme suit : <br>

</p>
<ul>
    <li>Le plan de continuation : qui permet un rééchelonnement des dettes sur une période allant jusqu’à 10 ans</li>
    <li>Le plan de cession : qui permet de céder des unités de production de l’entreprise et de rembourser les dettes</li>
    <li>La liquidation judiciaire : qui prononcée par le tribunal. Lorsque la situation de l’entreprise est 
irrémédiablement compromise, ce jugement met fin à la vie de l’entreprise et entraine la vente de tous ses actifs 
et le paiement de dettes.</li>
   
</ul>
