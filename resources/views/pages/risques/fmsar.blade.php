                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Services
                    </h2>
                    <p class="section_normale_text">
                        <strong>L'assurance Responsabilité Civile Professionnelle</strong> est une obligation pour
                        toutes les professions réglementées. Elle
                        concerne les professionnels du droit, de la santé, du bâtiment, du voyage et de l'immobilier,
                        ainsi que les experts-comptables ou les courtiers.
                        L'objectif de la Responsabilité Civile Professionnelle est de couvrir tous les dommages causés à
                        autrui. Elle prend en
                        charge tous dommages et incidents survenus dans l'accomplissement de l'activité de l'entreprise.
                        Elle couvre également
                        les biens de ses clients, ainsi que les clients impactés par l'activité professionnelle.
                        Si la RC Pro est obligatoire seulement pour certaines activités, elle reste fortement conseillée
                        pour toutes les entreprises
                        car elle offre un niveau de garantie fondamental susceptible de couvrir les sinistres les plus
                        courants. <br>
                        <strong>L’assurance multirisque professionnelle</strong> est une autre assurance d'entreprise.
                        La non-souscription à cette garantie peut
                        exposer certaines entreprises à des poursuites pénales. Cette dernière couvre la plupart des
                        dommages auxquels
                        l'entreprise est susceptible d'être confrontée. Il peut s'agir d'un incendie, d'un dégât des
                        eaux, d'un acte de vandalisme,
                        d'un vol, des dommages électriques, d'un bris de glace, etc. Cette assurance professionnelle
                        prend en charge les sinistres
                        sur les bâtiments professionnels, le mobilier et/ou le matériel informatique. <br>
                        <strong>Recours Juridique professionnel :</strong> Parfois couplée à la multirisque
                        professionnelle ou à la RC Pro, cette garantie n'est
                        pas une obligation. En revanche, elle est fortement recommandée. Cette dernière couvre
                        l'ensemble des frais juridiques
                        en cas de litige avec un client et vous offre une protection supplémentaire. Elle est également
                        un appui quotidien si vous
                        avez besoin de conseils d'ordre juridique. <br>
                        <strong>La Responsabilité Civile Exploitation</strong> est une assurance d'entreprise qui
                        s'applique en cas de sinistre survenant pendant
                        l'exploitation, c'est-à-dire dans le cadre des activités quotidiennes de l'entreprise. Il ne
                        s'agit pas d'une garantie perte
                        d'exploitation. Cette dernière porte uniquement sur la responsabilité civile de l'entreprise à
                        l'occasion de dommages sur
                        des tiers. S'il s'agit d'une assurance professionnelle non obligatoire, cette dernière est
                        fortement recommandée. En effet,
                        en tant qu'entrepreneur, vous êtes dans l'obligation de réparer les éventuels dommages causés à
                        autrui. <br>
                        <strong>L'assurance des locaux :</strong> protège l'ensemble des bâtiments et biens matériels en
                        cas de sinistre. Elle peut couvrir non
                        seulement les locaux, mais également les équipements ou le stock de marchandises contre de
                        nombreux types de dégâts
                        en fonction de garanties souscrites : d'incendie, de dégât des eaux, d'événement climatique, de
                        catastrophe naturelle,
                        d'attentat, de vol, d'acte de vandalisme ou de détérioration à la suite d'installations
                        électriques défectueuses. D'autres
                        options, telles que le bris de glace ou la rupture de la chaîne du froid, peuvent lui être
                        ajoutées. Il s'agit d'une assurance
                        facultative, mais indispensable compte tenu des sommes engagées en cas de sinistre. <br>
                        <strong>La cyber-assurance</strong> est une garantie qui protège les entreprises contre les
                        risques informatiques et les cyber-attaques.
                        Son objectif est de maintenir un niveau de sécurité optimal et une certaine stabilité
                        financière. La cyber-assurance couvre
                        notamment les conséquences financières liées à la perte de données informatiques, ou les
                        dommages et intérêts réclamés
                        par un tiers. Là encore, il s'agit d'une assurance non obligatoire. Cependant, celle-ci peut
                        s'avérer indispensable,
                        notamment si votre société traite des données sensibles.
                    </p>
