                            organismes, associations et ONG, marocains et étrangers pour la promotion de
                            l’investissement dans la région nord ;</li>
                        <li>Organisation des séminaires, tables rondes et colloques autour de thèmes
                            d’actualité à caractère économique, social, juridique et fiscal ; processus de mise
                            à niveau ;</li>
                        <li>Assistance des entreprises en cas de conflit social ;</li>
                        <li>Echange permanent avec les membres à travers les circulaires, bulletins de
                            liaison et médias locaux et nationaux.
