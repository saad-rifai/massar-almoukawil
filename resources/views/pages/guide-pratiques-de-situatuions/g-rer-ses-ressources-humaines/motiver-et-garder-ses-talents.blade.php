                    <h5>1. POURQUOI INT&Eacute;GRER UNE NOUVELLE RECRUE ET COMMENT PR&Eacute;PARER
                        L&rsquo;ARRIV&Eacute;E D&rsquo;UN(E) EMPLOY&Eacute;(E)?</h5>
                    <p>&nbsp;</p>
                    <p>Il n&rsquo;est pas tr&egrave;s coh&eacute;rent que vous passiez du temps &agrave; trouver les
                        bonnes recrues pour ensuite n&eacute;gliger leur prise en main&hellip; et pourtant c&rsquo;est
                        une erreur dans laquelle beaucoup commettent ! La <strong>fid&eacute;lisation des collaborateurs
                        </strong>devra, par la suite, &ecirc;tre au c&oelig;ur des enjeux de l&rsquo;entreprise, quel
                        que soit son secteur d&rsquo;activit&eacute; et son m&eacute;tier.</p>
                    <p>Pour rendre l&rsquo;int&eacute;gration efficace, il faut l&rsquo;organiser au mieux pour que le
                        collaborateur :</p>
                    <ul>
                        <li>prenne connaissance de l&rsquo;entreprise et des collaborateurs, des valeurs et de la
                            culture</li>
                        <li>s&rsquo;int&egrave;gre &agrave; sa nouvelle &eacute;quipe et apprenne &agrave;
                            conna&icirc;tre ses futurs collaborateurs</li>
                        <li>prenne en main son poste et ses missions</li>
                    </ul>
                    <p>A travers ce parcours, le collaborateur maximisera ses chances de r&eacute;ussir son
                        int&eacute;gration et de rester au sein de votre entreprise.</p>
                    <p><strong>Anticiper et pr&eacute;parer l&rsquo;int&eacute;gration :
                        </strong>l&rsquo;int&eacute;gration ne consiste pas uniquement &agrave; accueillir la nouvelle
                        recrue le jour de son arriv&eacute;e &ndash; il faut l&rsquo;anticiper et la pr&eacute;parer.
                        Voici quelques <strong>principes cl&eacute;s </strong>pour bien la r&eacute;ussir :</p>
                    <ol>
                        <li>
                            <p><strong>Informer de l&rsquo;arriv&eacute;e du
                                    collaborateur</strong><br /><strong><br /></strong>Tout d&rsquo;abord, il est
                                primordial de pr&eacute;venir l&rsquo;ensemble de l&rsquo;&eacute;quipe de
                                l&rsquo;arriv&eacute;e d&rsquo;une nouvelle recrue. L&rsquo;id&eacute;e n&rsquo;est pas
                                juste de l&rsquo;accueillir et de l&rsquo;installer devant un ordinateur ! Il faut
                                <strong style="font-size: 14px;">informer les fonctions supports</strong><span
                                    style="font-size: 14px;">, qui seront sollicit&eacute;es pour le mat&eacute;riel et
                                    l&rsquo;aspect administratif de l&rsquo;int&eacute;gration de la nouvelle
                                    recrue.<br /><br /></span>Votre nouveau ou votre nouvelle collaborateur(trice) a
                                besoin de <strong>sentir que son arriv&eacute;e a
                                    &eacute;t&eacute;&nbsp;</strong><strong>anticip&eacute;e </strong>afin de se faire
                                sa place parmi les collaborateurs. Rien de plus simple que l&rsquo;envoi d&rsquo;un
                                email une semaine avant pour informer de l&rsquo;arriv&eacute;e de la personne en
                                question, &agrave; compl&eacute;ter par un email d&rsquo;introduction de la nouvelle
                                recrue, une semaine apr&egrave;s avoir int&eacute;gr&eacute; vos &eacute;quipes, pour se
                                pr&eacute;senter.<br /><br /></p>
                        </li>
                        <li>
                            <p><strong>Pr&eacute;parer l&rsquo;espace de travail<br /><br /></strong>Il faut
                                s&rsquo;assurer que la personne ne manque de rien et que son espace de travail est
                                pr&ecirc;t. Il faut ainsi se poser les bonnes questions en amont, afin de ne pas se
                                retrouver en difficult&eacute; le jour J :</p>
                        </li>
                    </ol>
                    <ul>
                        <li>De quelles fournitures a-t-il besoin ?</li>
                        <li>Son ordinateur est-il configur&eacute; pour son arriv&eacute;e ?</li>
                        <li>Son bureau est-il propre et bien rang&eacute; ?</li>
                        <li>A-t-il &agrave; sa disposition tous les outils et documents permettant de faciliter sa prise
                            de poste?</li>
                    </ul>
                    <ul>
                        <li><strong>R&eacute;gler les d&eacute;tails administratifs</strong></li>
                    </ul>
                    <p>&nbsp;</p>
                    <p>On a tendance &agrave; minimiser l&rsquo;impact du volet administratif sur la prise en main
                        d&rsquo;un nouveau collaborateur. Pourtant, c&rsquo;est un <strong>facteur d&eacute;terminant
                        </strong>de l&rsquo;image que va garder le collaborateur en t&ecirc;te. Aussi, il est
                        recommand&eacute; de s&rsquo;en occuper s&eacute;rieusement avant l&rsquo;arriv&eacute;e de la
                        nouvelle recrue. Parmi les &eacute;l&eacute;ments cl&eacute;s &agrave; v&eacute;rifier :</p>
                    <ul>
                        <li>Le contrat a-t-il &eacute;t&eacute; sign&eacute; ?</li>
                        <li>Un RIB a-t-il &eacute;t&eacute; fourni ?</li>
                        <li>Les dipl&ocirc;mes ont-ils &eacute;t&eacute; remis aux ressources humaines ?</li>
                    </ul>
                    <p>Pour ne rien oublier, n&rsquo;h&eacute;sitez pas &agrave; pr&eacute;parer une TO DO liste valable
                        pour toutes les nouvelles arriv&eacute;es. Ainsi, le processus sera rod&eacute;, et vous
                        gagnerez en efficacit&eacute; !</p>
                    <p>&nbsp;</p>
                    <p><strong>Bon &agrave; savoir : </strong><strong>parmi les id&eacute;es que vous pouvez utiliser,
                            la notion d&rsquo;&rdquo;aiguilleur&rdquo; ou de &ldquo;mentor&rdquo; est tr&egrave;s
                            ing&eacute;nieuse. Il s&rsquo;agit de nommer un responsable dans l&rsquo;&eacute;quipe
                            future de la nouvelle recrue, et de le charger de lui faire visiter l&rsquo;entreprise et de
                            le pr&eacute;senter &agrave; l&rsquo;ensemble des collaborateurs.</strong></p>
                    <p>&nbsp;</p>
                    <h5>2. COMMENT DONNER DU SENS &Agrave; SES SALARI&Eacute;S &Agrave; TRAVERS LES VALEURS DE
                        L&rsquo;ENTREPRISE ?</h5>
                    <p>Pour savoir comment motiver ses salari&eacute;s, il faut s&rsquo;int&eacute;resser &agrave; la
                        derni&egrave;re marche de la pyramide de Maslow, c&rsquo;est-&agrave;-dire <strong>la
                            r&eacute;alisation de soi.</strong></p>
                    <p>Souvent, pour cela, nous avons besoin que nos actions aillent plus loin que simplement
                        cr&eacute;er de la richesse financi&egrave;re. Nous avons besoin de <strong>donner du sens
                            &agrave; nos actions</strong>, et de sentir que nous transcendons ainsi notre existence.
                        Lorsque nous travaillons pour une entreprise qui nous permet de r&eacute;aliser tout ou partie
                        de ce que nous sommes, alors nous sommes m&eacute;caniquement plus motiv&eacute;s,
                        impliqu&eacute;s et fid&egrave;les.</p>
                    <p>Ainsi, il est tr&egrave;s important que vous puissiez donner du sens &agrave; vos collaborateurs,
                        que ce soit &agrave; travers :</p>
                    <ul>
                        <li><strong>Votre activit&eacute; </strong>m&ecirc;me : notion d&rsquo;implication dans un
                            &eacute;cosyst&egrave;me, de servir une cha&icirc;ne de valeur, de participer &agrave; la
                            construction d&rsquo;un projet plus large que votre simple activit&eacute;</li>
                        <li>Des <strong>actions positives d&rsquo;ordre socio-environnementales </strong>que votre
                            entreprise pourrait avoir dans le cadre de sa RSE (Responsabilit&eacute; Sociale de
                            l&rsquo;Entreprise).</li>
                    </ul>
                    <p>La motivation et le succ&egrave;s cr&eacute;ent aussi de la <strong>coh&eacute;sion
                            d&rsquo;&eacute;quipe</strong>, qui cr&eacute;e ensuite de la motivation et du
                        succ&egrave;s. Tous les rouages sont en place pour cr&eacute;er une dynamique
                        d&rsquo;&eacute;quipe forte et puissante&thinsp;!</p>
                    <p><strong>Les valeurs jouent le r&ocirc;le de r&egrave;gle du jeu. </strong>Elles r&eacute;gissent
                        les relations entre les personnes et d&eacute;finissent ce que nous pouvons faire ou non :
                        <strong>int&eacute;grit&eacute;</strong>, <strong>solidarit&eacute;</strong>,
                        <strong>bienveillance </strong>ou encore <strong>respect </strong>sont des exemples de valeurs
                        qui peuvent d&eacute;terminer la bonne attitude &agrave; avoir.</p>
                    <p>Ces valeurs doivent &ecirc;tre partag&eacute;es par toutes les &eacute;quipes. Ainsi, est-il
                        important de prendre le temps pour qu&rsquo;elles puissent &ecirc;tre d&eacute;finies et
                        adopt&eacute;es par tout le monde. Elles doivent aussi <strong>&ecirc;tre garanties</strong>.
                        Cela signifie que si certains ne les respectent pas, une action sp&eacute;cifique doit
                        &ecirc;tre mise en place pouvant aller jusqu&rsquo;&agrave; des sanctions potentielles et un
                        rappel &agrave; l&rsquo;ordre.</p>
                    <h5>3. COMMENT INSTAURER UN CLIMAT DE CONFIANCE POUR SES SALARI&Eacute;S ?</h5>
                    <p>La confiance est le second point essentiel pour motiver ses salari&eacute;s. En effet, elle
                        <strong>permettra &agrave; nos salari&eacute;s de sentir qu&rsquo;ils sont importants</strong>,
                        qu&rsquo;ils ont bien quelque chose &agrave; apporter &agrave; l&rsquo;entreprise et que nous
                        comptons sur eux pour le faire. Ils se sentiront donc bien.</p>
                    <p>La confiance permet aussi d&rsquo;essayer de nouvelles actions, de tenter quelque chose de
                        nouveau. Les salari&eacute;s n&rsquo;auront pas peur de le faire, ce qui permettra une
                        <strong>&eacute;volution des proc&eacute;dures, des mani&egrave;res d&rsquo;agir ou encore
                            d&rsquo;innover. </strong>Ainsi, il sera plus facile et plus efficace d&rsquo;atteindre les
                        objectifs.</p>
                    <p>La confiance est donc bonne pour l&rsquo;entreprise et ses salari&eacute;s. Elle se cultive
                        &agrave; travers le <strong>droit &agrave; l&rsquo;erreur et l&rsquo;autonomie. </strong>Des
                        encouragements et de la reconnaissance de mani&egrave;re r&eacute;guli&egrave;re montreront la
                        confiance des dirigeants dans ses collaborateurs.</p>
                    <p>Assurer le respect est aussi un moyen de comprendre comment motiver ses salari&eacute;s. Le
                        respect c&rsquo;est aussi ce qui fait que chacun peut exister et se sentir important. Si je suis
                        respect&eacute;, c&rsquo;est que j&rsquo;existe et que je suis consid&eacute;r&eacute; comme
                        quelqu&rsquo;un d&rsquo;important.</p>

                    <br>
                    <br>
                    <h5>4. QU&rsquo;EST CE QUE L&rsquo;AUTONOMIE ET LA RESPONSABILIT&Eacute; DE SES SALARI&Eacute;S ?
                    </h5>
                    <br>
                    <p>Donner de l&rsquo;autonomie est aussi un autre facteur cl&eacute; de succ&egrave;s pour motiver
                        ses salari&eacute;s. Donner de l&rsquo;autonomie &ccedil;a veut dire <strong>donner les moyens
                            &agrave; nos &eacute;quipes d&rsquo;atteindre leurs objectifs ou de r&eacute;aliser leur
                            travail par elles-m&ecirc;mes, en toute ind&eacute;pendance.</strong></p>
                    <p>Leur donner de l&rsquo;autonomie c&rsquo;est leur donner la possibilit&eacute; de prendre des
                        d&eacute;cisions, de d&eacute;terminer et d&rsquo;obtenir tous les moyens n&eacute;cessaires
                        ainsi que d&rsquo;agir pour faire ce qu&rsquo;elles ont &agrave; faire.</p>
                    <p>Donner de l&rsquo;autonomie valorise les salari&eacute;s en leur donnant une haute importance,
                        une plus grande confiance, ainsi que la capacit&eacute; d&rsquo;agir plus librement pour faire
                        son travail. L&rsquo;autonomie va de pair avec</p>
                    <p>la <strong>responsabilisation. </strong>Responsabiliser, c&rsquo;est rendre quelqu&rsquo;un
                        conscient qu&rsquo;il devra r&eacute;pondre de ses actes. C&rsquo;est donc un point cl&eacute;
                        pour savoir comment motiver ses salari&eacute;s. La responsabilisation est extr&ecirc;mement
                        motivante parce que le salari&eacute; sait que ses actions d&eacute;pendent vraiment de lui. Il
                        pourra donc vraiment faire en fonction de ce qu&rsquo;il croit et r&eacute;fl&eacute;chira
                        &agrave; deux fois avant d&rsquo;agir.</p>
                    <p>Ainsi, ce mode de fonctionnement permet aux salari&eacute;s de d&eacute;cider et d&rsquo;agir en
                        toute connaissance de cause.</p>
                    <p>Cela cr&eacute;e de la motivation par le fait que les personnes sentent qu&rsquo;elles ont un
                        <strong>impact sur leur travail. </strong>Elles ont le sentiment de faire avancer les projets.
                        Elles se sentent importantes et utiles, d&rsquo;o&ugrave; la</p>
                    <p>motivation. Suivant le niveau de responsabilit&eacute;s, cela peut aller jusqu&rsquo;&agrave; la
                        r&eacute;alisation de soi et donc une motivation bien plus forte.</p>
                    <p>&nbsp;</p>
                    <h5>5. OPTIMISER LES R&Eacute;UNIONS ET FLUIDIFIER LA HI&Eacute;RARCHIE</h5>
                    <p>Parmi les facteurs de d&eacute;motivation de la vie en entreprise, la
                        &ldquo;<strong>r&eacute;unionite&rdquo; </strong>caracole en t&ecirc;te du classement. Une
                        r&eacute;union inutile ou encore trop longue fait perdre le sens de l&rsquo;action, et
                        cr&eacute;e, chez le collaborateur, une hantise de la prochaine rencontre. Non seulement les
                        salari&eacute;s perdront du temps, mais en plus ils ne pourront pas faire ce qui est important
                        et qui cr&eacute;e de la valeur.</p>
                    <p>Pour savoir comment motiver ses salari&eacute;s, il faut <strong>supprimer tout ce qui ne
                            cr&eacute;e pas de valeur : </strong>r&eacute;unions inutiles, reportings inutiles,
                        effectuer des trajets alors que nous pouvons avoir une Visioconf&eacute;rence, etc.</p>
                    <p>Ainsi, chacun aura plus de temps pour se consacrer &agrave; l&rsquo;essentiel : faire ce qui
                        cr&eacute;e de la valeur au travail pour passer du temps pour sa vie personnelle.</p>
                    <p>En ce qui concerne la hi&eacute;rarchie, il est certain que dans l&rsquo;entreprise
                        traditionnelle il y a une hi&eacute;rarchie pour le &laquo;&thinsp;commandement&thinsp;&raquo;.
                        Cependant, il faut bien faire la diff&eacute;rence entre les avantages li&eacute;s &agrave; la
                        fonction (comme une voiture de fonction parce que nous nous d&eacute;pla&ccedil;ons
                        r&eacute;guli&egrave;rement voir des clients) qui sont tout &agrave; fait justifi&eacute;s et
                        les avantages que nous nous octroyons parce que nous sommes le manager : une voiture de fonction
                        qui ne sert que pour la vie priv&eacute;e. C&rsquo;est en faisant sentir aux gens qu&rsquo;ils
                        sont importants et en les faisant grandir qu&rsquo;ils deviennent motiv&eacute;s.</p>
                    <p>&nbsp;</p>
                    <h5>6. COMMENT APPRENDRE &Agrave; &Eacute;COUTER SES COLLABORATEURS ?</h5>
                    <p>Souvent les collaborateurs ne se sentent pas assez &eacute;cout&eacute;s et compris. Cet
                        &eacute;tat de fait n&rsquo;est pas n&eacute;cessairement li&eacute; &agrave; leur
                        &eacute;volution, la recherche d&rsquo;une augmentation ou d&rsquo;une promotion. Il
                        s&rsquo;agit tr&egrave;s souvent d&rsquo;un <strong>besoin basique d&rsquo;avoir le sentiment
                            d&rsquo;&ecirc;tre &eacute;cout&eacute; et compris.</strong></p>
                    <p>En g&eacute;n&eacute;ral, le besoin d&rsquo;&eacute;coute vient du fait que les collaborateurs
                        cherchent juste &agrave; ce que leur manager comprenne ce qu&rsquo;il traverse ou ce
                        qu&rsquo;ils vivent. Savoir comment motiver ses salari&eacute;s c&rsquo;est savoir les
                        &eacute;couter en toute simplicit&eacute;. Pour cela, il faudra mettre en place une
                        <strong>&eacute;coute active</strong>, montrer l&rsquo;exemple et illustrer nos propos.</p>
                    <p><strong>&Ecirc;tre exemplaire c&rsquo;est surtout rassurer, respecter, d&eacute;velopper la
                            coh&eacute;sion d&rsquo;&eacute;quipe. </strong>Ces notions cr&eacute;ent incontestablement
                        de la motivation. &Ecirc;tre exemplaires, c&rsquo;est aussi montrer que l&rsquo;on fait partie
                        de l&rsquo;&eacute;quipe. Les managers doivent ainsi &ecirc;tre exemplaires pour montrer
                        qu&rsquo;ils ne sont pas au-dessus de l&rsquo;&eacute;quipe. C&rsquo;est aussi une question de
                        respect par rapport aux salari&eacute;s, car nous ne les positionnons pas en dessous de nous.
                        Donner de la reconnaissance s&rsquo;av&egrave;re aussi essentiel pour savoir comment motiver ses
                        salari&eacute;s. Cela signifie simplement <strong>dire &agrave; un de ses collaborateurs qui a
                            bien fait son travail ou qui a bien agi que nous sommes satisfaits de son travail.
                        </strong>Il est vrai que dans l&rsquo;activit&eacute; au quotidien, il est parfois difficile de
                        s&rsquo;arr&ecirc;ter pour donner de la reconnaissance, mais c&rsquo;est essentiel pour que le
                        collaborateur soit rassur&eacute; sur son travail, sur notre point de vue. De plus, cela lui
                        donnera un sentiment de satisfaction qui sera la source de sa motivation. Enfin, cela
                        contribuera &agrave; l&rsquo;encourager &agrave; refaire ce qu&rsquo;il a bien fait et donc
                        &agrave; poursuivre son travail de qualit&eacute;.</p>
                    <p>&nbsp;</p>
                    <h5>7. POURQUOI FORMER SES COLLABORATEURS?</h5>
                    <p>Permettre aux collaborateurs de suivre des formations enverra un signal fort &agrave;
                        l&rsquo;&eacute;quipe. Il s&rsquo;agit en effet de montrer que l&rsquo;entreprise compte sur eux
                        en investissant en eux. Cela leur donnera de l&rsquo;importance &agrave; court terme, mais aussi
                        &agrave; long terme. En effet, une action de formation est une action faite au moment
                        pr&eacute;sent, mais dont les b&eacute;n&eacute;fices arrivent &agrave; moyen ou long terme.</p>
                    <p>Au Maroc, il existe des <strong>organismes publics de formation </strong>tels que l&rsquo;OFPPT,
                        ainsi que des <strong>organismes priv&eacute;s </strong>de formation. Les deux types peuvent
                        proposer des formations adapt&eacute;es &agrave; vos collaborateurs. En fonction de vos besoins.
                    </p>
                    <p>De plus, d&eacute;velopper ses comp&eacute;tences, c&rsquo;est le faire grandir et le faire
                        devenir une &laquo;&thinsp;meilleure version de lui-m&ecirc;me&thinsp;&raquo;. C&rsquo;est
                        <strong>extr&ecirc;mement stimulant </strong>et cela cr&eacute;e de la motivation.</p>
                    <p>Ensuite, en d&eacute;veloppant les comp&eacute;tences, les collaborateurs pourront
                        progressivement prendre de plus en plus de responsabilit&eacute;s et &eacute;voluer
                        hi&eacute;rarchiquement au sein de l&rsquo;entreprise. Il est certain que cela les motivera, car
                        ils se sentiront de plus en plus importants et utiles.</p>
                    <p>&nbsp;</p>
                    <h5>8. COMMENT G&Eacute;RER LE TEMPS DE TRAVAIL DE SES COLLABORATEURS ?</h5>
                    <p>Autre point pour savoir comment motiver ses salari&eacute;s : <strong>leur permettre de mieux
                            &eacute;quilibrer leur vie priv&eacute;e et leur vie professionnelle. </strong>En effet,
                        c&rsquo;est toujours une attente forte de pouvoir concilier les deux</p>
                    <p>au maximum. Pour cela, il faudra ajuster au mieux leurs horaires et permettre &agrave; ses
                        employ&eacute;s de s&rsquo;organiser au mieux en fonction de leurs contraintes.</p>
                    <p>Suivant les secteurs d&rsquo;activit&eacute;, cela peut &ecirc;tre plus ou moins simple. Mais
                        l&rsquo;int&eacute;r&ecirc;t est de rester orient&eacute; sur les r&eacute;sultats et sur les
                        livrables plut&ocirc;t que de suivre le temps pass&eacute;. Beaucoup d&rsquo;entreprises mettent
                        de l&rsquo;&eacute;nergie &agrave; suivre les heures pass&eacute;es au bureau &agrave; travers
                        le pointage par exemple.</p>
                    <p>Aujourd&rsquo;hui, et notamment avec la crise du COVID-19, l&rsquo;on se rend compte que
                        <strong>la productivit&eacute; du salari&eacute; n&rsquo;est pas li&eacute;e au nombre
                            d&rsquo;heures qu&rsquo;il travaille, et que l&rsquo;autonomie peut &ecirc;tre source
                            d&rsquo;un regain notoire de productivit&eacute;. </strong>Il faut donc rester flexible dans
                        la gestion des horaires de ses employ&eacute;s et se focaliser sur les temps &agrave; forte
                        valeur ajout&eacute;e (r&eacute;unions, comit&eacute;s, rencontres clients, production de
                        livrables, etc.)</p>
                    <p>Les <strong>espaces de travail </strong>incluant les <strong>espaces de pause </strong>sont aussi
                        tr&egrave;s importants pour motiver les salari&eacute;s. En effet, les &eacute;quipes y passent
                        beaucoup de temps, et l&rsquo;environnement influence m&eacute;caniquement leur moral et leur
                        motivation. Si le cadre de travail est tr&egrave;s basique, voire d&eacute;labr&eacute;, le
                        message envoy&eacute; est que les conditions de travail des salari&eacute;s importent peu. Ce
                        qui p&egrave;sera sur la motivation.</p>
                    <p>&Agrave; l&rsquo;inverse, un cadre de travail agr&eacute;able avec un espace de d&eacute;tente
                        confortable et bien &eacute;quip&eacute; enverra le message que les conditions de travail des
                        salari&eacute;s sont importantes. En plus d&rsquo;avoir de bonnes conditions de travail, ce qui
                        est source de motivation au travail, les salari&eacute;s se sentiront donc
                        consid&eacute;r&eacute;s et respect&eacute;s. Ils seront donc doublement motiv&eacute;s.</p>
                    <p>&nbsp;</p>
                    <h5>9. COMMENT MANAGER PAR LE R&Eacute;SULTAT ?</h5>
                    <p><strong>Donner des objectifs c&rsquo;est donner un but &agrave; ses collaborateurs. </strong>Non
                        seulement le fait d&rsquo;avoir des objectifs motive de mani&egrave;re cons&eacute;quente une
                        &eacute;quipe &agrave; aller de l&rsquo;avant, mais une fois l&rsquo;objectif atteint, les
                        collaborateurs seront satisfaits du travail accompli et seront donc encore plus motiv&eacute;s.
                    </p>
                    <p>Cependant, pour bien s&rsquo;entendre et r&eacute;ussir le pari de la gestion par objectifs, ces
                        derniers doivent respecter un certain nombre de r&egrave;gles, et notamment &ecirc;tre
                        <strong>SMART </strong>:</p>
                    <ul>
                        <li><strong>Sp&eacute;cifique </strong>: l&rsquo;objectif doit &ecirc;tre personnalis&eacute; en
                            fonction de la personne responsable de le r&eacute;aliser. Plus il sera simple, plus il sera
                            efficace ;</li>
                        <li><strong>Mesurable </strong>: il doit &ecirc;tre mesurable, &agrave; savoir, nous devons
                            pouvoir &eacute;valuer si l&rsquo;objectif a &eacute;t&eacute; atteint ;</li>
                        <li><strong>Ambitieux et Accept&eacute; </strong>: cet objectif doit susciter
                            l&rsquo;adh&eacute;sion pour impliquer les collaborateurs et &ecirc;tre suffisamment
                            ambitieux pour que le r&eacute;sultat soit marquant ;</li>
                        <li><strong>R&eacute;aliste </strong>: l&rsquo;objectif doit &ecirc;tre accessible pour ne pas
                            d&eacute;motiver les &eacute;quipes ;</li>
                        <li><strong>Temporellement d&eacute;fini </strong>: il doit y avoir un d&eacute;lai avec des
                            dates</li>
                    </ul>
                    <p>&nbsp;</p>
                    <h5>10. COMMENT G&Eacute;RER LES CONFLITS ?</h5>
                    <p>L&rsquo;information est souvent consid&eacute;r&eacute;e dans l&rsquo;entreprise comme un
