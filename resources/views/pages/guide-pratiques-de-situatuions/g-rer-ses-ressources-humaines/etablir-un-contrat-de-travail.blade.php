                    <h5>1. POUR QUEL CONTRAT DE TRAVAIL OPTER ?</h5>
                    <p>Le code du travail marocain d&eacute;taille <strong>trois types de contrats </strong>de travail :</p>
                    <ul>
                    <li>Le Contrat &agrave; Dur&eacute;e Ind&eacute;termin&eacute;e (<strong>CDI</strong>)</li>
                    <li>Le Contrat &agrave; Dur&eacute;e D&eacute;termin&eacute;e (<strong>CDD</strong>)</li>
                    <li>Le Contrat pour une <strong>mission sp&eacute;cifique</strong></li>
                    </ul>
                    <p>A ces trois contrats, il faut ajouter les <strong>contrats d&rsquo;insertion </strong>(dits &laquo; contrats ANAPEC &raquo;) d&eacute;livr&eacute;s par l&rsquo;agence et donnant lieu &agrave; des exon&eacute;rations fiscales.</p>
                    <p>Ci-dessous sont d&eacute;taill&eacute;s les principaux contrats :</p>
                    <p>&nbsp;</p>
                    <p><strong>CDI</strong></p>
                    <ul>
                    <li><strong>Caract&eacute;ristiques </strong>: contrat conclu sans limitation de dur&eacute;e. La rupture d&rsquo;un CDI est souvent difficile et peut s&rsquo;av&eacute;rer co&ucirc;teuse pour l&rsquo;employeur. Les licenciements doivent &ecirc;tre justifi&eacute;s et sont soumis &agrave; des r&egrave;gles l&eacute;gales assez drastiques (proc&eacute;dure, pr&eacute;avis, indemnit&eacute;s&hellip;)</li>
                    <li><strong>Crit&egrave;res d&rsquo;&eacute;ligibilit&eacute; </strong>: aucun</li>
                    <li><strong>A savoir </strong>: Les CDI sont assortis d&rsquo;une p&eacute;riode d&rsquo;essai renouvelable uniquement une fois durant laquelle l&rsquo;employeur ou l&rsquo;employ&eacute; peuvent rompre le contrat sans pr&eacute;avis ni indemnit&eacute;. La dur&eacute;e maximale de cette p&eacute;riode d&rsquo;essai est de :</li>
                    <li>15 jours pour les ouvriers</li>
                    <li>1 mois et demi pour les employ&eacute;s</li>
                    <li>3 mois pour les cadres</li>
                    </ul>
                    <p><strong>CDD</strong></p>
                    <ul>
                    <li><strong>Caract&eacute;ristiques </strong>: contrat temporaire conclu de date &agrave; date con&ccedil;u pour pourvoir un emploi qui n&rsquo;est pas li&eacute; &agrave; l&rsquo;activit&eacute; normale de l&rsquo;entreprise. Sauf cas grave ou cas de force majeure, le CDD ne peut &ecirc;tre rompu avant son terme sans accord des deux</li>
                    <li><strong>Crit&egrave;res d&rsquo;&eacute;ligibilit&eacute; </strong>: On ne peut recourir au CDD que dans les cas suivants :</li>
                    <li>Remplacement d&rsquo;un salari&eacute; absent</li>
                    <li>Accroissement temporaire de l&rsquo;activit&eacute; de l&rsquo;entreprise</li>
                    <li>Travail &agrave; caract&egrave;re saisonnier</li>
                    <li>Nouvelle ouverture d&rsquo;&eacute;tablissement</li>
                    <li><strong>A savoir </strong>: La dur&eacute;e maximale d&rsquo;un CDD est de 12 mois non renouvelable sauf cas exceptionnels (quand le salari&eacute; est embauch&eacute; pour la cr&eacute;ation d&rsquo;une entreprise par exemple). Pass&eacute; 12 mois de travail, le CDD se transforme automatiquement et tacitement en CDI.</li>
                    </ul>
                    <p>&nbsp;</p>
                    <p><strong>Contrat d&rsquo;insertion</strong></p>
                    <ul>
                    <li><strong>Caract&eacute;ristiques </strong>: Contrat de stage d&rsquo;une dur&eacute;e de 24 mois ciblant les jeunes dipl&ocirc;m&eacute;s</li>
                    <li><strong>Crit&egrave;res d&rsquo;&eacute;ligibilit&eacute; </strong>: Le salari&eacute; doit r&eacute;pondre aux crit&egrave;res suivants :</li>
                    <li>&Ecirc;tre inscrit &agrave; l&rsquo;ANAPEC</li>
                    <li>&Ecirc;tre au moins dipl&ocirc;m&eacute; du Bac</li>
                    <li>Avoir un salaire compris entre 1600 et 6000 dirhams</li>
                    <li><strong>A savoir </strong>: Ce contrat donne lieu &agrave; une exon&eacute;ration d&rsquo;IR et des cotisations CNSS/TFP pendant 24 mois. Cette exon&eacute;ration est prolong&eacute;e de 12 mois si le stage d&eacute;bouche sur un contrat de droit commun</li>
                    </ul>
                    <h5>2. QUELLES SONT LES R&Egrave;GLES &Agrave; RESPECTER EN TERMES DE R&Eacute;MUN&Eacute;RATION ?</h5>
                    <p>M&ecirc;me si le chef d&rsquo;entreprise b&eacute;n&eacute;ficie d&rsquo;une grande libert&eacute; dans le choix du niveau de r&eacute;mun&eacute;ration de ses salari&eacute;s, il doit toutefois respecter certaines r&egrave;gles :</p>
                    <ul>
                    <li>Proposer un salaire au moins &eacute;gal au salaire minimum (SMIG) :</li>
                    </ul>
                    <p>Le montant du SMIG est fix&eacute; et mis &agrave; jour r&eacute;guli&egrave;rement au niveau national</p>
                    <ul>
                    <li>Conna&icirc;tre ses obligations d&rsquo;&eacute;galit&eacute; de traitement :</li>
                    </ul>
                    <p>Le principe &laquo; &agrave; salaire &eacute;gal, travail &eacute;gal &raquo; doit &ecirc;tre respect&eacute; au moment de fixer les salaires de base. L&rsquo;employeur ne peut d&eacute;cider de r&eacute;mun&eacute;rer diff&eacute;remment deux personnes exer&ccedil;ant exactement la m&ecirc;me fonction.</p>
                    <p>Bon &agrave; savoir : Le SMIG a &eacute;t&eacute; revaloris&eacute; par rapport au 1er juillet 2019, le taux horaire passant de 14,13 <strong>DH &agrave; 14,81 dirhams au 1er juillet 2020 sur la base de 191 heures de travail<br /></strong><strong>par mois. Le salaire </strong><a href="http://www.maroc.ma/">mensuel </a><strong>brut est d&eacute;sormais de 2.828,71 dirhams et le salaire mensuel net passe de 2.517 &agrave; 2.638 dirhams.<br /><br /></strong></p>
                    <h5>3. COMMENT &Eacute;TABLIR UN CONTRAT DE TRAVAIL ?</h5>
                    <p>En l&rsquo;absence d&rsquo;un service RH, la r&eacute;daction de votre contrat de travail s&rsquo;effectue le plus souvent avec votre <strong>fiduciaire</strong>, parfois avec votre <strong>comptable</strong>.</p>
                    <p>Lors de l&rsquo;&eacute;laboration de ce contrat, nous vous invitons &agrave; v&eacute;rifier avec votre service RH / fiduciaire / comptable que l&rsquo;ensemble des &eacute;l&eacute;ments ci-dessous sont bien mentionn&eacute;s dans votre contrat de travail : <strong>Mentions &agrave; ne pas oublier :</strong></p>
                    <ul>
                    <li>Description du poste</li>
                    <li>Date de d&eacute;but du contrat (et de fin selon le type de contrat)</li>
                    <li>Salaire brut, primes associ&eacute;es et modalit&eacute;s de versement</li>
                    <li>Lieu de travail</li>
                    <li>Mobilit&eacute;s pr&eacute;vues</li>
                    <li>Dur&eacute;e de travail hebdomadaire et horaires journaliers</li>
                    <li>Nombre de jours de cong&eacute;s par an</li>
                    <li>Dur&eacute;e de la p&eacute;riode d&rsquo;essai (en cas de p&eacute;riode d&rsquo;essai)</li>
                    <li>Dur&eacute;e du pr&eacute;avis de rupture du contrat</li>
                    </ul>
                    <p>Clauses sp&eacute;cifiques &agrave; pr&eacute;voir :</p>
                    <ul>
                    <li>Clause <strong>d&rsquo;exclusivit&eacute; </strong>: interdit au salari&eacute; de se livrer &agrave; des activit&eacute;s concurrentes de celle de l&lsquo;entreprise ;</li>
                    <li>Clause d&rsquo;obligation de <strong>discr&eacute;tion </strong>: engage le salari&eacute; &agrave; ne divulguer aucun document, information ou donn&eacute;e sur l&rsquo;entreprise pendant et apr&egrave;s la dur&eacute;e du contrat ;</li>
                    <li>Clause de <strong>non concurrence </strong>: interdit au salari&eacute; de travailler pour des entreprises concurrentes sur le territoire marocain et pendant une certaine p&eacute;riode apr&egrave;s la rupture du contrat. Pour que cette clause soit effective, il est important de pr&eacute;ciser une <strong>dur&eacute;e</strong>, un <strong>p&eacute;rim&egrave;tre </strong>g&eacute;ographique et de pr&eacute;voir des d&eacute;dommagements de votre part en contrepartie de cette contrainte et de la part du salari&eacute; en cas de non- respect et de cette clause ;</li>
                    <li>Clause <strong>d&rsquo;objectifs </strong>: conditionne la poursuite du contrat de travail &agrave; la r&eacute;alisation d&rsquo;objectifs pr&eacute;d&eacute;finis ;</li>
                    <li>Clause du <strong>droit d&rsquo;image </strong>: dans le cas o&ugrave; le salari&eacute; peut &ecirc;tre amen&eacute; &agrave; repr&eacute;senter directement ou indirectement son entreprise, cette clause donne l&rsquo;autorisation &agrave; l&rsquo;employeur d&rsquo;utiliser son image ;</li>
                    <li>Clause <strong>d&rsquo;invention </strong>: dans le cas o&ugrave; un salari&eacute; peut &ecirc;tre amen&eacute; &agrave; r&eacute;aliser une invention dans le cadre de son travail, cette clause permet d&rsquo;organiser les effets de cette invention (propri&eacute;t&eacute; intellectuelle, r&eacute;mun&eacute;ration exceptionnelle&hellip;)</li>
                    </ul>
                    <h5>4. QUELLES SONT LES FORMALIT&Eacute;S ADMINISTRATIVES &Agrave; ACCOMPLIR POUR PERMETTRE &Agrave; MON ENTREPRISE DE RECRUTER ?</h5>
                    <p>Afin de recruter dans les r&egrave;gles de l&rsquo;art, plusieurs formalit&eacute;s sont &agrave; respecter, dont voici la liste exhaustive:</p>
                    <h3>D&eacute;claration d&rsquo;existence &agrave; l&rsquo;inspection du travail</h3>
                    <ul>
                    <li><strong>A quel moment effectuer cette formalit&eacute; ? : </strong>Avant la premi&egrave;re embauche et de pr&eacute;f&eacute;rence avant le d&eacute;but de l&rsquo;activit&eacute;</li>
                    <li><strong>Objectif : </strong>L&rsquo;inspecteur du travail est un &eacute;l&eacute;ment important de la vie d&rsquo;une entreprise. Son autorisation doit &ecirc;tre demand&eacute;e pour toute d&eacute;rogation &agrave; la l&eacute;gislation sociale</li>
                    <li><strong>A qui m&rsquo;adresser ? </strong>A l&rsquo;inspection du travail</li>
                    </ul>
                    <h3>Paraphe du Livre de paie et du livre de cong&eacute;</h3>
                    <ul>
                    <li><strong>A quel moment effectuer cette formalit&eacute; ? : </strong>Au plus tard lors de la premi&egrave;re embauche</li>
                    <li><strong>Objectif : </strong>Le livre de paie permet &agrave; l&rsquo;inspecteur de contr&ocirc;ler le respect de la r&eacute;glementation en mati&egrave;re de r&eacute;mun&eacute;ration.</li>
                    <li><strong>A qui m&rsquo;adresser ? </strong>A l&rsquo;inspection du travail</li>
                    <li>Demande d&rsquo;immatriculation en tant qu&rsquo;employeur &agrave; la CNSS</li>
                    <li><strong>A quel moment effectuer cette formalit&eacute; ? : </strong>D&egrave;s l&rsquo;embauche du premier salari&eacute;</li>
                    </ul>
                    <ul>
                    <li><strong>Objectif : </strong>Pour permettre aux salari&eacute;s de b&eacute;n&eacute;ficier des prestations sociales</li>
                    <li><strong>A qui m&rsquo;adresser ? </strong>A la CNSS</li>
                    </ul>
                    <p>&nbsp;</p>
                    <h3>Etablissement d&rsquo;un r&egrave;glement int&eacute;rieur Pour les entreprises de plus de 10 salari&eacute;s</h3>
                    <ul>
                    <li><strong>A quel moment effectuer cette formalit&eacute; ? : </strong>Dans un d&eacute;lai de 2 ans apr&egrave;s le d&eacute;marrage de l&rsquo;activit&eacute;</li>
                    <li><strong>Objectif : </strong>Afin de d&eacute;finir les conditions de travail dans l&rsquo;entreprise, comme par exemple la dur&eacute;e du travail et du repos, le salaire, les mesures de s&eacute;curit&eacute;, les sanctions. Tout salari&eacute; doit pouvoir prendre connaissance de ce r&egrave;glement, &agrave; tout moment</li>
                    <li></li>
                    </ul>
                    <table class="table" width="100%">
                    <tbody>
                    <tr>
                    <td>
                    <p><strong>A retenir </strong>: <strong>chaque fois que vous recrutez un salari&eacute;, il vous faut penser &agrave; effectuer les d&eacute;marches suivantes</strong></p>
                    </td>
                    </tr>
                    </tbody>
                    </table>
                    <ul>
                    <li><strong>A qui m&rsquo;adresser ? </strong>Consulter les repr&eacute;sentants des travailleurs avant l&rsquo;&eacute;laboration du r&egrave;glement</li>
                    </ul>
                    <ul>
                    <li><strong>Immatriculation &agrave; la CNSS </strong>: assurez-vous d&rsquo;abord que votre nouveau salari&eacute; dispose bien d&rsquo;un num&eacute;ro d&rsquo;immatriculation &agrave; la CNSS car ce num&eacute;ro lui est attribu&eacute; pour toute sa vie professionnelle et qu&rsquo;une double immatriculation pourrait le l&eacute;ser dans ses droits. S&rsquo;il n&rsquo;est pas immatricul&eacute;, vous devez effectuer cette immatriculation</li>
                    <li><strong>D&eacute;claration du salari&eacute; &agrave; l&rsquo;assurance Accident du Travail </strong>(ainsi que toutes les assurances compl&eacute;mentaires souscrites par l&rsquo;entreprise)</li>
                    <li>D&eacute;claration du salari&eacute; &agrave; la CIMR (rappel : l&rsquo;affiliation de votre entreprise &agrave; la CIMR n&rsquo;est pas obligatoire)</li>
                    <li><strong>Enregistrement de votre salari&eacute; aupr&egrave;s de vos prestataires </strong>(cantine, informatique&hellip;)</li>
                    <li>Octroi de la carte du travail</li>
                    <li>Formation sur les mesures de s&eacute;curit&eacute;</li>
                    <li>Cr&eacute;ation d&rsquo;un dossier administratif pour le collaborateur</li>
                    </ul>
