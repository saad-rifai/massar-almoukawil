                    <p>&nbsp;</p>
                    <p>Structurez bien votre entretien et tenez-vous-en &agrave; cette structure car le temps passe toujours rapidement. En r&egrave;gle g&eacute;n&eacute;rale, un entretien se d&eacute;roule en 4 temps :</p>
                    <ul>
                    <li><strong>Se pr&eacute;senter : </strong>le recruteur se pr&eacute;sente, rappelle le contexte de l&rsquo;entretien et le poste &agrave; pourvoir. Il d&eacute;taille le d&eacute;roul&eacute; &agrave; venir de l&rsquo;entretien au candidat.</li>
                    </ul>
                    <p>Il s&rsquo;agit de donner une bonne impression d&egrave;s le d&eacute;part et de mettre le candidat en confiance afin de d&eacute;gager le plus de valeur possible de cet entretien</p>
                    <ul>
                    <li><strong>&Eacute;changer et &eacute;valuer le candidat : </strong>cette s&eacute;quence sert &agrave; comprendre le profil du candidat, sa formation, ses exp&eacute;riences, sa motivation et sa personnalit&eacute;. Cette &eacute;tape se d&eacute;roule g&eacute;n&eacute;ralement en 2-3 temps :
                    <ol>
                    <li>Le candidat se pr&eacute;sente (bri&egrave;vement)</li>
                    <li>Le recruteur pose des questions sur le profil, l&rsquo;exp&eacute;rience, les comp&eacute;tences&hellip;etc. du candidat Certains recruteurs en profitent pour faire des exercices avec le candidat pour &eacute;valuer ses comp&eacute;tences (cas pratiques, tests&hellip;etc.). Ces exercices se pr&eacute;parent n&eacute;cessairement &agrave; l&rsquo;avance.</li>
                    </ol>
                    </li>
                    </ul>
                    <p>Cette s&eacute;quence est primordiale pour bien saisir l&rsquo;identit&eacute; du candidat, consacrez au moins 30 minutes &agrave; cette partie.</p>
                    <ol start="3">
                    <li><strong>Pr&eacute;sentation de l&rsquo;entreprise : </strong>le recruteur pr&eacute;sente l&rsquo;entreprise, son fonctionnement, sa culture, ses &eacute;quipes (le cas &eacute;ch&eacute;ant, l&rsquo;&eacute;quipe dans laquelle se situe le poste &agrave; pourvoir) &hellip;etc. L&rsquo;objectif est d&rsquo;&eacute;valuer la compatibilit&eacute; du candidat avec l&rsquo;entreprise et de confirmer ou d&rsquo;infirmer sa motivation en observant ses r&eacute;actions et ses questions.</li>
                    </ol>
                    <p>Ne sous-estimez pas cette s&eacute;quence, elle est tr&egrave;s importante car elle permet de s&rsquo;assurer que le candidat a r&eacute;ellement la capacit&eacute; et l&rsquo;envie de s&rsquo;adapter &agrave; l&rsquo;entreprise et d&rsquo;y apporter de la valeur. Pr&eacute;voyez au moins 20 minutes.</p>
                    <ol start="4">
                    <li><strong>Faire le bilan avec le candidat : </strong>il s&rsquo;agit ici de cl&ocirc;turer efficacement l&rsquo;entretien. Demandez au candidat s&rsquo;il a d&rsquo;autres questions et expliquez la suite du processus. Vous pouvez &eacute;galement demander au candidat de partager ses impressions sur le d&eacute;roul&eacute; de l&rsquo;entretien afin de vous am&eacute;liorer davantage pour la</li>
                    </ol>
                    <p>Dans ce cas, vous pouvez &eacute;galement partager vos propres impressions et fournir des conseils au candidat pour la suite.</p>
                
