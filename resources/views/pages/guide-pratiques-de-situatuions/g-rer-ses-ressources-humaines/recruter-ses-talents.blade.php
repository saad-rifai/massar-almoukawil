                    <h5>1. COMMENT D&Eacute;FINIR MON BESOIN EN RECRUTEMENT ?</h5>
                    <p>En amont de toutes vos recherches de candidats, il est important de bien <strong>qualifier votre besoin </strong>et <strong>pr&eacute;ciser le profil </strong>que vous recherchez.</p>
                    <p>Parmi les crit&egrave;res que vous devez &eacute;tablir dans la fiche de poste, voici les plus communs :</p>
                    <ul>
                    <li>Formation acad&eacute;mique : formation sp&eacute;cifique, niveau licence, master,</li>
                    <li>Domaines de comp&eacute;tences : droit, gestion, sciences appliqu&eacute;es..</li>
                    <li>Niveau d&rsquo;exp&eacute;rience : d&eacute;butant, 2, 5, 10 ans d&rsquo;exp&eacute;riences</li>
                    <li>Type de contrat de travail propos&eacute; : stage r&eacute;mun&eacute;r&eacute; ou non, CDD, CDI, alternance</li>
                    <li>Qualit&eacute;s recherch&eacute;es : esprit d&rsquo;&eacute;quipe, polyvalent, rigoureux, .</li>
                    <li>Clauses sp&eacute;cifiques : mobilit&eacute;, connaissances linguistiques / culturelles&hellip;</li>
                    </ul>
                    <p>La fiche de poste que vous &eacute;tablirez permettra de fixer le profil adapt&eacute; pour la fonction que vous devez combler, et doit mentionner l&rsquo;intitul&eacute; du poste, les activit&eacute;s principales et secondaires du poste, les liaisons hi&eacute;rarchiques et fonctionnelles (organigramme), le niveau de r&eacute;mun&eacute;ration ainsi que les moyens attribu&eacute;s au poste.</p>
                    <p><strong>A retenir : <em>Pensez &agrave; distinguer (pour vous-m&ecirc;me) les qualit&eacute;s indispensables pour le poste et celles qui sont seulement secondaires</em></strong></p>
                    <p>&nbsp;</p>
                    <h5>2. COMMENT FAIRE APPEL &Agrave; L&rsquo;AGENCE NATIONALE DE PROMOTION DES EMPLOIS ET DES COMP&Eacute;TENCES (ANAPEC) ?</h5>
                    <p>Parmi les organismes publics que vous avez &agrave; votre disposition et proposent un service gratuit, il y a l&rsquo;Agence Nationale de Promotion des Emplois et des Comp&eacute;tences (<strong>ANAPEC</strong>), &eacute;tablissement public &agrave; caract&egrave;re administratif, charg&eacute; de l&rsquo;emploi au Maroc.</p>
                    <p>Pour rechercher des candidats, vous avez plusieurs possibilit&eacute;s :</p>
                    <ul>
                    <li>Aller sur le site de l&rsquo;ANAPEC sur <a href="http://www.anapec.org/" target="_blank" rel="noopener">anapec.org/ </a>sur et consulter les offres d&rsquo;emploi mises en ligne ;</li>
                    <li>D&eacute;poser une annonce sur le site ;</li>
                    <li>Se rendre dans une agence, et expliciter votre demande aupr&egrave;s de conseillers qui vous fourniront en retour une liste de CV correspondant &agrave; votre recherche.</li>
                    </ul>
                    <p>Un nombre important de CV sont r&eacute;pertori&eacute;s sur le site de l&rsquo;ANAPEC, de niveau in&eacute;gal et qui ne correspondent pas toujours &agrave; votre demande. Pr&eacute;voyez donc de passer du temps &agrave; trier et s&eacute;lectionner les CV !</p>
                    <p><strong>Bon &agrave; savoir : </strong><strong><em>l&rsquo;ANAPEC offre des exon&eacute;rations de charges sociales et d&rsquo;Imp&ocirc;ts sur le Revenu (IR) pour les entreprises qui recrutent &agrave; travers des contrats de travail ANAPEC.</em></strong></p>
                    <p><em><strong>Pour voir les conditions et les avantages sur le site de l&rsquo;ANAPEC <a href="http://www.anapec.org/sigec-app-rv/" target="_blank" rel="noopener">http://www.anapec.org/sigec-app-</a> <a href="http://www.anapec.org/sigec-app-rv/" target="_blank" rel="noopener">rv/</a></strong></em></p>
                    <p>&nbsp;</p>
                    <h5>3. COMMENT UTILISER LES SITES D&rsquo;ANNONCE D&rsquo;EMPLOI POUR TROUVER DES CANDIDATS ?</h5>
                    <p>Il existe plusieurs sites sur lesquels vous pouvez diffuser des annonces :</p>
                    <p><strong>Des sites d&rsquo;emploi sp&eacute;cialis&eacute;s</strong>, sur lesquels la diffusion d&rsquo;annonces est souvent payante, comme <a href="https://marocemploi.net/" target="_blank" rel="noopener">marocemploi.net, </a><a href="https://www.emploi.ma/" target="_blank" rel="noopener">emploi.ma, </a><a href="https://www.rekrute.com/" target="_blank" rel="noopener">Rekrute.com</a></p>
                    <p>En plus de la possibilit&eacute; de diffuser des annonces, ces sites peuvent proposer des services annexes :</p>
                    <ul>
                    <li>Acc&egrave;s &agrave; une base de donn&eacute;es de CV avec un syst&egrave;me de recherche, d&rsquo;archivage et d&rsquo;alertes pour les CV qui vous int&eacute;ressent ;</li>
                    <li>Acc&egrave;s &agrave; un back-office pour consulter, par exemple, le nombre de visites sur vos annonces ;</li>
                    <li>Possibilit&eacute; de mettre en avant vos annonces sur le site et de les faire appara&icirc;tre dans des campagnes d&rsquo;emailing.</li>
                    </ul>
                    <p><strong>Co&ucirc;t : </strong>certains sites proposent la diffusion d&rsquo;annonces gratuitement tandis que pour d&rsquo;autres, ce service est payant (paiement &agrave; l&rsquo;annonce ou syst&egrave;me d&rsquo;abonnement)</p>
                    <p><strong>Des sites g&eacute;n&eacute;ralistes </strong>(Menara.ma, Maroc Annonces, Avito&hellip;) sur lesquels la publication est gratuite <strong>Les services relations entreprises des &eacute;coles et universit&eacute;s </strong>marocaines ou &eacute;trang&egrave;res publient souvent des annonces sur leurs sites. Vous pourrez recruter &agrave; travers ce canal : des stagiaires, des jeunes dipl&ocirc;m&eacute;s ou des anciens dipl&ocirc;m&eacute;s de l&rsquo;&eacute;cole. Ce canal permet de cibler pr&eacute;cis&eacute;ment les &eacute;coles et universit&eacute;s</p>
                    <p><strong>Bon &agrave; savoir : </strong><strong><em>les annonces que vous postez sont en g&eacute;n&eacute;ral visibles pour un temps limit&eacute; (souvent 1 ou 2 mois). Pensez &agrave; &eacute;tendre la dur&eacute;e de diffusion si vous n&rsquo;avez toujours pas trouv&eacute; votre candidat</em></strong></p>
                    <p>&nbsp;</p>
                    <h5>4. COMMENT FAIRE APPEL AUX CABINETS DE RECRUTEMENT?</h5>
                    <p>Il existe de <strong>tr&egrave;s nombreux cabinets de recrutement au Maroc</strong>, sp&eacute;cialis&eacute;s selon le niveau de qualification des candidats. En ce qui concerne leur facturation, en g&eacute;n&eacute;ral, il faudra compter un mois de salaire brut, mais ce tarif peut &ecirc;tre plus &eacute;lev&eacute; pour les grands cabinets (jusqu&rsquo;&agrave; 30% du salaire annuel). Les tarifs peuvent &ecirc;tre sujets &agrave; n&eacute;gociation.</p>
                    <p>Voici les avantages et inconv&eacute;nients des cabinets de recrutement :</p>
                    <p>Avantages :</p>
                    <ul>
                    <li>Des profils tri&eacute;s sur le volet</li>
                    <li>Un gain de temps important</li>
                    <li>La discr&eacute;tion dans le recrutement</li>
                    </ul>
                    <ul>
                    <li>La possibilit&eacute; d&rsquo;avoir recours &agrave; des approches directes</li>
                    <li>La possibilit&eacute; de faire passer les entretiens aux candidats avec l&rsquo;aide du cabinet de recrutement qui apportera une expertise technique, conseil&hellip;</li>
                    </ul>
                    <p>Inconv&eacute;nients :</p>
                    <ul>
                    <li>Un co&ucirc;t &eacute;lev&eacute; pour les haut</li>
                    </ul>
                    <p><strong>A retenir </strong>: <strong><em>Recourir &agrave; des cabinets de recrutement est utile pour les profils &agrave; faible revenus (cela vous &eacute;vitera de passer du temps &agrave; trier et s&eacute;lectionner des CV sur des postes tr&egrave;s concurrenc&eacute;s &agrave; moindre co&ucirc;t), ou alors les profils tr&egrave;s sp&eacute;cifiques que l&rsquo;on n&rsquo;arrive pas &agrave; trouver sans passer par des organismes sp&eacute;cialis&eacute;s.</em></strong></p>
                    <p>&nbsp;</p>
                    <h5>5. POURQUOI CR&Eacute;ER DES &Eacute;V&Eacute;NEMENTS PR&Eacute;SENTIELS ET EN LIGNE ?</h5>
                    <p>Pour trouver la perle rare, mettez toutes vos chances de votre c&ocirc;t&eacute; et participez &agrave; des &eacute;v&eacute;nements :</p>
                    <ul>
                    <li>Des &eacute;v&eacute;nements de networking</li>
                    <li>Des salons ou foires : salon de l&rsquo;&eacute;tudiant, salons sectoriels ou th&eacute;matiques&hellip;</li>
                    <li>Forums de recrutement organis&eacute;s par les &eacute;coles et universit&eacute;s</li>
                    </ul>
                    <p>Les r&eacute;seaux sociaux sont par ailleurs devenus un outil incontournable de communication mais aussi de recrutement !</p>
                    <p>Les 2 premiers r&eacute;seaux sociaux au monde et au Maroc que vous devez absolument utiliser :</p>
                    <p><br /> <strong>LinkedIn </strong>: Le premier r&eacute;seau social professionnel au monde. L&rsquo;outil de recherche de profils et les groupes sont des moyens efficaces de trouver des candidats. L&rsquo;option payante permet d&rsquo;am&eacute;liorer le marketing de son entreprise et de cibler plus efficacement les candidats.</p>
                    <p><strong>Bon &agrave; savoir : </strong><strong><em>Vous pouvez profiter des fonctionnalit&eacute;s payantes pendant 1 mois gratuitement. Pensez &agrave; annuler l&rsquo;option &agrave; la fin du mois. Co&ucirc;t : &agrave; partir de 37&euro;/mois (~398 dhs) pour le pack simple, 75&euro;/mois (~830 dhs) pour le pack &laquo; Recruter &raquo;</em></strong></p>
                    <p>&nbsp;</p>
                    <p><strong>Facebook entreprise : </strong>Pour utiliser efficacement Facebook, cr&eacute;ez votre page Entreprise et mettez-y r&eacute;guli&egrave;rement du contenu int&eacute;ressant afin de cr&eacute;er de l&rsquo;int&eacute;r&ecirc;t et une communaut&eacute; autour de votre entreprise.</p>
                    <p>Pour recruter sur Facebook, vous avez 2 options :</p>
                    <ol>
                    <li>Publier votre offre de poste sur votre page Entreprise. Les personnes suivant votre page (votre communaut&eacute;) pourront y r&eacute;pondre ou la</li>
                    <li>Identifier et rejoindre des groupes d&rsquo;int&eacute;r&ecirc;ts (des pages Facebook o&ugrave; des professionnels d&rsquo;un m&ecirc;me secteur partagent de l&rsquo;information). Il existe des groupes Facebook centr&eacute;s autour de : Secteurs (banque, &eacute;nergie, agroalimentaire&hellip;) Fonctions (marketing, commercial&hellip;) M&eacute;tiers (d&eacute;veloppeurs, graphistes&hellip;) Etc&hellip;</li>
                    </ol>
