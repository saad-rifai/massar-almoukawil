                    <p>Pendant toute la dur&eacute;e suivant le pr&eacute;avis, le salari&eacute; a droit &agrave; des <strong>absences r&eacute;mun&eacute;r&eacute;es </strong>pour chercher un emploi &agrave; raison de 2h par jour sans exc&eacute;der 8h par semaine.</p>
