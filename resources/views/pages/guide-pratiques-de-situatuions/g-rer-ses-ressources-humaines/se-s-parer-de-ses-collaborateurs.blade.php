                    <h5>1. COMMENT METTRE FIN &Agrave; UN CONTRAT DE TRAVAIL ?</h5>
                    <p>Les contrats de travail sont r&eacute;gis par le Code du Travail Marocain, et plus pr&eacute;cis&eacute;ment pour la partie relative &agrave; la s&eacute;paration, nous nous r&eacute;f&eacute;rons <strong>au chapitre V du Code du Travail marocain (articles 32 &agrave; 78) </strong>: <strong><em>&laquo; De la suspension et de la cessation du contrat de travail &raquo;.</em></strong></p>
                    <p>D&rsquo;apr&egrave;s ce document, il y a plusieurs fa&ccedil;ons de mettre fin &agrave; un contrat de travail :</p>
                    <h5>En cas de CDD</h5>
                    <ul>
                    <li>Pour tout contrat &agrave; dur&eacute;e d&eacute;termin&eacute;e, l&rsquo;arr&ecirc;t du travail <strong>s&rsquo;effectue naturellement</strong>, <strong>sans pr&eacute;avis ni indemnit&eacute; </strong>&agrave; l&rsquo;issue du de la dur&eacute;e fix&eacute;e par le</li>
                    <li>Ce contrat ne peut s&rsquo;achever, <strong>avant terme, que si l&rsquo;employeur et le salari&eacute; d&eacute;cident tous les deux d&rsquo;y mettre fin.</strong></li>
                    <li>En cas de <strong>force majeure</strong>, la partie qui met fin au contrat doit payer &agrave; l&rsquo;autre la totalit&eacute; du salaire relative &agrave; la p&eacute;riode non travaill&eacute;e.</li>
                    </ul>
                    <h5>Retraite</h5>
                    <p>&nbsp;</p>
                    <ul>
                    <li><strong>A l&rsquo;&acirc;ge de 60 ans</strong>, tout salari&eacute; peut prendre sa retraite sauf autorisation formul&eacute;e par arr&ecirc;t&eacute; de l&rsquo;autorit&eacute; gouvernementale charg&eacute;e du travail si l&rsquo;employeur en fait la demande et que le salari&eacute; y consent.</li>
                    <li>Il n&rsquo;y a pas <strong>de r&egrave;gle g&eacute;n&eacute;rale en mati&egrave;re de pr&eacute;avis et d&rsquo;indemnit&eacute; </strong>mais chaque entreprise peut pr&eacute;voir des dispositions sp&eacute;cifiques dans sa <strong>convention collective. </strong>Il est par exemple fr&eacute;quent que les salari&eacute;s re&ccedil;oivent une indemnit&eacute; proportionnelle &agrave; leur anciennet&eacute; dans l&rsquo;entreprise.</li>
                    </ul>
                    <p>&nbsp;</p>
                    <h5>2. COMMENT G&Eacute;RER UNE D&Eacute;MISSION ?</h5>
