                    <p>Il est en revanche en droit de demander <strong>des dommages-int&eacute;r&ecirc;ts si la d&eacute;mission intervient apr&egrave;s l&rsquo;un des comportements suivants de la part de l&rsquo;employeur </strong>(article 40 du code du travail) :</p>
                    <ul>
                    <li>L&rsquo;insulte grave</li>
                    <li>La pratique de toute forme de violence ou d&rsquo;agression dirig&eacute;e contre le salari&eacute;</li>
                    <li>Le harc&egrave;lement sexuel</li>
                    <li>L&rsquo;incitation &agrave; la d&eacute;bauche</li>
                    </ul>
                    <p>La dur&eacute;e du <strong>pr&eacute;avis de d&eacute;mission </strong>d&eacute;pend de l&rsquo;anciennet&eacute; du salari&eacute; dans l&rsquo;entreprise :</p>
