                    <h5>3. COMMENT G&Eacute;RER UN LICENCIEMENT ?</h5>
                    <p>Un employeur ne peut d&eacute;cider de r&eacute;silier de mani&egrave;re unilat&eacute;rale un contrat de travail <strong>sans motivation r&eacute;elle et s&eacute;rieuse</strong>. De ce fait, il faut distinguer 3 types de licenciement :</p>
                    <h3>I. Le licenciement pour faute grave</h3>
                    <p>La liste des fautes consid&eacute;r&eacute;es comme graves est expos&eacute;e dans l&rsquo;article 39 du code du travail, par exemple:</p>
                    <ul>
                    <li>Un certain nombre de d&eacute;lits : vol, abus de confiance, consommation de stup&eacute;fiants, agression corporelle&hellip;</li>
                    <li>La divulgation d&rsquo;un secret professionnel ayant caus&eacute; un pr&eacute;judice &agrave; l&rsquo;entreprise</li>
                    <li>Le refus d&eacute;lib&eacute;r&eacute; et injustifi&eacute; du salari&eacute; d&rsquo;ex&eacute;cuter un travail de sa comp&eacute;tence</li>
                    <li>La d&eacute;t&eacute;rioration grave des &eacute;quipements caus&eacute;e d&eacute;lib&eacute;r&eacute;ment par le salari&eacute; ou &agrave; la suite d&rsquo;une n&eacute;gligence de sa part</li>
                    <li>&hellip;etc.</li>
                    </ul>
                    <p>En cas de faute grave, <strong>le salari&eacute; peut &ecirc;tre licenci&eacute; imm&eacute;diatement sans pr&eacute;avis ni indemnit&eacute;.</strong></p>
                    <p>&nbsp;</p>
                    <h3>II.Le licenciement pour motif personnel</h3>
                    <p>Quand l&rsquo;employeur juge un salari&eacute; <strong>incapable d&rsquo;accomplir ses fonctions </strong>il peut licencier ce dernier pour motif personnel. Il devra alors l&rsquo;indemniser en cons&eacute;quence.</p>
                    <p>Proc&eacute;dure</p>
                    <ul>
                    <li>Un acte de licenciement doit &ecirc;tre <strong>r&eacute;dig&eacute; et transmis au salari&eacute; </strong>comprenant les motifs et les circonstances du licenciement ainsi que sa date effective. Une copie doit &eacute;galement &ecirc;tre remise &agrave; l&rsquo;agent charg&eacute; de l&rsquo;inspection du travail ;</li>
                    <li><strong>Dans les 8 jours suivant la constatation de l&rsquo;acte de licenciement</strong>, le salari&eacute; doit pouvoir se d&eacute;fendre aupr&egrave;s de son employeur en pr&eacute;sence du d&eacute;l&eacute;gu&eacute; des salari&eacute;s ou du repr&eacute;sentant syndical</li>
                    <li>Pr&eacute;avis : la r&eacute;glementation concernant la dur&eacute;e de pr&eacute;avis lors de licenciements est identique &agrave; celle pr&eacute;vue pour les d&eacute;missions (cf. ci-dessus)</li>
                    </ul>
                    <p>Indemnit&eacute;s</p>
                    <p>Les indemnit&eacute;s de licenciement sont calcul&eacute;es en <strong>fonction de l&rsquo;anciennet&eacute; du salari&eacute; dans l&rsquo;entreprise. </strong>Elles sont &eacute;gales &agrave; :</p>
                    <ul>
                    <li>96 heures de salaire pour les cinq premi&egrave;res ann&eacute;es d&rsquo;anciennet&eacute;</li>
                    <li>144 heures de salaire pour la p&eacute;riode d&rsquo;anciennet&eacute; allant de 6 &agrave; 10 ans</li>
                    <li>192 heures de salaire pour la p&eacute;riode d&rsquo;anciennet&eacute; allant de 11 &agrave; 15 ans</li>
                    <li>240 heures de salaire pour la p&eacute;riode d&rsquo;anciennet&eacute; d&eacute;passant 15 ans</li>
                    </ul>
                    <p>Des dispositions plus favorables au salari&eacute; peuvent &ecirc;tre pr&eacute;vues dans le contrat de travail, la convention collective de travail ou le r&egrave;glement int&eacute;rieur.</p>
                    <p>&nbsp;</p>
                    <h3>III.Le licenciement pour motifs technologiques, structurels ou &eacute;conomiques</h3>
                    <p>La proc&eacute;dure dans ce type de licenciement est d&eacute;taill&eacute;e dans les articles 66 et 67 du Code du Travail. Les salari&eacute;s disposent des m&ecirc;mes conditions de pr&eacute;avis et d&rsquo;indemnit&eacute; que pour le licenciement pour motif personnel.</p>
                    <p>&nbsp;</p>
                    <h5>4. COMBIEN CO&Ucirc;TE UN LICENCIEMENT EN TERMES D&rsquo;INDEMNIT&Eacute;S ?</h5>
                    <p><strong>En cas de CDD</strong>, et en vertu de l&rsquo;article 33 du Code du Travail, la partie qui met fin au contrat doit payer &agrave; l&rsquo;autre la totalit&eacute; du salaire relative &agrave; la p&eacute;riode non travaill&eacute;e.</p>
                    <p><strong>En cas de CDI, </strong>le salaire brut est le montant servant de base pour le calcul de tout droit reconnu. En plus de la prime d&rsquo;anciennet&eacute;, tous les avantages en esp&egrave;ces entrent en ligne de compte pour le calcul du salaire.</p>
                    <p>Dans certains cas, le salari&eacute; a droit &agrave; des indemnit&eacute;s de licenciement. En effet, &laquo; <strong>le salari&eacute; li&eacute; par un contrat de travail &agrave; dur&eacute;e ind&eacute;termin&eacute;e a droit &agrave; une indemnit&eacute;, en cas de licenciement apr&egrave;s six mois de travail dans la m&ecirc;me entreprise quels que soient le mode de r&eacute;mun&eacute;ration et la p&eacute;riodicit&eacute; du paiement du salaire. </strong>&raquo; (Art. 52, Code du Travail).</p>
                    <p>Les indemnit&eacute;s de licenciement sont calcul&eacute;es sur la base du salaire brut per&ccedil;u pendant les 52 derni&egrave;res semaines et de la dur&eacute;e de travail pass&eacute;e au service de l&rsquo;employeur.</p>
                    <p>L&rsquo;anciennet&eacute; est &eacute;galement prise en compte pour le calcul de l&rsquo;indemnit&eacute;, et elle court depuis l&rsquo;entr&eacute;e en fonction dans l&rsquo;entreprise jusqu&rsquo;&agrave; l&rsquo;expiration du pr&eacute;avis, m&ecirc;me si celui-ci n&rsquo;a pas &eacute;t&eacute; travaill&eacute;.</p>
                    <p>En vertu de l&rsquo;article 54 du code, sont consid&eacute;r&eacute;es comme p&eacute;riodes de travail effectif :</p>
                    <ul>
                    <li>Les p&eacute;riodes de cong&eacute;s pay&eacute;s ;</li>
                    <li>Les p&eacute;riodes de repos des femmes en couches ;</li>
                    <li>La dur&eacute;e de l&rsquo;incapacit&eacute; temporaire de travail lorsque le salari&eacute; a &eacute;t&eacute; victime d&rsquo;un accident du travail ou a &eacute;t&eacute; atteint d&rsquo;une maladie professionnelle ;</li>
                    <li>Les p&eacute;riodes de travail pendant lesquelles l&rsquo;ex&eacute;cution du contrat de travail a &eacute;t&eacute; suspendue, sans que le contrat ait &eacute;t&eacute; r&eacute;sili&eacute;, notamment pour cause d&rsquo;absence autoris&eacute;e, de maladie autre que professionnelle, de fermeture temporaire de l&rsquo;&eacute;tablissement par d&eacute;cision administrative ou par cas de force</li>
                    </ul>
                
