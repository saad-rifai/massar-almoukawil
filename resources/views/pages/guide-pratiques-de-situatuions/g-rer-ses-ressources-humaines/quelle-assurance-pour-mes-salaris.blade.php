                    <h5>1. QU&rsquo;EST CE QUE L&rsquo;ASSURANCE MALADIE OBLIGATOIRE (AMO) ?</h5>
                    <p>La loi marocaine a rendu certaines assurances obligatoires. Vous &ecirc;tes tenu de les souscrire sous peine de sanctions financi&egrave;res, voire p&eacute;nales. Parmi les assurances obligatoire, figure bien entendu l&rsquo;Assurance Maladie Obligatoire (<strong>AMO</strong>), <strong>qui est g&eacute;r&eacute;e par la CNSS.</strong></p>
                    <p>La cotisation &agrave; l&rsquo;AMO est pr&eacute;lev&eacute;e directement sur le salaire brut. Le taux de cotisation <strong>est fix&eacute; &agrave; 1,85% + 4,52 % de la masse salariale d&eacute;plafonn&eacute;e, </strong>r&eacute;parti comme suit :</p>
                    <ul>
                    <li>Pour toutes les entreprises marocaines au titre de la solidarit&eacute; AMO : 1,85% &agrave; la charge de l&rsquo;employeur ;</li>
                    </ul>
                    <ul>
                    <li>Pour les affili&eacute;s vers&eacute;s dans l&rsquo;assurance maladie obligatoire g&eacute;r&eacute;e par la CNSS : 2,26% de charges patronales et 2,26% de charge salariale.</li>
                    </ul>
                    <p>L&rsquo;AMO couvre un large panier de soins et de m&eacute;dicaments dont le d&eacute;tail est accessible en ligne sur le site de la CNSS : <a href="http://www.cnss.ma/fr/content/que-couvre-lamo" target="_blank" rel="noopener">Que couvre l&rsquo;AMO ? | La Caisse Nationale de S&eacute;curit&eacute; Sociale.</a></p>
                    <p>Le taux de remboursement est fix&eacute; &agrave; 70% de la Tarification Nationale de R&eacute;f&eacute;rence. Ce taux peut atteindre 90% pour certaines exceptions : traitement des maladies graves et invalidantes, affections de longues dur&eacute;es (ALD).</p>
                    <p>Outre le salari&eacute; concern&eacute;, l&rsquo;AMO couvre les membres de la famille &agrave; charge, &agrave; savoir :</p>
                    <ul>
                    <li>Le conjoint de l&rsquo;assur&eacute;</li>
                    <li>Les enfants de 21 ans ou moins (26 ans si les enfants sont non mari&eacute;s et poursuivent des &eacute;tudes sup&eacute;rieures)</li>
                    </ul>
                    <p>&nbsp;</p>
                    <h5>2. POURQUOI CONTRACTER UNE ASSURANCE &ldquo;ACCIDENT DU TRAVAIL&rdquo; ET QUELLES SONT LES ASSURANCES COMPL&Eacute;MENTAIRES ?</h5>
                    <p>L&rsquo;assurance &ldquo;Accident du Travail&rdquo; est g&eacute;r&eacute;e par les compagnies priv&eacute;es. Elle vise <strong>la r&eacute;paration des accidents et maladies subies par les assur&eacute;s dans l&rsquo;exercice de leur fonction </strong>et est souscrite par l&rsquo;employeur au profit de ses employ&eacute;s pour les couvrir contre les risques qu&rsquo;ils encourent dans l&rsquo;exercice de leur activit&eacute; professionnelle.</p>
                    <p>En plus de celles obligatoires, il est recommand&eacute; de souscrire des assurances compl&eacute;mentaires :</p>
                    <ul>
                    <li>Une assurance maladie compl&eacute;mentaire &agrave; l&rsquo;AMO</li>
                    <li>Des assurances qui pr&eacute;munissent vos salari&eacute;s des risques li&eacute;s &agrave; leur activit&eacute;</li>
                    </ul>
                    <p>Selon l&rsquo;assurance, les co&ucirc;ts et le d&eacute;tail des prestations varient mais les <strong>principales assurances de personne auxquelles vous pouvez souscrire </strong>sont les suivantes :</p>
                    <ul>
                    <li><strong>Assurance sant&eacute; compl&eacute;mentaire : </strong>Cette assurance agit en compl&eacute;ment de l&rsquo;AMO dans la couverture des frais m&eacute;dicaux et chirurgicaux. Son co&ucirc;t est &agrave; n&eacute;gocier et se situe en g&eacute;n&eacute;ral entre 0,75% et 1,5% de la masse salariale (hors assurance incapacit&eacute;/invalidit&eacute;)</li>
                    <li><strong>Assurance incapacit&eacute; invalidit&eacute; : </strong>Cette assurance garantit le versement d&rsquo;une indemnit&eacute; et d&rsquo;une rente d&rsquo;invalidit&eacute; en cas d&rsquo;interruption de travail due &agrave; une maladie ou un accident &agrave; caract&egrave;re non professionnel</li>
                    <li><strong>Assurance individuelle accident : </strong>Cette assurance couvre les dommages corporels subis dans le cas d&rsquo;accidents du travail</li>
                    <li><strong>Assurances de d&eacute;placement : </strong>Cette famille regroupe un ensemble d&rsquo;assurances pouvant entrer en jeu si vos salari&eacute;s sont amen&eacute;s &agrave; se d&eacute;placer dans le cadre de leur fonction : assurance de d&eacute;placement, assurance de rapatriement, couvertures maladies &agrave; l&rsquo;&eacute;tranger</li>
                    </ul>
                    <p>&nbsp;</p>
                    <h5>3. QU&rsquo;EST CE QUE LA RESPONSABILIT&Eacute; CIVILE OU RC ?</h5>
                    <p>La RC n&rsquo;est pas consid&eacute;r&eacute;e comme une assurance compl&eacute;mentaire au m&ecirc;me titre que celles pr&eacute;sent&eacute;es dans le tableau plus haut : alors que les assurances compl&eacute;mentaires visent &agrave; prot&eacute;ger les employ&eacute;s et les biens de l&rsquo;entreprise contre les d&eacute;g&acirc;ts et accidents externes, la <strong>RC vise &agrave; prot&eacute;ger l&rsquo;entreprise des d&eacute;g&acirc;ts et accidents qu&rsquo;elle peut elle-m&ecirc;me causer</strong>.</p>
                    <p><strong>Il est recommand&eacute; de contracter une RC</strong>, m&ecirc;me si elle n&rsquo;est pas tout le temps obligatoire car :</p>
                    <ul>
                    <li>Elle couvre les dommages corporels, mat&eacute;riels et immat&eacute;riels que votre entreprise pourrait causer aux tiers par le fait de :</li>
                    <li>votre personnel ;</li>
                    <li>votre mat&eacute;riel ;</li>
                    <li>vos marchandises encore non-livr&eacute;es ;</li>
                    <li>des immeubles o&ugrave; s&rsquo;exerce votre activit&eacute;</li>
                    </ul>
                    <ul>
                    <li>Pour une prime plus &eacute;lev&eacute;e, elle peut aussi couvrir au choix :</li>
                    <li>les biens lou&eacute;s, confi&eacute;s ou en d&eacute;p&ocirc;t ;</li>
                    <li>les travaux &agrave; l&rsquo;ext&eacute;rieur ;</li>
                    <li>les engins automoteurs ;</li>
                    <li>les services internes de votre entreprise (restaurant d&rsquo;entreprise, service m&eacute;dical) ;</li>
                    <li>les frais d&rsquo;assistance juridique &laquo; d&eacute;fense et recours &raquo;.</li>
                    </ul>
                    <p>Cependant, la Responsabilit&eacute; Civile doit progressivement &ecirc;tre rendue obligatoire dans de plus en plus de secteurs professionnels. Pour savoir si vous &ecirc;tes concern&eacute; par cette obligation, renseignez-vous aupr&egrave;s de votre assureur.</p>
                    <p>&nbsp;</p>
                    <p><strong>Bon &agrave; savoir : </strong><strong>Il est tr&egrave;s fortement conseill&eacute; de lancer plusieurs demandes de devis aupr&egrave;s de diverses assurances, l&rsquo;int&eacute;r&ecirc;t &eacute;tant de faire jouer la concurrence et ainsi b&eacute;n&eacute;ficier de la meilleure offre possible. La rupture d&rsquo;un contrat d&rsquo;assurance doit normalement s&rsquo;effectuer avec un pr&eacute;avis de 3 mois sinon le contrat est tacitement reconduit d&rsquo;un an. Vous pouvez faire appel &agrave; un courtier d&rsquo;assurance pour g&eacute;rer les contrats de votre entreprise</strong></p>
                
