                <div class="text-containet">

                    <p>Dans toute entreprise, <strong>la communication occupe une fonction strat&eacute;gique</strong>. Sa mission englobe trois objectifs cl&eacute;s :</p>
                    <ul>
                    <li>Positionner l&rsquo;entreprise</li>
                    <li>Contribuer &agrave; <strong>mieux faire conna&icirc;tre ses produits/services </strong>par tous ses publics</li>
                    <li>V&eacute;hiculer une <strong>image de marque </strong>originale et distincte de la concurrence.</li>
                    </ul>
                    <p>Les moyens de communication sont nombreux et parfois chers. Il s&rsquo;agit donc de se poser les bonnes questions avant de lancer sa communication. Voici donc une liste (non-exhaustive) de questions &agrave; se poser pour pr&eacute;parer son plan communication :</p>
                    <ol>
                    <li>Quelle est la situation du march&eacute; ?</li>
                    </ol>
                    <h3>I convient avant tout d&rsquo;analyser le march&eacute; dans lequel vous intervenez :</h3>
                    <ul>
                    <li>Quelle est la <strong>dynamique </strong>de march&eacute; ?</li>
                    <li>Quelles sont les <strong>attentes </strong>des clients ?</li>
                    <li><strong>O&ugrave; ach&egrave;tent-ils </strong>leurs produits et comment les ach&egrave;tent-ils ?</li>
                    <li>Quels sont les <strong>m&eacute;dias</strong>, <strong>influencers</strong>, et <strong>tendances </strong>qu&rsquo;ils suivent ?</li>
                    </ul>
                    <h3>II. Quelle est mon audience cible ?</h3>
                    <p>L&rsquo;une des premi&egrave;res &eacute;tapes indispensables est de d&eacute;terminer pr&eacute;cis&eacute;ment le(s) public(s) cible(s) qui sont l&rsquo;objet de votre communication : <strong>qui souhaitez-vous joindre </strong>et quel est le profil d&eacute;mographique et sociologique pour chacun de vos publics ?</p>
                    <p>Plus vos cibles seront pr&eacute;cises et en lien avec vos objectifs, plus vos actions seront percutantes et potentiellement concluantes. Pour chacun des publics cibles, il faudra <strong>d&eacute;terminer l&rsquo;approche, le style et le contenu </strong>de vos communications.</p>
                    <p>Communiquez plus largement aupr&egrave;s de chaque acteur cl&eacute; de votre &eacute;cosyst&egrave;me ! Selon les entreprises, il sera important de <strong>communiquer r&eacute;guli&egrave;rement </strong>aupr&egrave;s de vos fournisseurs, des d&eacute;cideurs publics, agents de distribution, etc. Vous devez alors montrer que vous &ecirc;tes un acteur s&eacute;rieux et implant&eacute; durablement dans le paysage.</p>
                    <ul>
                    <li>Quels sont mes objectifs de communication ?</li>
                    </ul>
                    <p>En mati&egrave;re de communication, les objectifs sont g&eacute;n&eacute;ralement regroup&eacute;s en trois grandes cat&eacute;gories :</p>
