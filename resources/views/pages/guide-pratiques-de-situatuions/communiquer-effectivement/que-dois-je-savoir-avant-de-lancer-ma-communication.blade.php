                    <h3>IV. Quel est le message que je veux transmettre ?</h3>
                    <p>D&eacute;terminez pr&eacute;cis&eacute;ment ce que vous attendez comme &laquo; action &raquo; de votre cible :</p>
                    <ul>
                    <li>Qu&rsquo;elle vous appelle ?</li>
                    <li>Qu&rsquo;elle t&eacute;l&eacute;charge votre catalogue ?</li>
                    <li>Qu&rsquo;elle se d&eacute;place en point de vente ?</li>
                    </ul>
                    <p>D&eacute;terminez ensuite quel style de discours s&rsquo;adapte &agrave; votre public car des cibles diff&eacute;rentes peuvent n&eacute;cessiter des messages diff&eacute;rents.</p>
                    <h3>V. Par quel(s) moyen(s) vais-je communiquer le bon message, &agrave; la bonne personne, au bon moment ?</h3>
                    <p>Il est important de bien s&eacute;lectionner les outils de communication les mieux adapt&eacute;s pour atteindre vos objectifs. Que ce soit en organisant un <strong>&eacute;v&eacute;nement</strong>, des <strong>relations de presse</strong>, une campagne sur les <strong>r&eacute;seaux sociaux</strong>, ou par le biais de la <strong>publicit&eacute; </strong>; tous les moyens sont envisageables.</p>
                    <p>Le public cible &agrave; joindre et le message &agrave; lui transmettre orienteront le choix des m&eacute;dias, des techniques et des supports les plus appropri&eacute;s</p>
                    <h3>VI. Quels sont le budget et les comp&eacute;tences dont j&rsquo;ai besoin ?</h3>
                    <p>Il est important d&rsquo;&eacute;valuer quels sont <strong>vos besoins </strong>en ressources humaines, financi&egrave;res et mat&eacute;rielles pour chacun des moyens de communication qui seront d&eacute;ploy&eacute;s. Il importe de <strong>d&eacute;terminer le budget </strong>d&egrave;s le d&eacute;but car il est ainsi plus facile de travailler avec les diff&eacute;rents fournisseurs.</p>
                    <h3>VII. Comment puis-je tester mon plan de communication ?</h3>
                    <p><strong>Tester votre plan de communication </strong>avant de le lancer afin d&rsquo;&eacute;viter des erreurs co&ucirc;teuses. Pr&eacute;sentez par exemple votre communication &agrave; vos clients existants et recueillez leurs impressions. Ou ne prenez qu&rsquo;une petite partie de vos prospects pour tester votre communication marketing. <strong>&Eacute;valuez les retours </strong>et conservez les tactiques qui ont g&eacute;n&eacute;r&eacute; le plus de r&eacute;sultats.</p>
                    <h3>VIII. Comment &eacute;valuer les r&eacute;sultats de ma communication ?</h3>
                    <p>Pr&eacute;voyez comment <strong>mesurer l&rsquo;efficacit&eacute; </strong>de votre communication avec des crit&egrave;res qui &eacute;valueront la notori&eacute;t&eacute;, la croissance des ventes, etc. Ainsi, vous serez en mesure plus tard d&rsquo;ajuster votre communication aupr&egrave;s du m&ecirc;me type de public.</p>
