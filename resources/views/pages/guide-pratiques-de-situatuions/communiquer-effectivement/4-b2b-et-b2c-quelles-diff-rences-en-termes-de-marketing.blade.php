</td>
<td>
<p><strong>B2C</strong></p>
</td>
</tr>
<tr>
<td>
<p><em>Ton du discours</em></p>
</td>
<td>
<p>Les marketeurs utilisent en g&eacute;n&eacute;ral le jargon du secteur, le discours est professionnel</p>
</td>
<td>
<p>Le ton employ&eacute; doit &ecirc;tre simple et efficace pour &ecirc;tre compris par la majorit&eacute; des clients finaux</p>
</td>
</tr>
<tr>
<td>
<p><em>Motifs d&rsquo;achat</em></p>
</td>
<td>
<p>En qu&ecirc;te d&rsquo;efficacit&eacute; et d&rsquo;expertise, les d&eacute;cisions d&rsquo;achat en B2B ob&eacute;issent souvent &agrave; un processus rationnel car elles doivent &ecirc;tre justifi&eacute;es &agrave; la hi&eacute;rarchie par</p>
<p>une argumentation logique et fiable</p>
</td>
<td>
<p>Plus pouss&eacute;s par le d&eacute;sir, par la qu&ecirc;te de satisfaction, les clients B2C ont un processus d&rsquo;achat souvent moins rationnel qu&rsquo;en B2B, plus &eacute;motionnel</p>
</td>
</tr>
