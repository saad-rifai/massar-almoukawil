                    <h3>I. R&eacute;daction du brief</h3>
                    <p>Vous devez commencer par r&eacute;diger un brief, un <strong>document &eacute;crit par
                            l&rsquo;annonceur </strong>(le directeur marketing, le directeur de la communication ou le
                        chef de produit concern&eacute;) qui comprend les informations suivantes :</p>
                    <ul>
                        <li>Description de l&rsquo;entreprise, de ses marques, de ses produits et de son</li>
                        <li>Diagnostic marketing de l&rsquo;entreprise et de chacune de ses marques avec historique de
                            sa strat&eacute;gie de communication.</li>
                        <li>Description des objectifs marketing et de communication, les cibles et le budget de
                            communication, avec les contraintes juridiques sp&eacute;cifiques aux march&eacute;s
                            concern&eacute;s.</li>
                    </ul>
                    <h5>II. Choix de l&rsquo;agence</h5>
                    <p>L&rsquo;habitude des march&eacute;s publicitaires est d&rsquo;organiser une comp&eacute;tition
                        entre agences de publicit&eacute; : vous devez &eacute;tablir <strong>une short-list</strong>.
                    </p>
                    <p>Les crit&egrave;res qui doivent pr&eacute;valoir votre choix sont : la cr&eacute;ativit&eacute;
                        per&ccedil;ue, la taille, le caract&egrave;re international, les comp&eacute;tences
                        sp&eacute;cifiques, les services propos&eacute;s et le prix.</p>
                    <p>Les &eacute;quipes constitu&eacute;es par les agences candidates disposent alors d&rsquo;un temps
                        r&eacute;duit pour d&eacute;finir une proposition, &agrave; partir de laquelle vous pourrez
                        effectuer votre s&eacute;lection. Vous pouvez vous faire aider dans ce processus par un conseil
                        en choix d&rsquo;agence.</p>
                    <p>Lorsque vous aurez s&eacute;lectionn&eacute; l&rsquo;agence avec laquelle vous souhaitez
                        collaborer, vous signerez le contrat o&ugrave; la r&eacute;mun&eacute;ration et les
                        pr&eacute;rogatives de l&rsquo;agence seront fix&eacute;es.</p>
                    <h3>III. Cr&eacute;ation du message publicitaire en trois &eacute;tapes cl&eacute;s</h3>
                    <ol>
                        <li><strong>La strat&eacute;gie de cr&eacute;ation </strong>: document d&rsquo;environ 2 pages
                            &eacute;tabli par vous et le responsable commercial de l&rsquo;agence qui doit servir
                            &agrave; orienter le travail des &eacute;quipes de cr&eacute;ation. Il doit &ecirc;tre
                            simple, en coh&eacute;rence avec votre strat&eacute;gie marketing et ne doit pas se
                            substituer au travail des cr&eacute;atifs.</li>
                    </ol>
                    <ol start="2">
                        <li><strong>Le projet cr&eacute;atif </strong>: cette phase est g&eacute;r&eacute;e par
                            l&rsquo;agence. Elle consiste &agrave; mat&eacute;rialiser le concept publicitaire sous la
                            forme de maquettes (pour les dessins) ou de story-board (pour les vid&eacute;os).
                            L&rsquo;id&eacute;al est de pouvoir tester ces esquisses sur la cible de communication.
                            Attention : ces tests peuvent &ecirc;tre co&ucirc;teux.</li>
                        <li><strong>La production du message </strong>: le prototype que vous aurez choisi est ensuite
                            r&eacute;alis&eacute; par des sp&eacute;cialistes (graphistes, photographes,
                            cin&eacute;astes) au sein de l&rsquo;agence s&eacute;lectionn&eacute;e ou en free-lance.
                        </li>
                    </ol>
                    <h3>IV. R&eacute;alisation du plan m&eacute;dia</h3>
                    <p>Le plan m&eacute;dia est un document contenant la liste des m&eacute;dias retenus, la liste des
                        supports s&eacute;lectionn&eacute;s pour chaque m&eacute;dia, ainsi que le calendrier des
                        insertions et le montant des investissements &agrave; r&eacute;aliser.</p>
                    <p>Le plan m&eacute;dia est r&eacute;alis&eacute; par l&rsquo;&eacute;quipe m&eacute;dias de
                        l&rsquo;agence ou alors par une centrale d&rsquo;achat d&rsquo;espaces ind&eacute;pendante.</p>
