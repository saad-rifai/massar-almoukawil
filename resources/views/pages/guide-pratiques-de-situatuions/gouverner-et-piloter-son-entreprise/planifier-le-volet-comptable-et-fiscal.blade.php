                    <h5>1. QUEL EST LE CADRE COMPTABLE ET FISCAL &Agrave; RESPECTER ?</h5>
                    <p>La comptabilit&eacute; de votre entreprise est une fonction cl&eacute;, &agrave; laquelle il va
                        falloir accorder une importance primordiale et capitale.</p>
                    <p>La plupart des informations comptables r&eacute;glementaires au Maroc sont accessibles au grand
                        public. A commencer par le texte juridique de r&eacute;f&eacute;rence en mati&egrave;re de
                        comptabilit&eacute; qui est le <strong>Code G&eacute;n&eacute;ral de la Normalisation Comptable
                            (CGNC)</strong>. Ce texte d&eacute;taille l&rsquo;ensemble des postes de comptabilit&eacute;
                        et des normes &agrave; adopter dans la tenue de vos comptes.</p>
                    <p>Le Minist&egrave;re de l&rsquo;Economie, des Finances et de la R&eacute;forme de
                        l&rsquo;Administration, &agrave; travers la Direction des Entreprises Publiques et de la
                        Privatisation (DEPP), qui assure le secr&eacute;tariat g&eacute;n&eacute;ral du Conseil National
                        de la Comptabilit&eacute; (CNC), a lanc&eacute; le projet de modernisation du cadre Comptable
                        national pour le rapprocher des standards internationaux et moderniser le cadre comptable et
                        financier national.</p>
                    <p>De plus, le <strong>Code g&eacute;n&eacute;ral des imp&ocirc;ts (CGI) </strong>est
                        &eacute;galement le texte de r&eacute;f&eacute;rence <a
                            href="https://portail.tax.gov.ma/wps/portal/DGI/Documentation-fiscale/Code-general-des-impots"
                            target="_blank" rel="noopener">disponible sur internet </a><strong>et la Loi 9.88
                        </strong>relative aux obligations comptables des commer&ccedil;ants.</p>
                    <p><strong>Bon &agrave; savoir </strong>: <strong>selon la forme juridique de la
                            soci&eacute;t&eacute; (S.A., S.A.R.L,&hellip;) des lois sp&eacute;cifiques peuvent
                            s&rsquo;appliquer.</strong></p>
                    <p>&nbsp;</p>
                    <p>Toutes les entreprises marocaines ont pour obligation de d&eacute;poser chaque ann&eacute;e leur
                        bilan comptable pour l&rsquo;exercice &eacute;coul&eacute;. Tout contribuable doit se plier aux
                        obligations comptables cit&eacute;es dans les deux documents officiels susmentionn&eacute;s, et
                        notamment :</p>
                    <ul>
                        <li>&ldquo; Les contribuables doivent tenir une comptabilit&eacute; conform&eacute;ment &agrave;
                            la l&eacute;gislation et la r&eacute;glementation en vigueur, de mani&egrave;re &agrave;
                            <strong>permettre &agrave; l&rsquo;administration d&rsquo;exercer les contr&ocirc;les
                                pr&eacute;vus par le pr&eacute;sent code&rdquo; </strong>(extrait de l&rsquo;article 145
                            de la CGI) ;</li>
                        <li>&ldquo;&hellip;et r&eacute;aliser les d&eacute;clarations en toute <strong>transparence
                            </strong>et en &eacute;vitant toute irr&eacute;gularit&eacute; ;&rdquo;</li>
                        <li>&ldquo; Lorsque les &eacute;critures d&rsquo;un exercice comptable ou d&rsquo;une
                            p&eacute;riode d&rsquo;imposition pr&eacute;sentent des irr&eacute;gularit&eacute;s graves
                            de nature &agrave; mettre en cause la valeur probante de la comptabilit&eacute;,
                            <strong>l&rsquo;administration peut d&eacute;terminer la base d&rsquo;imposition
                            </strong>d&rsquo;apr&egrave;s les &eacute;l&eacute;ments dont elle dispose &raquo; (extrait
                            de l&rsquo;article 213 de la CGI) ;</li>
                        <li>&ldquo;A cet effet, <strong>elle peut &eacute;valuer son revenu global annuel </strong>pour
                            tout ou partie de la p&eacute;riode non prescrite lorsque, pour la p&eacute;riode
                            consid&eacute;r&eacute;e, ledit revenu n&rsquo;est pas en rapport avec ses d&eacute;penses,
                            telles que d&eacute;finies &agrave; l&rsquo;article 29 &raquo; (extrait de l&rsquo;article
                            216 de la CGI). Tout manquement est <strong>passible de graves sanctions&rdquo; </strong>;
                        </li>
                        <li>&ldquo; Ind&eacute;pendamment des sanctions fiscales &eacute;dict&eacute;es par le
                            pr&eacute;sent code, est punie d&rsquo;une amende de cinq mille (5.000) dirhams &agrave;
                            cinquante mille (50.000) dirhams, <strong>toute personne qui, en vue de se soustraire
                                &agrave; sa qualit&eacute; de contribuable ou au paiement de l&rsquo;imp&ocirc;t ou en
                                vue d&rsquo;obtenir des d&eacute;ductions ou remboursements&rdquo; </strong>(extrait de
                            l&rsquo;article 192 de la CGI).</li>
                    </ul>
                    <h5>2. COMMENT FAIRE POUR BIEN RESPECTER SES &Eacute;CH&Eacute;ANCES FISCALES ?</h5>
                    <p>L&rsquo;ensemble des informations relatives &agrave; la fiscalit&eacute; sont accessibles sur le
                        site de la <strong>Direction G&eacute;n&eacute;rale des Imp&ocirc;ts</strong>.</p>
                    <p>Vous y trouverez notamment :</p>
                    <ul>
                        <li>Un <strong>calendrier fiscal </strong>d&eacute;taillant mois par mois les d&eacute;marches
                            et paiements &agrave; r&eacute;aliser</li>
                        <li>La derni&egrave;re version du <strong>Code G&eacute;n&eacute;ral des Imp&ocirc;ts
                            </strong>accessible en ligne</li>
                        <li>Les <strong>textes de lois </strong>relatifs aux autres imp&ocirc;ts et taxes
                            g&eacute;r&eacute;s par la DGI</li>
                        <li>Les textes de lois relatifs aux <strong>imp&ocirc;ts locaux </strong>g&eacute;r&eacute;s par
                            la DGI : taxe professionnelle, taxe d&rsquo;habitation et taxe de services communaux</li>
                    </ul>
                    <p>Voici un panorama des principales d&eacute;marches &agrave; r&eacute;aliser pour les principaux
                        imp&ocirc;ts et taxes :</p>
                    <ul>
                        <li><strong>le paiement de la TVA </strong>: la TVA se r&egrave;gle de mani&egrave;re mensuelle
                            (si le chiffre d&rsquo;affaires de l&rsquo;entreprise est sup&eacute;rieur &agrave; 1
                            million de DH) ou trimestrielle (si inf&eacute;rieur &agrave; 1 million de DH).
                            L&rsquo;&eacute;tablissement de la TVA se fait en faisant un solde de la TVA factur&eacute;e
                            et de la TVA pay&eacute;e ;</li>
                        <li><strong>le paiement de l&rsquo;IR </strong>: se paie sur les salaires des collaborateurs de
                            votre entreprise au plus tard &agrave; la fin du mois suivant le versement du salaire ;</li>
                        <li><strong>le paiement de l&rsquo;IS </strong>: l&rsquo;imp&ocirc;t sur les
                            soci&eacute;t&eacute;s se paie &agrave; la fin des mois de mars, juin, septembre et
                            d&eacute;cembre, sur la base de l&rsquo;IS de l&rsquo;ann&eacute;e pr&eacute;c&eacute;dente.
                        </li>
                    </ul>
                    <h5>3. COMMENT CALCULER L&rsquo;IMP&Ocirc;T SUR LES SOCI&Eacute;T&Eacute;S (IS) ET
                        L&rsquo;IMP&Ocirc;T SUR LE REVENU (IR) DE VOTRE ENTREPRISE ?</h5>
                    <p>En premier lieu, si vous souhaitez b&eacute;n&eacute;ficier d&rsquo;un accompagnement
                        pr&eacute;cis et adapt&eacute; pour g&eacute;rer et optimiser votre fiscalit&eacute;, il est
                        fortement conseill&eacute; de vous <strong>rapprocher d&rsquo;organismes
                            sp&eacute;cialis&eacute;s </strong>: comptable ou expert-comptable, cabinets de conseil en
                        fiscalit&eacute; ou avocat sp&eacute;cialis&eacute;.</p>
                    <p>Imp&ocirc;t sur les soci&eacute;t&eacute;s (IS)</p>
                    <p>Le calcul d&rsquo;IS est d&eacute;termin&eacute; &agrave; partir du r&eacute;sultat fiscal net,
                        &eacute;gal &agrave; l&rsquo;exc&eacute;dent des produits sur les charges, apr&egrave;s
                        d&eacute;duction des reports d&eacute;ficitaires. Il concerne toute personne morale (entreprise)
                        : SARL, SA, etc. Le taux applicable pour d&eacute;terminer le l&rsquo;IS est <strong>progressif
                        </strong>selon le montant imposable (10%, 20%, 31%).</p>
                    <p>Imp&ocirc;t sur le revenu (IR)</p>
                    <p>L&rsquo;IR est vers&eacute; chaque mois pour le compte des employ&eacute;s de l&rsquo;entreprise
                        sur une base imposable qui comprend les revenus sur salaires. L&rsquo;imp&ocirc;t est
                        progressif, de 10% &agrave; partir de 30 000 dhs de revenus annuels &agrave; 38% pour les plus
                        hauts revenus au-del&agrave; de 180.000 dirhams annuels.</p>
                    <p>Illustration</p>
                    <p>Une SARL compos&eacute;e d&rsquo;un seul salari&eacute; (le dirigeant) qui r&eacute;alise un
                        r&eacute;sultat net fiscal annuel de 300.000 dirhams. Son taux d&rsquo;IS applicable est de 10%,
                        et le montant de l&rsquo;IS &agrave; payer sera de 300.000 x 10% = 30 000 dirhams.</p>
                    <p>A supposer que la soci&eacute;t&eacute; verse un salaire de 8.000 DH bruts &agrave; son
                        dirigeant, le salaire brut annuel est de</p>
                    <p>8.000 DH x 12 = 96.000 DH, le salaire net imposable sera d&rsquo;environ 72.000 dirhams. Le taux
                        d&rsquo;IR applicable sera donc de 30% (tranche de 60.001 &agrave; 80.000 dirhams) et la somme
                        &agrave; d&eacute;duire pour cette tranche est de 14 000 dirhams (voir bar&egrave;me de
                        l&rsquo;imp&ocirc;t sur le revenu ci-dessous). Au final, le montant de l&rsquo;IR &agrave; payer
                        sera de l&rsquo;ordre de 72000 x 30% &ndash; 14000 DH = 7600 DH.</p>
