                    <h5>4. COMMENT CALCULER VOTRE TVA ?</h5>
                    <p>De mani&egrave;re g&eacute;n&eacute;rale, pour la TVA, le taux commun est de 20%. Le calcul se
                        fait en faisant le <strong>solde de la TVA per&ccedil;ue </strong>(&agrave; travers la
                        facturation de vos services et produits) <strong>et de la TVA pay&eacute;e </strong>(&agrave;
                        travers l&rsquo;achat de services ou de produits). Le solde est alors soit cr&eacute;diteur
                        (vous devez payer &agrave; l&rsquo;Etat le cr&eacute;dit) soit d&eacute;biteur (l&rsquo;Etat
                        vous rembourse la TVA).</p>
                    <p>Des <strong>particularit&eacute;s </strong>existent pour certains types de
                        soci&eacute;t&eacute;s, et notamment :</p>
                    <ul>
                        <li><strong>Les soci&eacute;t&eacute;s exportatrices de produits et services </strong>(sauf
                            m&eacute;taux de r&eacute;cup&eacute;ration) sont exon&eacute;r&eacute;es pendant 5 ans
                            d&rsquo;imp&ocirc;t sur les soci&eacute;t&eacute;s et b&eacute;n&eacute;ficient d&rsquo;un
                            taux r&eacute;duit &agrave; 17,5% au-del&agrave; de cette p&eacute;riode. Les produits
                            export&eacute;s sont par ailleurs exon&eacute;r&eacute;s de TVA ;</li>
                        <li><strong>Les soci&eacute;t&eacute;s industrielles </strong>sont exon&eacute;r&eacute;es
                            d&rsquo;imp&ocirc;ts les 5 premi&egrave;res ann&eacute;es (activit&eacute;s industrielles
                            d&eacute;finies par d&eacute;cret) ;</li>
                        <li>Certains produits font l&rsquo;objet de taux de TVA r&eacute;duits :</li>
                        <li>-7% pour certains produits de grande consommation</li>
                        <li>-10% pour certains produits alimentaires et boissons, pour l&rsquo;h&ocirc;tellerie, et pour
                            les avocats et notaires</li>
                        <li>-14% pour d&rsquo;autres produits</li>
                    </ul>
                    <p>Il existe aussi des <strong>taux sp&eacute;cifiques </strong>&agrave; certains produits
                        particuliers. Vous pouvez vous adresser &agrave;</p>
                    <p>la <a href="https://portail.tax.gov.ma/wps/portal/DGI/Accueil" target="_blank"
                            rel="noopener">Direction G&eacute;n&eacute;rale des Imp&ocirc;ts (DGI) </a>pour obtenir un
                        d&eacute;tail des taux sp&eacute;cifiques &agrave; votre activit&eacute;.</p>
