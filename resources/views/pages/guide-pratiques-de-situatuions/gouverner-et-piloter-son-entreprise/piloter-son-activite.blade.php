                <div class="text-containet">
                    <h5>1. QU&rsquo;EST CE QUE LE TABLEAU DE BORD DE PILOTAGE D&rsquo;ACTIVIT&Eacute; ET COMMENT IL SE
                        PR&Eacute;SENTE ?</h5>
                    <p><strong>Le tableau de bord </strong>est l&rsquo;outil essentiel pour le suivi de
                        l&rsquo;activit&eacute; de votre entreprise. Tout bon manager interagit avec son tableau de bord
                        r&eacute;guli&egrave;rement, afin de s&rsquo;assurer qu&rsquo;il suit
                        <strong>l&rsquo;application de son business plan</strong>. Le tableau de bord se place ainsi au
                        centre de la strat&eacute;gie de l&rsquo;entreprise afin d&rsquo;en piloter la performance.</p>
                    <p>Il existe des <strong>mod&egrave;les de r&eacute;f&eacute;rence </strong>servant de cadre
                        &agrave; la conception et production de tableaux de bord, un instrument d&rsquo;aide &agrave; la
                        d&eacute;cision efficace. En r&eacute;alit&eacute;, le tableau de bord est utilis&eacute;
                        &agrave; tous les niveaux de l&rsquo;entreprise du vendeur sur le terrain qui suit ses
                        r&eacute;sultats, au dirigeant de la structure, en passant par l&rsquo;ensemble des cadres
                        op&eacute;rationnels ou bien les pilotes de processus en charge de l&rsquo;am&eacute;lioration
                        de la performance des flux sous leur responsabilit&eacute;.</p>
                    <p>Le tableau de bord est un instrument de pilotage qui permet <strong>d&rsquo;appr&eacute;cier la
                            situation &agrave; l&rsquo;instant t</strong>, et qui facilite la prise de d&eacute;cision.
                        Il offre une lecture directe car il pr&eacute;sente les &eacute;l&eacute;ments suivants dans sa
                        structure :</p>
                    <ul>
                        <li>Les pr&eacute;visions : objectifs de vente, prix cible, marge pr&eacute;visionnelle, etc.
                        </li>
                        <li>Le r&eacute;alis&eacute; : la valeur r&eacute;elle observ&eacute;e des indicateurs suivis
                        </li>
                        <li>Les &eacute;carts entre l&rsquo;objectif et la r&eacute;alit&eacute;</li>
                        <li>L&rsquo;action rectificative qui en d&eacute;coule</li>
                    </ul>
                    <p>Parmi les indicateurs suivis, ces derniers peuvent &ecirc;tre de plusieurs natures :</p>
                    <ul>
                        <li>Indicateurs m&eacute;tiers (RH, logistique, )</li>
                        <li>Indicateurs financiers</li>
                        <li>Indicateurs de qualit&eacute;</li>
                        <li>Indicateurs commerciaux</li>
                    </ul>
                    <p>L&rsquo;image du tableau de bord d&rsquo;un v&eacute;hicule illustre parfaitement le tableau de
                        bord de l&rsquo;entreprise. Voyez le comme un moyen de visualiser les m&eacute;triques
                        n&eacute;cessaires &agrave; votre activit&eacute; afin de la piloter efficacement.</p>
                    <p>Parmi les crit&egrave;res de r&eacute;ussite pour construire son tableau de bord, il est
                        conseill&eacute; de :</p>
                    <ol>
                        <li><strong>Faire participer l&rsquo;ensemble des collaborateurs </strong>et des utilisateurs
                            &agrave; l&rsquo;&eacute;laboration du tableau de bord et des indicateurs &agrave; suivre
                        </li>
                        <li><strong>Limiter le nombre d&rsquo;indicateurs </strong>&agrave; 5-7 maximum</li>
                        <li>D&eacute;finir des <strong>indicateurs mesurables</strong></li>
                        <li>Elaborer un <strong>tableau clair et lisible</strong></li>
                    </ol>
                    <p>&nbsp;</p>
                    <h5>2. LES TROIS &Eacute;TAPES CL&Eacute;S POUR &Eacute;LABORER UN TABLEAU DE BORD DE PILOTAGE
                        D&rsquo;ACTIVIT&Eacute;</h5>
                    <p>Les trois &eacute;tapes cl&eacute;s de construction d&rsquo;un tableau de bord se
                        d&eacute;clinent comme suit :</p>
                    <h3>I. D&eacute;finir la finalit&eacute; et le contenu du tableau de bord</h3>
                    <p>&nbsp;</p>
                    <ol>
                        <li><strong>D&eacute;terminer les objectifs du tableau de bord</strong></li>
                    </ol>
                    <p>Pour qu&rsquo;il soit efficace, un tableau de bord doit avoir une finalit&eacute;, un objectif
                        pr&eacute;cis dans le fonctionnement de l&rsquo;entreprise. Il ne s&rsquo;agit pas de
                        s&rsquo;encombrer d&rsquo;un document qu&rsquo;il faut remplir juste pour la bonne forme</p>
                    <p>Ainsi, les questions cl&eacute;s &agrave; se poser pour &eacute;tablir la finalit&eacute;
                        d&rsquo;un tableau de bord sont :</p>
                    <ul>
                        <li>qui est le destinataire du tableau de bord ?</li>
                        <li>Qu&rsquo;a-t-on besoin d&rsquo;avoir comme information ?</li>
                        <li>Que doit permettre l&rsquo;information remont&eacute;e ?</li>
                        <li>Que va-t-on faire des r&eacute;sultats remont&eacute;s ?</li>
                    </ul>
                    <p>Une fois que l&rsquo;on a r&eacute;pondu &agrave; ces questions cl&eacute;s, la finalit&eacute;
                        du tableau de bord appara&icirc;t plus &eacute;vidente : il peut servir &agrave; piloter
                        l&rsquo;activit&eacute; de mani&egrave;re g&eacute;n&eacute;rale, ou le suivi commercial,
                        o&ugrave; les indicateurs de sant&eacute; financi&egrave;re, ou l&rsquo;organisation, etc.</p>
                    <ol start="2">
                        <li><strong>Choisir les indicateurs cl&eacute;s</strong></li>
                    </ol>
                    <p>&nbsp;</p>
                    <p>D&eacute;finir les indicateurs de suivi n&rsquo;est pas une chose facile : on peut rapidement
                        tomber dans de la surench&egrave;re et rester bloqu&eacute;s dans des suivis d&rsquo;indicateurs
                        qui n&rsquo;aident ni &agrave; la d&eacute;cision, ni au pilotage de l&rsquo;activit&eacute;.
                        Aussi, il faut &ecirc;tre conscient, avant de choisir, qu&rsquo;il existe <strong>deux types
                            d&rsquo;indicateurs :</strong></p>
                    <ul>
                        <li><strong>Indicateurs de r&eacute;sultat </strong>: &eacute;valuent la performance de
                            l&rsquo;entreprise et sont reli&eacute;s &agrave; un objectif. Ils peuvent par exemple
                            &ecirc;tre relatifs au suivi financier, &agrave; la rentabilit&eacute;, &agrave; la
                            productivit&eacute;, au co&ucirc;t, etc. Ils permettent, en somme, d&rsquo;appr&eacute;cier
                            si l&rsquo;objectif est atteint ou pas.</li>
                        <li><strong>Indicateurs d&rsquo;activit&eacute; </strong>: mesurent l&rsquo;&eacute;tat de
                            l&rsquo;activit&eacute;, d&rsquo;un point de vue des processus. Ils concernent par exemple
                            les quantit&eacute;s vendues, les stocks, etc.</li>
                    </ul>
                    <p>Dans l&rsquo;&eacute;laboration d&rsquo;un tableau de bord, <strong>il est essentiel
                            d&rsquo;avoir des indicateurs d&rsquo;activit&eacute; et de r&eacute;sultat</strong>, et que
                        ces deux derniers soient li&eacute;s &agrave; des objectifs pr&eacute;cis
                        cat&eacute;goris&eacute;s dans le tableau de bord.</p>
                    <ol start="3">
                        <li><strong>Organiser le pilotage</strong></li>
                    </ol>
                    <p>&nbsp;</p>
                    <p>Le pilotage du tableau de bord contient <strong>deux &eacute;l&eacute;ments cl&eacute;s
                        </strong>&agrave; prendre en compte :</p>
                    <ul>
                        <li>Qui va utiliser le tableau de bord ?</li>
                    </ul>
                    <p>Si le tableau de bord a un impact transverse au niveau de l&rsquo;organisation, il est primordial
                        d&rsquo;associer tous les collaborateurs dans son alimentation. Dans le cas d&rsquo;une startup,
                        il est essentiel que le g&eacute;rant imagine une utilisation finale du tableau de bord pour en
                        faire, d&egrave;s le d&eacute;marrage, un outil clair de suivi, diffusable et lisible par
                        d&rsquo;autres parties prenantes.</p>
                    <ul>
                        <li>A quelle fr&eacute;quence utiliser le tableau de bord ?</li>
                    </ul>
                    <p>La fr&eacute;quence d&rsquo;utilisation du tableau de bord d&eacute;pend des questions
                        li&eacute;es &agrave; la p&eacute;riodicit&eacute; de l&rsquo;activit&eacute; de
                        l&rsquo;entreprise (inutile de suivre un tableau de bord toutes les semaines si la
                        remont&eacute;e de l&rsquo;indicateur suivi se fait par trimestre !). <strong>Plus on se
                            rapproche des op&eacute;rations, plus la fr&eacute;quence de mise &agrave; jour est
                            &eacute;lev&eacute;e</strong>. Par exemple, dans l&rsquo;usine, un op&eacute;rateur qui suit
                        une machine a besoin d&rsquo;un tableau de bord journalier pour s&rsquo;assurer de sa
                        production. A l&rsquo;inverse, les d&eacute;cideurs strat&eacute;giques doivent pouvoir observer
                        des tendances pour la prise de d&eacute;cision ; ils ont besoin d&rsquo;un temps plus long pour
                        r&eacute;agir.</p>
                    <h3>II. D&eacute;finir la solution technologique ou le support</h3>
                    <p>Bien que le bon vieux tableau Excel soit le plus pl&eacute;biscit&eacute;, il existe
                        aujourd&rsquo;hui de nombreux outils en ligne qui remplissent la fonction de tableau de bord et
                        permettent un suivi d&rsquo;activit&eacute; pr&eacute;cis. <strong>La seule question est :
                            d&rsquo;o&ugrave; viennent les informations qui servent &agrave; l&rsquo;&eacute;laboration
                            du tableau de bord ? </strong>Si vous utilisez d&eacute;j&agrave; un outil de CRM (Customer
                        Relationship management) ou une autre solution d&eacute;di&eacute;e, il faut voir comment
                        l&rsquo;interface entre le CRM et votre tableau de bord peut &ecirc;tre int&eacute;gr&eacute;e.
                        Si c&rsquo;est la premi&egrave;re fois, pas de panique : Excel peut tr&egrave;s bien faire
                        l&rsquo;affaire le temps de mettre en marche son activit&eacute; et de se doter d&rsquo;outils
                        plus int&eacute;gr&eacute;s par la suite.</p>
                    <h3>III. Elaborer la structure et la pr&eacute;sentation du tableau de bord</h3>
                    <p>&nbsp;</p>
                    <ol>
                        <li><strong>Organiser les informations</strong></li>
                    </ol>
                    <p>&nbsp;</p>
                    <p>La pr&eacute;sentation des informations du tableau de bord est un point essentiel et critique
                        pour faire passer les bons messages aux collaborateurs. Combien de r&eacute;unions peuvent
                        d&eacute;raper pour cause de mauvaise interpr&eacute;tation des indicateurs et des performances
                        !</p>
                    <p>Pour pr&eacute;senter de mani&egrave;re efficace un tableau de bord, gardez en t&ecirc;te que ce
                        dernier doit &ecirc;tre <strong>simple, efficace, coh&eacute;rent et orient&eacute;
                            action.</strong></p>
                    <p>L&rsquo;organisation peut ainsi se faire par :</p>
                    <ul>
                        <li><strong>Chantier ou objectif </strong>(exemple : Chantier 1/ Am&eacute;liorer la performance
                            &ndash; indicateur 1, 2 et 3 ; Chantier 2 / Conqu&eacute;rir de nouveaux march&eacute;s
                            &ndash; indicateurs 1, 3 et 3, ) ;</li>
                        <li><strong>Groupe d&rsquo;indicateurs </strong>(indicateurs de chiffre d&rsquo;affaires,
                            indicateurs de rentabilit&eacute;, indicateurs d&rsquo;efficacit&eacute; commerciale, )</li>
                        <li><strong>Zone g&eacute;ographique ou business unit </strong>(Afrique, Asie, Moyen Orient ou
                            par BU)<br /><br /><br /></li>
                    </ul>
                    <ol start="2">
                        <li><strong>Choisir la visualisation graphique</strong></li>
                    </ol>
                    <p>&nbsp;</p>
                    <p>Le dernier point par rapport au pilotage de son activit&eacute; est la <strong>visualisation
                            graphique des informations</strong>. En effet, de nombreuses fa&ccedil;ons existent de
                        mettre en lien et en comparaison des informations, et il faut pouvoir choisir les bonnes
                        repr&eacute;sentations afin d&rsquo;&ecirc;tre le plus pertinent et d&rsquo;amener la
                        d&eacute;cision la plus optimale.</p>
                    <p>Parmi les moyens et outils de visualisation, on peut citer :</p>
                    <ul>
                        <li><strong>Les camemberts ou diagrammes circulaires </strong>repr&eacute;sentent des
                            proportions ou des pourcentages parmi un ensemble donn&eacute;. Il faut cependant veiller
                            &agrave; ne pas mettre trop d&rsquo;indicateurs sinon le diagramme devient vite illisible ;
                        </li>
                        <li><strong>Les jauges </strong>pour repr&eacute;senter des ratios &agrave; monitorer ;</li>
                        <li><strong>Mes graphiques lin&eacute;aires </strong>repr&eacute;sentent une tendance, une
                            &eacute;volution, et peuvent comparer les tendances de plusieurs indicateurs entre eux ;
                        </li>
                        <li><strong>Les histogrammes ou diagramme en barre ou b&acirc;tons</strong>, enfin, qui
                            permettent de comparer des valeurs entre</li>
                    </ul>
                    <p>&nbsp;</p>
                    <p><strong>A retenir : </strong><strong>les graphiques doivent rester simples et
                            &eacute;pur&eacute;s. Ils doivent pouvoir &ecirc;tre lisibles, utilisables et duplicables et
                            permettre la prise de d&eacute;cision rapide.</strong></p>

