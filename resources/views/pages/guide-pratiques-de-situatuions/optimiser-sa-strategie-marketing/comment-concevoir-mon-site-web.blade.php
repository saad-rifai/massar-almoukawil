                    <h5><strong>II. </strong>Faire appel &agrave; une agence de cr&eacute;ation de site internet ou &agrave; un d&eacute;veloppeur</h5>
                    <p>Le r&ocirc;le d&rsquo;une agence de cr&eacute;ation de site web ou d&rsquo;un d&eacute;veloppeur est d&rsquo;assurer la r&eacute;alisation technique et le d&eacute;veloppement informatique de votre site web. Ils pourront r&eacute;aliser de tr&egrave;s beaux sites sur la base</p>
                    <p>du <strong>cahier des charges </strong>que vous leur soumettrez.</p>
                    <p>Votre cahier des charges devra mentionner les principales modalit&eacute;s techniques et graphiques que vous souhaitez retrouver dans votre site :</p>
                    <ul>
                    <li><strong>Caract&eacute;ristiques du site </strong>: langues, adaptation &agrave; diff&eacute;rents types d&rsquo;&eacute;crans, affichage optimis&eacute;, &ldquo;Responsive Design&rdquo;, ouverture sur les r&eacute;seaux sociaux, etc.</li>
                    <li><strong>Arborescence du site </strong>: menu et type affichage, &ldquo;footer&rdquo;, rubriques principales, pages suppl&eacute;mentaires,</li>
                    </ul>
                    <ul>
                    <li><strong>El&eacute;ments techniques </strong>: type de solution CMS souhait&eacute;e, code couleurs,</li>
                    </ul>
                    <p>Votre prestataire devra r&eacute;aliser des tests et apporter les corrections n&eacute;cessaires avant de rendre votre site fonctionnel. Dans votre cahier des charges vous pourrez pr&eacute;ciser que le prestataire mettra les contenus en ligne selon la forme de la charte retenue dans les langues pr&eacute;vues.</p>
                    <p>Une fois votre site r&eacute;alis&eacute;, votre prestataire pourra &eacute;ventuellement en assurer la maintenance (selon des modalit&eacute;s &agrave; pr&eacute;ciser) et faire &eacute;voluer vos applications.</p>
