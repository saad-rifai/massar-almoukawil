                    <h5><strong>I. </strong>Utiliser un syst&egrave;me de gestion de contenu (CMS) en Open Source</h5>
                    <p>Les <strong>CMS </strong>(Content Management Software) d&eacute;signent la famille de logiciels en libre d&rsquo;acc&egrave;s (<strong>Open Source</strong>) pour concevoir et mettre &agrave; jour relativement facilement des sites web.</p>
                    <p>Parmi les principaux CMS disponibles, par ordre de popularit&eacute; on trouve <a href="https://fr.wordpress.com/">WordPress </a>(principal syst&egrave;me de gestion de contenu), <a href="https://www.drupal.fr/">Drupal </a>et <a href="https://www.joomla.fr/">Joomla </a>(principales caract&eacute;ristiques ci-dessous).</p>
                    <p>A noter qu&rsquo;ils proposent souvent des <strong>extensions payantes </strong>pour am&eacute;liorer l&rsquo;esth&eacute;tique du site, d&eacute;velopper le back-office (suivi d&rsquo;indicateurs de performance).</p>
                    <p>WordPress</p>
                    <p>&nbsp;</p>
                    <ul>
                    <li>CMS le plus utilis&eacute; au monde (73% de parts de march&eacute;)</li>
                    <li>Gratuit&eacute; et et rapidit&eacute; d&rsquo;installation</li>
                    <li>Ergonomie agr&eacute;able et syst&egrave;me intuitif</li>
                    <li>Forte communaut&eacute; sur le net qui accompagne les d&eacute;butants</li>
                    <li>Large biblioth&egrave;que de plugins (extensions) mais attention aux plugins non officiel qui sont en r&eacute;alit&eacute; des malwares</li>
                    <li>N&eacute;cessite des connaissances en programmation pour modifier la structure de son site</li>
                    </ul>
                    <p>Joomla</p>
                    <p>&nbsp;</p>
                    <ul>
                    <li>Id&eacute;al pour un projet cons&eacute;quent et des plateformes complexes</li>
                    <li>Puissance de gestion, nombreuses extensions et modules disponibles (relativement compliqu&eacute;s &agrave; mettre en place)</li>
                    <li>D&eacute;conseill&eacute; pour un blog ou un site vitrine peu cons&eacute;quent</li>
                    </ul>
