                <br>
                <div class="text-containet">

                    <p>Il existe <strong>3 types de d&eacute;p&ocirc;t </strong>(marque, brevet, dessin et mod&egrave;le
                        industriel) offrant un titre de propri&eacute;t&eacute; industrielle distinct et qui permettent,
                        par la voie d&rsquo;une publication, de <strong>b&eacute;n&eacute;ficier d&rsquo;un monopole
                            d&rsquo;exploitation sur un territoire donn&eacute;, pendant une dur&eacute;e
                            d&eacute;termin&eacute;e.</strong></p>
                    <p></p>
                    <p>La demande doit &ecirc;tre soumise par une ou plusieurs personnes physiques (entreprises
                        individuelles) ou morales (soci&eacute;t&eacute;s). Si la marque est d&eacute;pos&eacute;e par
                        plusieurs personnes physiques ou plusieurs personnes morales, elles seront
                        repr&eacute;sent&eacute;es par un mandataire.</p>
