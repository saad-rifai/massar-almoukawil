                    <p></p>
                    <p>Comment d&eacute;poser une marque au Maroc et &agrave; l&rsquo;international ?</p>
                    <p>Il est primordial de prot&eacute;ger vos marques &agrave; l&rsquo;int&eacute;rieur du Maroc et/ou
                        dans les pays o&ugrave; vous exercez votre activit&eacute;.</p>
                    <p><em>&Eacute;tapes &agrave; suivre pour d&eacute;poser une marque au Maroc</em></p>
                    <ol>
                        <li>Choisir sa marque</li>
                    </ol>
                    <p>Pour &ecirc;tre &eacute;ligible, une marque doit &ecirc;tre :</p>
                    <ul>
                        <li><strong>Licite </strong>: ne comporter ni de signes interdits par la loi ni
                            d&rsquo;&eacute;l&eacute;ments trompeurs pouvant induire le consommateur en erreur</li>
                        <li><strong>Distinctive </strong>: ne pas &ecirc;tre assimilable aux produits d&rsquo;une
                            entreprise concurrente et &ecirc;tre ind&eacute;pendant de la d&eacute;signation habituelle
                            de l&rsquo;objet marqu&eacute; (ex : marque &laquo; beurre &raquo; pour d&eacute;signer du
                            beurre)</li>
                        <li><strong>Disponible </strong>: pour v&eacute;rifier la disponibilit&eacute; d&rsquo;une
                            marque au Maroc ou &agrave; l&rsquo;international, l&rsquo;OMPIC met &agrave; disposition
                            des bases de donn&eacute;es sur son site web</li>
                    </ul>
                    <p>Il vous faut aussi d&eacute;terminer la (ou les) classe(s) de vos produits/services en fonction
                        de la &ldquo;classification de Nice&rdquo; (ordonn&eacute;e en 45 classes), disponible sur le
                        site de l&rsquo;OMPIC.</p>
                    <h6>2. D&eacute;poser sa marque</h6>
                    <p>Le dossier doit &ecirc;tre d&eacute;pos&eacute; <strong>aupr&egrave;s de l&rsquo;OMPIC</strong>.
                        Il contient un formulaire et des reproductions de la marque en noir et blanc et en couleur.</p>
                    <h6>3. Valider le d&eacute;p&ocirc;t</h6>
                    <p>Le processus de validation du d&eacute;p&ocirc;t de votre marque comprend plusieurs phases :</p>
                    <ul>
                        <li>Le dossier est examin&eacute; sur le fond et sur la forme par l&rsquo;OMPIC ;</li>
                        <li>Si votre demande est valid&eacute;e, la marque est d&eacute;pos&eacute;e pour une
                            p&eacute;riode de 2 mois dans le &laquo; Catalogue Officiel des Marques &raquo;, accessible
                            sur le site web de l&rsquo;OMPIC ;</li>
                        <li>Si aucune opposition n&rsquo;est survenue, la marque est enregistr&eacute;e et vous recevez
                            un certificat d&rsquo;enregistrement.</li>
                    </ul>
     
                    <p><em>&Eacute;tapes &agrave; suivre pour d&eacute;poser une marque &agrave;
                            l&rsquo;international</em></p>
                    <p></p>
                    <p>Le d&eacute;p&ocirc;t d&rsquo;une marque dans vos pays d&rsquo;exportation peut se faire via
                        l&rsquo;OMPIC qui pr&eacute;sentera votre demande &agrave; l&rsquo;OMPI (<a
                            href="https://www.wipo.int/portal/fr/">Organisation Mondiale de la Propri&eacute;t&eacute;
                            Intellectuelle</a>). <strong>Pour d&eacute;poser une marque &agrave; l&rsquo;international,
                            vous devez d&rsquo;abord avoir effectu&eacute; un d&eacute;p&ocirc;t au Maroc.</strong></p>
                    <p></p>
                    <p>Lorsque vous avez d&eacute;pos&eacute; une marque de fabrique, de commerce ou de service au
                        Maroc, vous &ecirc;tes prioritaire pendant 6 mois pour la d&eacute;poser dans les autres pays
                        signataires de la Convention de Paris (174 pays).</p>
                    <p>Le paiement des droits s&rsquo;effectue directement &agrave; l&rsquo;OMPI en Francs Suisses.
                        L&rsquo;OMPI met &agrave; votre disposition cet outil pour estimer les droits que vous aurez
                        &agrave; payer : <a href="https://www.wipo.int/madrid/feescalculator/">WIPO Madrid Fee
                            Calculator.</a></p>
                    <h5>2. Comment d&eacute;poser un brevet d&rsquo;invention au Maroc et &agrave;
                        l&rsquo;international?</h5>
                    <p>Un brevet d&rsquo;invention vous permet de prot&eacute;ger et de rentabiliser votre innovation,
                        qu&rsquo;il s&rsquo;agisse d&rsquo;un produit ou d&rsquo;un proc&eacute;d&eacute; technique,
                        pour une <strong>dur&eacute;e maximale de 20 ans</strong>. Le brevet entrera dans
                        <strong>l&rsquo;actif immat&eacute;riel </strong>de votre entreprise et vous donnera un avantage
                        strat&eacute;gique sur vos concurrents. L&rsquo;OMPIC est charg&eacute;e du d&eacute;p&ocirc;t
                        des brevets au Maroc et &agrave; l&rsquo;international.
                    </p>
                    <p><em>Comment d&eacute;poser un brevet au Maroc ?</em></p>
                    <p>Evaluation de votre invention</p>
                    <p>Pour &ecirc;tre brevetable, votre invention doit respecter 3 crit&egrave;res :</p>
                    <ul>
                        <li><strong>Nouveaut&eacute; : </strong>elle ne doit pas avoir &eacute;t&eacute; rendue publique
                            par un autre soit par un brevet, soit dans d&rsquo;autres types de communication, soit par
                            l&rsquo;usage ;</li>
                        <li><strong>Activit&eacute; inventive : </strong>elle ne doit pas d&eacute;couler de
                            mani&egrave;re &eacute;vidente, pour un &ldquo;homme de m&eacute;tier&rdquo; de
                            l&rsquo;&eacute;tat de la technique ;</li>
                    </ul>
                    <ul>
                        <li><strong>Application industrielle : </strong>elle doit pr&eacute;senter une utilit&eacute;
                            d&eacute;termin&eacute;e, probante et cr&eacute;dible.</li>
                    </ul>
                    <h6>2. D&eacute;p&ocirc;t du brevet</h6>
                    <p>Le formulaire de d&eacute;p&ocirc;t doit &ecirc;tre transmis &agrave; l&rsquo;OMPIC et
                        pr&eacute;senter un m&eacute;moire descriptif de l&rsquo;invention (pr&eacute;cisant le domaine
                        d&rsquo;intervention, l&rsquo;&eacute;tat de la technique ant&eacute;rieur, une description du
                        probl&egrave;me technique et de la solution apport&eacute;e). Vous disposez alors d&rsquo;un
                        mois pour payer les droits de d&eacute;p&ocirc;ts de votre demande avant qu&rsquo;elle ne soit
                        retir&eacute;e.</p>
                    <h6>3. Proc&eacute;dure pr&eacute;liminaire</h6>
                    <p>Votre d&eacute;p&ocirc;t est examin&eacute; par un sp&eacute;cialiste du domaine technique de
                        l&rsquo;invention qui remet un rapport sur l&rsquo;&eacute;tat de la technique (liste de brevets
                        existants en relation avec votre invention) et &eacute;met une opinion sur la
                        brevetabilit&eacute; de votre invention. Une fois le rapport &eacute;mis, le d&eacute;posant a 3
                        mois pour &eacute;mettre des corrections et des observations s&rsquo;il le souhaite.</p>
                    <h6>4. Publication</h6>
                    <p>18 mois apr&egrave;s votre demande et si celle-ci n&rsquo;a pas &eacute;t&eacute; rejet&eacute;e,
                        le rapport pr&eacute;liminaire (&eacute;ventuellement amend&eacute;) est publi&eacute;. Dans les
                        2 mois qui suivent la publication de la demande, tout individu ou entreprise a la
                        possibilit&eacute; d&rsquo;adresser des observations. Le d&eacute;posant a 2 mois pour
                        r&eacute;pondre &agrave; ces observations s&rsquo;il le souhaite.</p>
                    <h6>5. Rapport d&eacute;finitif et d&eacute;livrance</h6>
                    <p>En fonction du rapport de recherche pr&eacute;liminaire et des observations du demandeur et/ou
                        des tiers, un rapport d&eacute;finitif est arr&ecirc;t&eacute;. S&rsquo;il n&rsquo;y a pas de
                        motifs de rejet, le brevet est d&eacute;livr&eacute; apr&egrave;s paiement des droits et
                        publi&eacute; avec son rapport d&eacute;finitif.</p>
                    <p><em>II.Comment d&eacute;poser un brevet &agrave; l&rsquo;international ?</em></p>
                    <p></p>
                    <p><strong>PCT &ndash; Syst&egrave;me international des brevets </strong>: Un brevet
                        d&rsquo;invention n&rsquo;a d&rsquo;effet que dans le pays dans lequel il est
                        d&eacute;livr&eacute; et en vigueur. En dehors de ce territoire, tout le monde est libre
                        d&rsquo;exploiter l&rsquo;invention. La m&eacute;thode la plus simple et la moins co&ucirc;teuse
                        est la m&eacute;thode PCT puisqu&rsquo;elle permet de demander la protection d&rsquo;une
                        invention simultan&eacute;ment dans 148 pays.</p>
                    <p>Plus d&rsquo;infos sur le site de l&rsquo;OMPI <a href="http://www.wipo.int/pct/fr/">: </a><a
                            href="https://www.wipo.int/pct/fr/">PCT &mdash; Le syst&egrave;me international des
                            brevets</a></p>
                    <h5>3. Comment d&eacute;poser un Dessin ou Mod&egrave;le Industriel (DMI) au Maroc et &agrave;
                        l&rsquo;international ?</h5>
                    <p>D&eacute;poser un dessin ou mod&egrave;le industriel vous permet de <strong>prot&eacute;ger
                            l&rsquo;apparence et l&rsquo;aspect esth&eacute;tique de votre produit</strong>, ce qui
                        repr&eacute;sente un int&eacute;r&ecirc;t vital pour pr&eacute;server l&rsquo;image et
                        l&rsquo;identit&eacute; de votre entreprise.</p>
                    <ul>
                        <li><em>Comment d&eacute;poser un DMI au Maroc ?</em></li>
                    </ul>
                    <ol>
                        <li>Evaluation de la nouveaut&eacute;</li>
                    </ol>
                    <p>Pour &ecirc;tre d&eacute;pos&eacute;, votre dessin ou mod&egrave;le doit &ecirc;tre
                        consid&eacute;r&eacute; comme nouveau, c&rsquo;est-&agrave;-dire <strong>poss&eacute;der un
                            caract&egrave;re propre</strong>, et ne pas &ecirc;tre semblable &agrave; un produit rendu
                        public par une publicit&eacute; ou tout autre moyen. Vous pouvez faire une recherche
                        d&rsquo;ant&eacute;riorit&eacute; en consultant la base de donn&eacute;es marocaine de
                        l&rsquo;OMPIC ou la base de donn&eacute;es internationale.</p>
                    <h6>2. D&eacute;p&ocirc;t du Dessin ou Mod&egrave;le Industriel (DMI)</h6>
                    <p>Votre DMI peut &ecirc;tre d&eacute;pos&eacute; :</p>
                    <ul>
                        <li>Physiquement : au si&egrave;ge de l&rsquo;OMPIC &agrave; Casablanca, dans l&rsquo;une de ses
                            antennes r&eacute;gionales, &agrave; la CFCIM ou &agrave; la Chambre de commerce de
                            Casablanca</li>
                    </ul>
                    <ul>
                        <li>En ligne: sur la plateforme <a href="https://www.directinfo.ma/">Directinfo &ndash;
                                OMPIC</a></li>
                    </ul>
                    <p>Votre d&eacute;p&ocirc;t devra notamment comporter un formulaire rempli (disponible sur le site
                        de l&rsquo;OMPIC), des reproductions graphiques. Vous devez &eacute;galement payer les droits de
                        d&eacute;p&ocirc;t pour que votre dossier soit examin&eacute;.</p>
                    <h6>3. Examen de recevabilit&eacute;</h6>
                    <p>Votre DMI est &eacute;valu&eacute; par des membres de l&rsquo;OMPIC pour v&eacute;rifier que sont
                        bien pr&eacute;sentes l&rsquo;ensemble des pi&egrave;ces demand&eacute;es, si votre DMI respecte
                        bien la d&eacute;finition et s&rsquo;il ne porte pas atteinte aux bonnes m&oelig;urs.</p>
                    <h6>4. Publication et enregistrement</h6>
                    <p>Si l&rsquo;examen est satisfaisant, votre demande est publi&eacute;e au Catalogue Officiel des
                        Dessins et Mod&egrave;les Industriels pour une dur&eacute;e de 2 mois. Si aucune
                        r&eacute;clamation n&rsquo;est &eacute;mise, votre DMI est enregistr&eacute; pour une
                        dur&eacute;e de 5 ans &agrave; compter de la date du d&eacute;p&ocirc;t.</p>
                    <p>Tarifs (HT) en vigueur au 2 septembre 2019</p>
                    <ul>
                        <li>Droit de d&eacute;p&ocirc;t (5 mod&egrave;les) : 600 dhs (480 dhs si d&eacute;marche en
                            ligne);</li>
                        <li>Droit de renouvellement (5 mod&egrave;les) : 600 dhs (480 dhs si d&eacute;marche en ligne).
                        </li>
                    </ul>
                    <p>L&rsquo;ensemble des tarifs en vigueur est disponible sur le site de l&rsquo;OMPIC dans la
                        rubrique : <a href="http://www.ompic.org.ma/fr/content/tarifs-rcc">Tarifs.</a></p>
                    <ol>
                        <li><em>Comment d&eacute;poser un DMI &agrave; l&rsquo;international ?</em></li>
                    </ol>
                    <p></p>
                    <p>Le mode de d&eacute;p&ocirc;t le plus simple est la proc&eacute;dure offerte par
                        l&rsquo;Arrangement de La Haye, qui est administr&eacute;e par l&rsquo;OMPIC. Un d&eacute;posant
                        peut d&eacute;poser une demande internationale aupr&egrave;s de l&rsquo;OMPIC : le dessin ou
                        mod&egrave;le sera alors prot&eacute;g&eacute; dans autant de pays membres du trait&eacute; que
                        le souhaite le d&eacute;posant.</p>
                    <p>Toutes les informations sur l&rsquo;Arrangement de La Haye ainsi qu&rsquo;un formulaire de
                        demande et un outil de calcul des droits exigibles par pays sont disponibles sur le site web de
                        l&rsquo;OMPI : <a href="https://www.wipo.int/hague/fr/">Syst&egrave;me international des</a> <a
                            href="https://www.wipo.int/hague/fr/">dessins et mod&egrave;les industriels &ndash; La
                            Haye</a></p>

