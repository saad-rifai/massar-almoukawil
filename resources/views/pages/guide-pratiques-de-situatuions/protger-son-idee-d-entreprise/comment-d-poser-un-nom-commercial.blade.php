                <div class="text-containet">
                    <p>Votre nom commercial peut correspondre (ou pas) &agrave; votre d&eacute;nomination sociale ou
                        raison sociale (l&rsquo;appellation sous laquelle votre soci&eacute;t&eacute; commerciale est
                        inscrite au Registre du Commerce). Cette d&eacute;nomination/raison sociale doit &ecirc;tre
                        unique, simple &agrave; retenir et suffisamment explicite.</p>
                    <p><strong>Votre nom commercial est le nom qui va figurer sur tous vos documents </strong>(factures,
                        cartes de visite) en plus des mentions obligatoires (d&eacute;nomination sociale, identifiants
                        fiscaux, etc.).</p>
                    <p>Le d&eacute;p&ocirc;t du nom commercial constitue la premi&egrave;re &eacute;tape de la
                        cr&eacute;ation d&rsquo;entreprise. Cette d&eacute;marche peut s&rsquo;effectuer rapidement soit
                        via votre <a target="_blank"
                            href="http://www.pncl.gov.ma/fr/Gestionterritoriale/Partenaires/Pages/centreRegional.aspx">Centre
                            R&eacute;gional d&rsquo;Investissement, </a>soit &agrave; travers votre comptable ou votre
                        fiduciaire (la prestation est factur&eacute;e en sus des op&eacute;rations courantes). Les
                        &eacute;tapes &agrave; suivre sont les suivantes :</p>
                    <h5>Choisir sa d&eacute;nomination sociale et/ou son nom commercial</h5>
                    <p>Vous devez v&eacute;rifier que le nom commercial de votre choix n&rsquo;est pas
                        d&eacute;j&agrave; attribu&eacute;. Pour cela, l&rsquo;Office Marocain de la
                        Propri&eacute;t&eacute; Industrielle et Commerciale (<strong>OMPIC</strong>) met &agrave; votre
                        disposition un outil gratuit sur son site : <a target="_blank"
                            href="http://www.ompic.org.ma/fr/content/recherche-dans-les-bases-de-donees-ompic">Recherche
                            dans les bases de donn&eacute;es OMPIC.</a></p>
                    <p>Par ailleurs, votre choix de nom commercial doit satisfaire aux crit&egrave;res suivants pour
                        &ecirc;tre attribu&eacute; :</p>
                    <ul>
                        <li>Ne pas &ecirc;tre contraire &agrave; l&rsquo;ordre public et aux bonnes m&oelig;urs ;</li>
                        <li>Ne pas susciter de confusion : c&rsquo;est-&agrave;-dire &ecirc;tre assimil&eacute; &agrave;
                            une marque existante ou &agrave; un organisme L&rsquo;utilisation de certains termes
                            n&eacute;cessite une autorisation sp&eacute;ciale ;</li>
                        <li>&Ecirc;tre distinctif : permettre &agrave; l&rsquo;entreprise de se distinguer
                            ais&eacute;ment des autres entreprises de son</li>
                    </ul>
                    <h5><strong>2. </strong>Demander un certificat n&eacute;gatif (CN)</h5>
                    <p>Le certificat n&eacute;gatif est un document qui atteste que le nom demand&eacute; n&rsquo;est
                        pas d&eacute;j&agrave; utilis&eacute; et peut donc &ecirc;tre inscrit <strong>pour
                            l&rsquo;immatriculation au Registre du Commerce </strong>(RC). Il existe deux moyens de
                        l&rsquo;obtenir:</p>
                    <ul>
                        <li>En ligne via le site <a target="_blank" href="http://www.directompic.ma/fr/cn">Directinfo &ndash; Demande en
                                ligne du certificat n&eacute;gatif</a></li>
                        <li>En direct en se rendant dans l&rsquo;une des 28 <a target="_blank"
                                href="http://www.ompic.org.ma/fr/antennes-regionales">Antennes R&eacute;gionales </a>de
                            l&rsquo;OMPIC ou dans l&rsquo;un des 15 <a target="_blank"
                                href="http://www.pncl.gov.ma/fr/Gestionterritoriale/Partenaires/Pages/centreRegional.aspx">Centres
                                R&eacute;gionaux d&rsquo;investissement </a>(CRI)</li>
                    </ul>
                    <p>Liste des pi&egrave;ces &agrave; fournir :</p>
                    <ul>
                        <li>Une demande sur imprim&eacute; &agrave; t&eacute;l&eacute;charger en ligne ou &agrave;
                            retirer aupr&egrave;s du CRI</li>
                        <li>Carte d&rsquo;identit&eacute; nationale (CIN) ou passeport</li>
                        <li>Photocopie de la CIN ou passeport l&eacute;galis&eacute; si le demandeur se fait
                            repr&eacute;senter</li>
                    </ul>
                    <h5><strong>3. </strong>Valider le d&eacute;p&ocirc;t</h5>
                    <p>Si la demande est valid&eacute;e, vous pourrez retirer le certificat n&eacute;gatif original. En
                        cas de rejet, vous avez la possibilit&eacute; de d&eacute;poser sans frais une nouvelle demande
                        dans un d&eacute;lai de 30 jours &agrave; compter de la date du premier
                        d&eacute;p&ocirc;t.<br /><br /></p>
                    <h5><strong>4. </strong>S&rsquo;inscrire au RCC</h5>
                    <p>Le certificat n&eacute;gatif atteste la disponibilit&eacute; du nom demand&eacute; et attribue
                        une r&eacute;servation d&rsquo;une ann&eacute;e afin d&rsquo;achever la proc&eacute;dure de
                        cr&eacute;ation d&rsquo;entreprise. Une fois les d&eacute;marches effectu&eacute;es et ce
                        certificat obtenu, il est <strong>indispensable d&rsquo;inscrire son nom commercial au Registre
                            Central du Commerce (RCC) </strong>dans le cadre de votre cr&eacute;ation
                        d&rsquo;entreprise.<br /><br /></p>
