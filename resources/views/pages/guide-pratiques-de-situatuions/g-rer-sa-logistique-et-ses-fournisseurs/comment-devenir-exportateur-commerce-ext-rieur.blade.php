                    <h5>1. QUEL PAYS CIBLER POUR SE D&Eacute;VELOPPER &Agrave; L&rsquo;INTERNATIONAL ET AVEC QUELS CRIT&Egrave;RES?</h5>
                    <p>Le d&eacute;veloppement &agrave; l&rsquo;international d&rsquo;une entreprise se fait g&eacute;n&eacute;ralement de deux mani&egrave;res :</p>
                    <ol>
                    <li><strong>Par opportunisme<br /><br /></strong>Des opportunit&eacute;s &agrave; l&rsquo;international peuvent se pr&eacute;senter spontan&eacute;ment, par exemple &agrave; travers des rencontres, des clients d&eacute;j&agrave; acquis qui s&rsquo;exportent eux-m&ecirc;mes, des sollicitations&hellip;etc.<br /><br /><br /></li>
                    <li><strong>A travers une strat&eacute;gie de d&eacute;veloppement rationalis&eacute;e<br /><br /></strong>L&rsquo;entreprise d&eacute;cide sciemment de se d&eacute;velopper &agrave; l&rsquo;international. Dans ce cas, elle a besoin de s&eacute;lectionner un ou plusieurs pays/march&eacute;s dans lesquels se d&eacute;velopper. Pour l&rsquo;aider et rationaliser son choix de pays/march&eacute;, l&rsquo;entreprise s&rsquo;appuie alors sur un ensemble de crit&egrave;res quantitatifs et qualitatifs.</li>
                    </ol>
                    <p>&nbsp;</p>
                    <p>Vous trouverez ci-dessous <strong>une liste non-exhaustive </strong>d&rsquo;indicateurs pouvant &ecirc;tre utilis&eacute;s pour d&eacute;terminer si un pays/march&eacute; est attractif :</p>
                    <p>&nbsp;</p>
                    <ol>
                    <li><strong>Indicateurs g&eacute;n&eacute;raux<br /><br /></strong><strong>Indicateurs macro&eacute;conomiques </strong>: PIB, croissance du PIB, commerce, inflation, inclusion dans le commerce mondial, niveau d&rsquo;efficacit&eacute; des infrastructures de transport, niveau de facilit&eacute; d&rsquo;importations, etc<br /><br /><strong>Indicateurs d&eacute;mographiques et sociaux </strong>: Population, r&eacute;partition des revenus, indicateurs de classes moyennes, part de la population urbaine<br /><br /><strong>Indicateurs g&eacute;opolitiques </strong>: Climat des affaires, indicateurs de stabilit&eacute; politique, relations diplomatiques et politiques avec le Maroc<br /><br /><strong>Donn&eacute;es de consommation </strong>: Consommation des m&eacute;nages par grands postes, indices de prix des grands postes<br /><br /><br /></li>
                    <li><strong>Indicateurs de modes de consommation<br /><br /></strong>Insights sur la demande :</li>
                    </ol>
                    <ul>
                    <li>Principaux usages des produits, sp&eacute;cificit&eacute;s culturelles, fr&eacute;quence d&rsquo;utilisation, produits de substitution, degr&eacute; de p&eacute;n&eacute;tration des produits &eacute;tudi&eacute;s et notori&eacute;t&eacute; des marques existantes</li>
                    <li>Comportements et drivers d&rsquo;achats par segment de clients et par cat&eacute;gorie produit</li>
                    <li>Attentes des consommateurs vis-&agrave;-vis de nouveaux produits ou nouveaux entrants</li>
                    <li>Evolution des tendances de consommation</li>
                    </ul>
                    <ul>
                    <li><strong>Indicateurs d&rsquo;attractivit&eacute; de march&eacute;</strong></li>
                    </ul>
                    <p>&nbsp;</p>
                    <ul>
                    <li>Estimation des march&eacute;s :</li>
                    <li>Taille en volume et en valeur, rythme de croissance et part de l&rsquo;informel par cat&eacute;gorie et le cas &eacute;ch&eacute;ant par sous segments</li>
                    <li>Perspectives futures</li>
                    </ul>
                    <ul>
                    <li>Niveaux de prix :</li>
                    <li>Perspectives futures</li>
                    </ul>
                    <p>&nbsp;</p>
                    <p><strong>&nbsp; &nbsp; 3. Indicateurs concurrentiels<br /><br /></strong>&nbsp; Profil des principaux acteurs de chaque march&eacute; :</p>
                    <ul>
                    <li>Dynamique de d&eacute;veloppement : croissance organique, croissance externe, nouveaux entrants, acteurs internationaux / r&eacute;gionaux, Actionnariat, donn&eacute;es financi&egrave;res, sant&eacute; financi&egrave;re</li>
                    <li>Couverture g&eacute;ographique</li>
                    <li>Portefeuille produits / marques et positionnement</li>
                    <li>Parts de march&eacute;</li>
                    <li>Business model</li>
                    </ul>
                    <p><strong>A retenir </strong>: <strong>Outre les indicateurs g&eacute;n&eacute;raux ci-dessus, vous devez &eacute;galement d&eacute;terminer quels sont les indicateurs sp&eacute;cifiques &agrave; votre sp&eacute;cialit&eacute; qui vous aideront &agrave; &eacute;valuer l&rsquo;attractivit&eacute; d&rsquo;un pays/march&eacute;. Par exemple, dans le cas d&rsquo;une entreprise de Retail, vous pouvez choisir d&rsquo;adopter des indicateurs tels que la force et la perception des marques concurrentes, le niveau de maturit&eacute; des infrastructures de logistique, les marges capt&eacute;es aux diff&eacute;rentes &eacute;tapes de la cha&icirc;ne de valeur, etc.</strong></p>
                    <p>&nbsp;</p>
                    <h5>2. COMMENT &Eacute;VALUER UNE OPPORTUNIT&Eacute; COMMERCIALE DANS UN MARCH&Eacute; DONN&Eacute; ?</h5>
                    <p><strong>Mon produit peut-il entrer dans le pays ?</strong></p>
                    <p>Le co&ucirc;t r&eacute;el de vos produits &agrave; l&rsquo;export ne s&rsquo;arr&ecirc;te pas au seul co&ucirc;t de production. Vous devez prendre en compte le fait que plusieurs obstacles r&eacute;glementaires, techniques, logistiques ou encore financiers viennent compliquer le calcul initial.</p>
                    <p>Retrouvez ici-bas quelques questions &agrave; se poser lorsque vous &eacute;tudiez la possibilit&eacute; d&rsquo;exporter :</p>
                    <ul>
                    <li><strong>Suis-je autoris&eacute; &agrave; exporter </strong>mon produit dans ce march&eacute; ?</li>
                    </ul>
                    <p>Existe-t-il des r&eacute;glementations locales qui m&rsquo;emp&ecirc;chent d&rsquo;exporter mon produit ?</p>
                    <ul>
                    <li><strong>Existe-t-il un accord commercial </strong>entre le Maroc et mon march&eacute;-cible ?</li>
                    </ul>
                    <p>Puis-je b&eacute;n&eacute;ficier commercialement ou techniquement d&rsquo;un &eacute;ventuel accord bilat&eacute;ral (baisse des tarifs &agrave; l&rsquo;importation, conformit&eacute; des produits&hellip;etc.) ?</p>
                    <ul>
                    <li><strong>Quels sont les co&ucirc;ts li&eacute;s aux proc&eacute;dures d&rsquo;exportation </strong>sp&eacute;cifiques &agrave; ce pays (tarifs, d&eacute;douanement, logistique, stockage&hellip;etc.) ? <strong>Quel est le prix de mon produit/service avant marge ? En prenant en compte tous les surco&ucirc;ts ?</strong></li>
                    </ul>
                    <p><strong>O&ugrave; trouver des informations sur les march&eacute;s cible ?</strong></p>
                    <p>Pour obtenir ce type d&rsquo;information, vous pouvez vous rendre sur les sites web suivants :</p>
                    <ul>
                    <li>AMDIE : <a href="http://www.amdie.gov.ma/" target="_blank" rel="noopener">Agence Marocaine de D&eacute;veloppement des Investissement et des Exportations</a></li>
                    <li>ASMEX : <a href="http://asmex.org/" target="_blank" rel="noopener">ASMEX &ndash; Association Marocaine des Exportateurs</a></li>
                    <li>MCINET Minist&egrave;re du Commerce ext&eacute;rieur : <a href="http://www.mcinet.gov.ma/ce/" target="_blank" rel="noopener">Minist&egrave;re du Commerce Ext&eacute;rieur</a></li>
                    </ul>
                    <p>&nbsp;</p>
                    <h5>3. COMMENT TROUVER DES PARTENAIRES D&rsquo;AFFAIRES &Agrave; L&rsquo;&Eacute;TRANGER ?</h5>
                    <p>Si vous souhaitez importer ou exporter, la premi&egrave;re &eacute;tape est souvent la <strong>recherche de partenaires</strong></p>
                    <p><strong>: </strong>clients, revendeurs, fournisseurs.</p>
                    <p>Si vous souhaitez vous faire aider dans votre d&eacute;marche de recherche de partenaires, vous pouvez adh&eacute;rer &agrave; des organismes qui vous mettront en contact avec des acteurs &agrave; l&rsquo;&eacute;tranger :</p>
                    <ul>
                    <li>Chambres de commerce r&eacute;gionales</li>
                    <li>Chambres de commerce internationales (dont CFCIM)</li>
                    <li>F&eacute;d&eacute;rations sectorielles</li>
                    </ul>
                    <p>Certains acteurs peuvent &eacute;galement r&eacute;aliser pour vous des missions de prospection sur mesure :</p>
                    <ul>
                    <li>Chambres de commerce internationales (dont CFCIM)</li>
                    <li>Maroc Export</li>
                    <li>Cabinets de prospection priv&eacute;s</li>
                    </ul>
                    <p>Ces missions de prospection consistent en des <strong>voyages d&rsquo;affaires </strong>avec des rencontres organis&eacute;es et des visites en entreprise afin de vous permettre de rencontrer des partenaires potentiels. En ce qui concerne le prix, <strong>les forfaits d&eacute;pendent du pays et de l&rsquo;acteur</strong>. Ils incluent en g&eacute;n&eacute;ral transports et h&ocirc;tels. Par exemple : pour un voyage de 5 jours au S&eacute;n&eacute;gal tous frais pay&eacute;s, la CFCIM facture 31500 dhs/personne.</p>
                    <p>&nbsp;</p>
                    <p>&nbsp;</p>
                    <h5>4. COMMENT DISTRIBUER MES PRODUITS &Agrave; L&rsquo;&Eacute;TRANGER ?</h5>
                    <p>La distribution de ses produits et/ou services &agrave; l&rsquo;&eacute;tranger pose la question primordiale du <strong>mode de pr&eacute;sence de l&rsquo;entreprise </strong>&agrave; l&rsquo;&eacute;tranger. Le choix du mode de pr&eacute;sence d&eacute;pend de plusieurs facteurs, parmi lesquels les plus importants &agrave; retenir sont :</p>
                    <ul>
                    <li>Les <strong>capacit&eacute;s </strong>de l&rsquo;entreprise (financi&egrave;res et humaines) ;</li>
                    <li>La <strong>strat&eacute;gie de d&eacute;veloppement </strong>international (d&eacute;veloppement long-terme / court-terme, en propre / en partenariat&hellip;etc.);</li>
                    <li>La l&eacute;gislation du pays</li>
                    </ul>
                    <p>Chaque entreprise <strong>doit ainsi choisir au cas par cas </strong>soit de passer par des interm&eacute;diaires, soit de s&rsquo;implanter dans le pays cible.</p>
                    <p>&nbsp;</p>
                    <p>&nbsp;</p>
                    <h5>5. QUELS INTERM&Eacute;DIAIRES POUR DISTRIBUER MES PRODUITS &Agrave; L&rsquo;&Eacute;TRANGER ?</h5>
                    <p>Plusieurs solutions existent pour ce faire, dont notamment :</p>
                    <ol>
                    <li><strong>Agent commercial</strong></li>
                    </ol>
                    <p><strong>Description: </strong>Mandataire charg&eacute;, de fa&ccedil;on permanente et ind&eacute;pendante, de n&eacute;gocier et &eacute;ventuellement de conclure des contrats de vente, d&rsquo;achat, de location ou de prestations de services, au nom et pour le compte d&rsquo;autres entreprises.</p>
                    <p>Avantages:</p>
                    <ul>
                    <li><strong>Ma&icirc;trise de la politique commerciale </strong>(prix, communication&hellip;) ; l&rsquo;agent ne fait qu&rsquo;appliquer les directions</li>
                    <li><strong>Connaissance du march&eacute; </strong>par l&rsquo;agent qui poss&egrave;de g&eacute;n&eacute;ralement un carnet d&rsquo;adresses d&eacute;j&agrave; existant</li>
                    <li><strong>Co&ucirc;ts limit&eacute;s </strong>: l&rsquo;agent est r&eacute;mun&eacute;r&eacute; &agrave; la commission sur chaque contrat r&eacute;alis&eacute;</li>
                    </ul>
                    <p>Inconv&eacute;nients:</p>
                    <ul>
                    <li><strong>Gestion des t&acirc;ches administrative</strong>s, logistiques, financi&egrave;res et commerciales par l&rsquo;entreprise</li>
                    <li>Succ&egrave;s commercial d&eacute;pendant fortement des <strong>qualit&eacute;s de l&rsquo;agent </strong>(risque de contractualiser un agent peu efficace)</li>
                    <li><strong>Risque d&rsquo;appropriation de la client&egrave;le par l&rsquo;agent </strong>: en cas de rupture de contrat avec l&rsquo;agent, l&rsquo;entreprise pourrait perdre sa client&egrave;le</li>
                    <li>Int&eacute;r&ecirc;t de l&rsquo;agent potentiellement plus port&eacute; sur les <strong>profits &agrave; court-terme </strong>que sur la rentabilit&eacute; &agrave; long- terme</li>
                    </ul>
                    <p>&nbsp;</p>
                    <p><strong>&nbsp; &nbsp; 2. Importateur &ndash; Distributeur<br /><br /></strong><strong>Description </strong>: L&rsquo;importateur ou distributeur est un commer&ccedil;ant ind&eacute;pendant &eacute;tranger qui ach&egrave;te ferme les produits de l&rsquo;exportateur et qui ensuite les revend &agrave; son nom et pour son propre compte, donc &agrave; ses risques et profits. Il se r&eacute;mun&egrave;re en pr&eacute;levant une marge b&eacute;n&eacute;ficiaire.</p>
                    <p><strong>Avantages:</strong></p>
                    <p>&nbsp;</p>
                    <ul>
                    <li>T&acirc;ches administratives, logistiques&hellip;etc. <strong>assum&eacute;es par le distributeur</strong></li>
                    <li>Risque de <strong>non-paiement </strong>par les clients finaux <strong>assum&eacute; par le distributeur</strong></li>
                    <li><strong>Connaissance du march&eacute; </strong>par l&rsquo;importateur &ndash; distributeur</li>
                    </ul>
                    <p>&nbsp;</p>
                    <p><strong>Inconv&eacute;nients:</strong></p>
                    <ul>
                    <li><strong>Perte totale du contr&ocirc;le de la politique commerciale </strong>: le distributeur ma&icirc;trise seul les prix finaux, la communication&hellip;etc.</li>
                    <li><strong>D&eacute;pendance vis-&agrave;-vis des performances </strong>/ qualit&eacute;s du distributeur</li>
                    <li>Marge de l&rsquo;entreprise r&eacute;duite</li>
                    </ul>
                    <p>&nbsp;</p>
                    <p><strong>Concession</strong></p>
                    <p><strong>Description </strong>: Accord entre deux entreprises, l&rsquo;une c&eacute;dant &agrave; l&rsquo;autre la location d&rsquo;une enseigne/ marque et la garantie d&rsquo;une exclusivit&eacute; territoriale pour le concessionnaire (appel&eacute; aussi parfois importateur exclusif)</p>
                    <p>&nbsp;</p>
                    <p><strong>Avantages:</strong></p>
                    <ul>
                    <li>Tous les avantages de l&rsquo;importateur-distributeur (ci-dessus)</li>
                    <li><strong>Une meilleure ma&icirc;trise de la politique commerciale </strong>: le contrat de concession impose une certaine dose de collaboration</li>
                    </ul>
                    <p><strong>Inconv&eacute;nients:</strong></p>
                    <ul>
                    <li>Tous les inconv&eacute;nients de l&rsquo;importateur-distributeur (ci-dessus)</li>
                    </ul>
                    <p>&nbsp;</p>
                    <p>&nbsp;</p>
                    <p><strong>&nbsp; &nbsp; 3. Franchise</strong></p>
                    <p><strong>Description </strong>: Accord commercial et juridique par lequel une entreprise appel&eacute;e &ldquo;franchiseur&rdquo; s&rsquo;engage pour une r&eacute;mun&eacute;ration &agrave; fournir &agrave; une seconde entreprise, dite &ldquo;franchis&eacute;e&rdquo; :</p>
                    <ul>
                    <li>une marque,</li>
                    <li>un savoir-faire</li>
                    <li>une assistance permanente.</li>
                    </ul>
                    <p>Le terme &laquo; franchise &raquo; ne s&rsquo;applique que si les trois conditions pr&eacute;c&eacute;dentes sont r&eacute;unies.</p>
                    <p><strong>Avantages:</strong></p>
                    <ul>
                    <li>D&eacute;veloppement commercial et de la marque <strong>rapide</strong></li>
                    <li>Investissements immobiliers <strong>support&eacute;s par les franchis&eacute;s</strong></li>
                    <li><strong>Conservation de la propri&eacute;t&eacute; de la marque et du savoir-faire </strong>par le franchiseur</li>
                    <li><strong>Partage du risque d&rsquo;exportation </strong>entre franchiseurs et franchis&eacute;s</li>
                    </ul>
                    <p><strong>Contr&ocirc;le par le franchiseur de la politique commerciale </strong>(prix, communication&hellip;) ; gagne de plus d&rsquo;informations sur le march&eacute;</p>
                    <p><strong>Inconv&eacute;nients:</strong></p>
                    <p>&nbsp;</p>
                    <ul>
                    <li>Risque que les franchis&eacute;s <strong>rompent le contrat et imitent le mod&egrave;le du franchiseur</strong></li>
                    <li><strong>D&eacute;pendance partielle de la performance commerciale vis-&agrave;-vis des franchis&eacute;s </strong>: le processus de s&eacute;lection des franchis&eacute;s est importante</li>
                    <li><strong>Gestion du r&eacute;seau de franchis&eacute; complexe </strong>: organiser, g&eacute;rer, animer&hellip;etc. La gestion du r&eacute;seau n&eacute;cessite des comp&eacute;tences sp&eacute;cifiques.</li>
                    </ul>
                    <p>&nbsp;</p>
                    <h5>6. COMMENT OUVRIR DES REPR&Eacute;SENTATIONS DANS LES PAYS CIBLES?</h5>
                    <ol>
                    <li><strong>Bureau de repr&eacute;sentation</strong></li>
                    </ol>
                    <p><strong>Description </strong>: Forme d&rsquo;implantation directe qui ne dispose pas de personnalit&eacute; morale, juridique et fiscale propre, au contraire de la filiale.</p>
                    <p>Avantages:</p>
                    <ul>
                    <li>G&eacute;n&eacute;ralement utilis&eacute;e pour <strong>contourner des r&egrave;gles juridiques ou techniques contraignantes</strong></li>
                    <li><strong>Ma&icirc;trise </strong>de la politique commerciale</li>
                    <li><strong>Captation de la totalit&eacute; des b&eacute;n&eacute;fices </strong>(financiers mais &eacute;galement connaissance du march&eacute;)</li>
                    <li><strong>Plus grande confiance des clients </strong>quant &agrave; la pr&eacute;sence sur le long-terme de l&rsquo;entreprise</li>
                    </ul>
                    <p>Inconv&eacute;nients:</p>
                    <ul>
                    <li><strong>Complexit&eacute; </strong>des t&acirc;ches administratives</li>
                    <li><strong>Risques int&eacute;gralement port&eacute;s par la maison-m&egrave;re </strong>(financiers, commerciaux, juridiques&hellip;etc.)</li>
                    <li>Investissement financier <strong>important</strong></li>
                    </ul>
                    <p>&nbsp;</p>
                    <p><strong>&nbsp; &nbsp; &nbsp;2. La filiale</strong></p>
                    <p>&nbsp;</p>
                    <p><strong>Description </strong>: Soci&eacute;t&eacute; de nationalit&eacute; locale avec une personnalit&eacute; juridique ind&eacute;pendante de sa maison m&egrave;re mais contr&ocirc;l&eacute;e majoritairement par celle-ci.</p>
                    <p>Mode de p&eacute;n&eacute;tration le plus complet d&rsquo;un march&eacute; &eacute;tranger : au-del&agrave; d&rsquo;une implantation locale permanente, la filiale dispose d&rsquo;une grande autonomie et a plus de responsabilit&eacute;s que le bureau de repr&eacute;sentation <strong>Avantages :</strong></p>
                    <ul>
                    <li><strong>Connaissance </strong>du march&eacute;</li>
                    <li>Contr&ocirc;le de la politique commerciale</li>
                    <li><strong>Possibilit&eacute;s d&rsquo;&eacute;conomies d&rsquo;&eacute;chelle </strong>en optimisant les op&eacute;rations logistiques, administratives&hellip;etc. avec la maison-m&egrave;re</li>
                    <li>Responsabilit&eacute; et risques pour la maison-m&egrave;re <strong>limit&eacute;s &agrave; hauteur de sa part dans le capital</strong></li>
                    <li><strong>Flexibilit&eacute; des formes que peut prendre la filiale &agrave; sa cr&eacute;ation </strong>(selon les besoins) : cr&eacute;ation ou rachat d&rsquo;entreprise, joint-venture avec une autre entreprise</li>
                    </ul>
                    <p>Inconv&eacute;nients:</p>
                    <ul>
                    <li><strong>Investissement de d&eacute;part et co&ucirc;ts fixes tr&egrave;s &eacute;lev&eacute;s </strong>par rapport aux autres solutions</li>
                    <li><strong>Engagement de long-terme </strong>: le retour sur investissement mettra probablement plusieurs ann&eacute;es &agrave; se mat&eacute;rialiser</li>
                    <li>Contr&ocirc;le de l&rsquo;entit&eacute; <strong>complexe </strong>par rapport aux autres formes (reporting, risques, conformit&eacute;&hellip;etc.)</li>
                    </ul>
                    <ul>
                    <li><strong>La Joint-Venture (JV)</strong></li>
                    </ul>
                    <p>&nbsp;</p>
                    <p><strong>Description </strong>: Accord entre deux entreprises partenaires &eacute;tablies dans des pays diff&eacute;rents qui souhaitent cr&eacute;er ou acqu&eacute;rir conjointement une filiale sur le march&eacute; du partenaire &eacute;tranger. La coop&eacute;ration est envisag&eacute;e sur le long terme.</p>
                    <p>Il existe souvent une relation compl&eacute;mentaire entre la joint-venture et les maisons m&egrave;res. Par exemple:</p>
                    <ul>
                    <li>La recherche et d&eacute;veloppement en commun</li>
                    <li>L&rsquo;assemblage par la joint-venture de produits issus des maisons m&egrave;res</li>
                    <li>La sous-traitance d&rsquo;une partie de la production des maisons m&egrave;res</li>
                    </ul>
                    <p>Avantages:</p>
                    <ul>
                    <li><strong>B&eacute;n&eacute;fices tir&eacute;s des forces/avantages du partenaire </strong>(r&eacute;seau de distribution, ressources humaines et financi&egrave;res, force commerciale&hellip;etc)</li>
                    <li><strong>Partage au moins th&eacute;orique des risques </strong>financiers et non-financiers</li>
                    <li>B&eacute;n&eacute;fices tir&eacute;s de la <strong>marque locale </strong>de l&rsquo;entreprise partenaire (peut lever certaines barri&egrave;res culturelles)</li>
                    </ul>
                    <p>Inconv&eacute;nients:</p>
                    <ul>
                    <li><strong>Partage </strong>des b&eacute;n&eacute;fices</li>
                    <li>Co&ucirc;ts de cr&eacute;ation et de dissolution <strong>&eacute;lev&eacute;s</strong></li>
                    <li><strong>Risque de m&eacute;sentente </strong>entre les deux entreprises</li>
                    <li><strong>Nouveaux risques </strong>: le risque de vol du savoir-faire et/ou de la propri&eacute;t&eacute; intellectuelle, le risque de conflit d&rsquo;int&eacute;r&ecirc;ts&hellip;etc.</li>
                    </ul>
                    <p>&nbsp;</p>
                    <h5>7. O&Ugrave; TROUVER LES INFORMATIONS RELATIVES &Agrave; LA DOUANE ?</h5>
                    <p>Avant d&rsquo;engager une d&eacute;marche d&rsquo;importation ou d&rsquo;exportation, il est notamment important de vous renseigner sur :</p>
                    <ol>
                    <li><strong>Les tarifs g&eacute;n&eacute;raux de douane</strong></li>
                    </ol>
                    <p>&nbsp;</p>
                    <ul>
                    <li>A l&rsquo;import :</li>
                    </ul>
                    <p>R&eacute;f&eacute;rez-vous au site officiel de l&rsquo;ADII (Administration des Douanes et Imp&ocirc;ts Indirects) qui propose une nomenclature des produits avec pour chacun le r&eacute;gime douanier en vigueur : <a href="http://www.douane.gov.ma/web/guest/nos-bases-legislatives-et-reglementaires">Administration des Douanes et</a> <a href="http://www.douane.gov.ma/web/guest/nos-bases-legislatives-et-reglementaires">Imp&ocirc;ts Indirects</a></p>
                    <ul>
                    <li>A l&rsquo;export : r&eacute;f&eacute;rez-vous &agrave; la douane de votre pays-cible.</li>
                    </ul>
                    <p>&nbsp;</p>
                    <p><strong>&nbsp; &nbsp; &nbsp;2. Tarifs g&eacute;n&eacute;raux d&rsquo;importation</strong></p>
                    <p>Il est difficile de faire ressortir des tarifs moyens tant les r&eacute;gimes d&rsquo;exception par produits et par pays sont nombreux. N&eacute;anmoins, dans le cadre du <strong>r&eacute;gime de droit commun</strong>, on peut recenser les taux suivants :</p>
                    <ul>
                    <li><strong>25% pour les produits finis de consommation </strong>qui risquent de concurrencer les articles similaires produits au niveau local : les v&eacute;hicules industriels, les v&ecirc;tements confectionn&eacute;s, les pneumatiques, les produits de c&eacute;ramique, les ouvrages en fonte et acier, les meubles ainsi que certains appareils d&rsquo;&eacute;clairage</li>
                    </ul>
                    <ul>
                    <li><strong>17,5% pour les &ldquo;demi-produits&rdquo; </strong>: on retrouve principalement dans cette cat&eacute;gorie les v&eacute;hicules de tourisme</li>
                    <li><strong>10% pour les produits semi-ouvr&eacute;s </strong>: certains types de bois, les tissus et certaines mati&egrave;res plastiques</li>
                    <li><strong>2,5% pour les intrants </strong>(tels les produits &eacute;nerg&eacute;tiques et les engrais), les biens d&rsquo;&eacute;quipement ainsi que les mat&eacute;riels et outillages, &agrave; condition que ces produits ne soient pas fabriqu&eacute;s localement. La liste comprend &eacute;galement les produits chimiques, les v&eacute;hicules import&eacute;s d&eacute;mont&eacute;s, les fils textiles et les produits pharmaceutiques non produits localement</li>
                    </ul>
                    <p>&nbsp;</p>
                    <h5>8. VERS QUELS ACTEURS SE TOURNER POUR ASSURER LA LOGISTIQUE DE MES OP&Eacute;RATIONS D&rsquo;IMPORT/EXPORT ?</h5>
                    <p>Vous pouvez vous faire assister dans votre logistique par les acteurs suivants afin d&rsquo;exporter et importer vos produits :</p>
                    <ol>
                    <li><strong>Les transitaires : contact privil&eacute;gi&eacute; pour les questions douani&egrave;res et de </strong></li>
                    </ol>
                    <p>Il peut rev&ecirc;tir plusieurs statuts selon le type de contrat qui le lie &agrave; son client :</p>
                    <ul>
                    <li>S&rsquo;il est li&eacute; &agrave; son client par un contrat de mandat (<strong>transitaire mandataire</strong>), il agit sur les instructions de son client mais garde une fonction de conseil en mati&egrave;re de stockage, r&eacute;exp&eacute;dition ou d&eacute;douanement de la marchandise</li>
                    <li>S&rsquo;il est li&eacute; &agrave; son client par un contrat de commission (<strong>transitaire commissionnaire</strong>) : il organise de fa&ccedil;on libre et autonome la totalit&eacute; du transport pour le compte de l&rsquo;exp&eacute;diteur ainsi que la d&eacute;claration en d&eacute;tail des marchandises en douane</li>
                    </ul>
                    <p>La liste des transitaires agr&eacute;&eacute;s par r&eacute;gion est accessible sur le site de la douane marocaine : <a href="http://www.douane.gov.ma/web/guest/listetransitaires">Administration</a> <a href="http://www.douane.gov.ma/web/guest/listetransitaires">des Douanes et Imp&ocirc;ts Indirects</a></p>
                    <ol>
                    <li><strong>Les courtiers en douane : interm&eacute;diaire entre le client et l&rsquo;administration douani&egrave;re.</strong></li>
                    </ol>
                    <p>Il assiste pour toutes les formalit&eacute;s douani&egrave;res &agrave; effectuer. A noter que la plupart des transitaires poss&egrave;dent dans leurs &eacute;quipes des courtiers en douane.</p>
                    <p>Vous avez &eacute;galement la possibilit&eacute; de sous-traiter vos op&eacute;rations, en particulier &agrave; l&rsquo;export :</p>
                    <ul>
                    <li><strong>Les SGE (Soci&eacute;t&eacute; de Gestion d&rsquo;Export)</strong></li>
                    </ul>
                    <p>soci&eacute;t&eacute;s de services ind&eacute;pendantes qui remplissent l&rsquo;ensemble des fonctions du service export d&rsquo;une entreprise, dans le cadre d&rsquo;un contrat de mandat.</p>
                    <ul>
                    <li><strong>Les SCI (Soci&eacute;t&eacute; de Commerce International)</strong></li>
                    </ul>
                    <p>&eacute;galement appel&eacute;es &ldquo;soci&eacute;t&eacute;s d&rsquo;import-export&rdquo; ou tout simplement &ldquo;n&eacute;gociants&rdquo;, ces soci&eacute;t&eacute;s ach&egrave;tent pour leurs propres comptes des produits appartenant &agrave; d&rsquo;autres entreprises et les revendent ensuite en leur nom propre sur les march&eacute;s &eacute;trangers en y appliquant la marge voulue.</p>
                    <ul>
                    <li><strong>Les courtiers</strong></li>
                    </ul>
                    <p>simples interm&eacute;diaires non propri&eacute;taires dont le r&ocirc;le consiste &agrave; mettre en rapport vendeur et acheteur.</p>
                    <p>&nbsp;</p>
                    <h5>9. QUELLES SONT LES PROC&Eacute;DURES ADMINISTRATIVES D&rsquo;IMPORT/EXPORT DEPUIS LE MAROC</h5>
                    <p>Le Minist&egrave;re de l&rsquo;Industrie et du Commerce &eacute;dite le <a href="https://www.mcinet.gov.ma/fr/content/guide-de-loperateur-du-commerce-exterieur-1" target="_blank" rel="noopener">Guide de l&rsquo;Op&eacute;rateur du Commerce Ext&eacute;rieur, </a>dans lequel vous trouverez toutes les proc&eacute;dures d&eacute;taill&eacute;es relatives &agrave; l&rsquo;importation comme &agrave; l&rsquo;exportation ainsi que des contacts dans les administrations.</p>
                    <p><strong>Bon &agrave; savoir </strong>: <strong>les pi&egrave;ces &agrave; fournir et d&eacute;marches peuvent &eacute;voluer et l&rsquo;information n&rsquo;est pas toujours disponible sur Internet. Il est donc recommand&eacute; de contacter directement le Minist&egrave;re du Commerce Ext&eacute;rieur pour obtenir des renseignements &agrave; jour.</strong></p>
                    <p>&nbsp;</p>
                    <h5>10. COMMENT G&Eacute;RER LES RISQUES LI&Eacute;S &Agrave; L&rsquo;EXPORTATION ?</h5>
                    <ol>
                    <li><strong>Le risque cr&eacute;dit (ou commercial)</strong></li>
                    </ol>
                    <p><strong>Qu&rsquo;est que c&rsquo;est ? </strong>Le risque cr&eacute;dit ou commercial est le risque que le d&eacute;biteur (le client) soit d&eacute;faillant au moment du paiement. Cette d&eacute;faillance g&eacute;n&egrave;re des impay&eacute;s qui affectent la tr&eacute;sorerie, voire les revenus de l&rsquo;entreprise.</p>
                    <p><strong>Comment g&eacute;rer ce risque ? </strong>Il est primordial de se renseigner au maximum sur la capacit&eacute; du client &agrave; payer : est-il solvable ? Est-il s&eacute;rieux ? Comment paie-t-il ses autres fournisseurs ? Respecte-t-il ses engagements ?</p>
                    <p>Quels outils peuvent-ils m&rsquo;aider &agrave; g&eacute;rer ce risque ?</p>
                    <ul>
                    <li>Le <strong>paiement &agrave; la commande : </strong>obtenir que l&rsquo;acheteur paie 100% de la facture d&egrave;s l&rsquo;&eacute;mission de la facture pro-forma par le vendeur. Le vendeur livre apr&egrave;s r&eacute;ception du paiement.</li>
                    </ul>
