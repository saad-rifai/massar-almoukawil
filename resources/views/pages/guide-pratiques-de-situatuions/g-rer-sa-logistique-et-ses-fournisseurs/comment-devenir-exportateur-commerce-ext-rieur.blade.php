                    <ol>
                    <li><strong>Le risque politique</strong></li>
                    </ol>
                    <p>&nbsp;</p>
                    <p><strong>Qu&rsquo;est que c&rsquo;est ? </strong>Tout &eacute;v&eacute;nement valant force majeure pour le d&eacute;biteur (acheteur) et qui l&rsquo;emp&ecirc;che de s&rsquo;acquitter de ses factures :</p>
                    <ul>
                    <li>&Eacute;v&eacute;nement politique (guerre, r&eacute;volution, insurrection&hellip;)</li>
                    </ul>
                    <ul>
                    <li>Calamit&eacute; (tremblement de terre, &eacute;ruption volcanique, raz-de-mar&eacute;e&hellip;)</li>
                    <li>Difficult&eacute;s &eacute;conomiques (p&eacute;nurie de devises&hellip;)</li>
                    <li>Acte, d&eacute;cision ou manquement valant d&eacute;cision arbitraire des autorit&eacute;s.</li>
                    </ul>
                    <p>Ce risque peut &eacute;galement rev&ecirc;tir la forme d&rsquo;un retrait des licences d&rsquo;importation ou des autorisations de change, d&rsquo;un embargo, de l&rsquo;instauration d&rsquo;une licence d&rsquo;importation pour des produits qui, auparavant, pouvaient &ecirc;tre import&eacute;s librement, ou de l&rsquo;absence de transfert en devises de sommes pay&eacute;es en monnaie locale.</p>
                    <p><strong>Comment g&eacute;rer ce risque ? </strong>Les recours sont tr&egrave;s difficiles &agrave; g&eacute;rer dans ce genre de situation et d&eacute;pendent des pays concern&eacute;s. Il s&rsquo;agit de bien &eacute;valuer le risque que pr&eacute;sente le pays concern&eacute;. Par exemple, plus les institutions du pays sont d&eacute;faillantes ou corrompues, plus le risque est accru.</p>
                    <p><strong>Quels outils peuvent-ils m&rsquo;aider &agrave; g&eacute;rer ce risque ? </strong>Il n&rsquo;y a pas d&rsquo;outils &agrave; proprement dit. Les grands assureurs du commerce international (Coface, Euler Hermes&hellip;etc.) proposent g&eacute;n&eacute;ralement sur leurs sites Internet des &eacute;valuations du risque politique par pays pour aider les entreprises dans leurs d&eacute;cisions.</p>
                    <ul>
                    <li><strong>Le risque de change</strong></li>
                    </ul>
                    <p><strong>Qu&rsquo;est que c&rsquo;est ? </strong>Lorsque les devises du vendeur et de l&rsquo;acheteur sont diff&eacute;rentes, toute &eacute;volution du taux de change se fait au d&eacute;triment d&rsquo;un des deux acteurs. Si la vente se r&eacute;alise avec la devise de l&rsquo;acheteur, le risque est port&eacute; par le vendeur qui peut voir le produit de sa vente fondre en cas de d&eacute;valuation de la devise du client.</p>
                    <p><strong>Comment g&eacute;rer ce risque ? </strong>L&rsquo;id&eacute;al pour l&rsquo;entreprise est d&rsquo;utiliser sa propre devise lors de la r&eacute;daction du contrat. Toutefois, cet &eacute;l&eacute;ment est &agrave; n&eacute;gocier avec l&rsquo;acheteur.</p>
                    <p>Quels outils peuvent-ils m&rsquo;aider &agrave; g&eacute;rer ce risque ?</p>
                    <p>Le risque de change peut &ecirc;tre att&eacute;nu&eacute; voire supprim&eacute; gr&acirc;ce &agrave; des assurances ou des options de vente sur la devise de transaction.</p>
                    <ol>
                    <li><strong>Le risque li&eacute; au transport de marchandises</strong></li>
                    </ol>
                    <p>&nbsp;</p>
                    <p><strong>Qu&rsquo;est que c&rsquo;est ? </strong>Les Incoterms d&eacute;terminent les obligations r&eacute;ciproques du vendeur et de l&rsquo;acheteur. Ils d&eacute;terminent notamment le moment de transfert de la responsabilit&eacute; des marchandises. L&rsquo;incoterm d&eacute;fini lors de la n&eacute;gociation commerciale a une incidence majeure sur la responsabilit&eacute; du vendeur dans la d&eacute;livrance de la marchandise.</p>
                    <p><strong>Comment g&eacute;rer ce risque ? </strong>Il existe plusieurs Incoterms. Lors de la n&eacute;gociation commerciale, il s&rsquo;agit pour l&rsquo;entreprise d&rsquo;obtenir l&rsquo;Incoterm qui lui est le plus avantageux. Cela d&eacute;pendra donc des capacit&eacute;s de l&rsquo;entreprise, de son pouvoir de n&eacute;gociation et du niveau de s&eacute;curit&eacute; qu&rsquo;elle souhaite obtenir.</p>
                    <p><strong>Quels outils peuvent-ils m&rsquo;aider &agrave; g&eacute;rer ce risque ? </strong>Il est fortement recommand&eacute; d&rsquo;assurer le transport de marchandise. Toutefois, il s&rsquo;agit d&rsquo;assurer la marchandise uniquement jusqu&rsquo;au moment de transfert de responsabilit&eacute;.</p>
                    <p>&nbsp;</p>
                    <h5>11. COMMENT ASSURER MES EXPORTATIONS/IMPORTATIONS ?</h5>
                    <p>Les op&eacute;rations li&eacute;es au commerce ext&eacute;rieur sont soumises &agrave; des risques multiples et de plusieurs natures qu&rsquo;il est important de couvrir. Voici les principales assurances li&eacute;es au commerce ext&eacute;rieur :</p>
                    <ol>
                    <li><strong>Couverture contre les risques de fluctuation</strong></li>
                    </ol>
                    <p><strong>Description </strong>: ces risques peuvent &ecirc;tre de 3 natures :</p>
                    <ul>
                    <li>Fluctuation des changes</li>
                    <li>Fluctuation des prix de certains produits de base (d&rsquo;origine agricole, mini&egrave;re ou &eacute;nerg&eacute;tique)</li>
                    <li>Fluctuation des taux d&rsquo;int&eacute;r&ecirc;t</li>
                    </ul>
                    <p><strong>A qui m&rsquo;adresser ? </strong>Aux &eacute;tablissements financiers agr&eacute;&eacute;s</p>
                    <p>&nbsp;</p>
                    <p><strong>&nbsp; &nbsp; &nbsp;2. Assurance-transport</strong></p>
                    <p>&nbsp;</p>
                    <p><strong>Description </strong>: couvrent contre les pertes ou dommages mat&eacute;riels survenant pendant le transport des marchandises</p>
                    <p>A qui m&rsquo;adresser ?</p>
                    <ul>
                    <li>&Agrave; l&rsquo;import : l&rsquo;assurance doit &ecirc;tre souscrite au Maroc</li>
                    <li>&Agrave; l&rsquo;export : l&rsquo;op&eacute;rateur peut s&rsquo;adresser &agrave; une compagnie d&rsquo;assurance &eacute;trang&egrave;re, &agrave; un agent mandataire, &agrave; un courtier mandataire ou &agrave; son transitaire</li>
                    </ul>
                    <p>&nbsp;</p>
                    <p><strong>&nbsp; &nbsp; 3. Assurance-cr&eacute;dit</strong></p>
                    <p>&nbsp;</p>
                    <p><strong>Description </strong>: garantit l&rsquo;exportateur contre ses cr&eacute;ances impay&eacute;es</p>
                    <p><strong>A qui m&rsquo;adresser ? </strong>&Agrave; la SMAEX (Soci&eacute;t&eacute; Marocaine d&rsquo;Assurance &agrave; l&rsquo;Exportation)</p>
              
