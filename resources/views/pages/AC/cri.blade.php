                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Présentation
                    </h2>
                    <p class="section_normale_text">
                        Le Centre Régional d'Investissement de la Région de Tanger-Tétouan-Al Hoceima, est un
                        établissement public doté de
                        la personnalité morale. Il a pour vocation la facilitation et l’accompagnent des investisseurs
                        dans la région.
                        Le CRI est chargé de contribuer à la mise en œuvre de la politique de l’Etat en matière de
                        développement, d’incitation
                        et de promotion des investissements et d’accompagnement global des entreprises, notamment les
                        TPE/PME.
                    </p>
                </div>

                <div class="text-containet">
                    <h2 class="blue-section-title">
                        Services
                    </h2>
                    <p class="section_normale_text">
                    <ul class="section_normale_list">
                        <li>Couvrir l’accompagnement global des entreprises dans la Région, en particulier les PME et
                            TPE. Il s’agit
                            également de contribuer à l’implémentation des stratégies relatives à la promotion de
                            l’investissement régional,
                            ainsi que les offres de développement intégré, en homogénéité avec les politiques publiques
                        </li>
                        <li> Contribuer à la planification et à la gestion de l’appui financier de l’Etat destiné aux
                            investisseurs, outre le
                            règlement amiable des conflits nés entre l’administration et les investisseurs.</li>
                        <li>Se consacrer en tant que guichet unique de l’investissement à l’échelle régionale à travers
                            le traitement intégré
                            et de bout en bout des dossiers d’investissements, à partir du dépôt des demandes jusqu’à la
                            délivrance des
                            autorisations, actes et décisions, et ce en collaboration avec les administrations et
                            institutions publiques
                            concernées.</li>
                        <li> Jouer le rôle d’interface entre l’investisseur et les administrations en veillant à la
                            simplification et à la
                            digitalisation des procédures administratives en vue de répondre aux exigences de célérité,
                            de transparence et
                            d’efficacité dans le traitement des dossiers d’investissement.</li>
                        <li>Accompagner les entreprises, notamment les PME et TPE à travers la mise en place de
                            plateformes dédiées en
                            collaboration avec les administrations et institutions publiques, afin de faciliter l’accès
                            aux données relatives
                            au foncier public, aux ressources humaines, au financement et aux opportunités de
                            partenariat.</li>
                        <li>Fournir aux entreprises le conseil et l’aide nécessaires pour surmonter les obstacles et
                            difficultés éventuelles
                            durant l’exercice de leurs activités.</li>
                        <li>Assurer le secrétariat de la Commission régionale unifiée de l’investissement (CRUI).</li>
                    </ul>
                  <br>

                    </p>
